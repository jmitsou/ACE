						<legend><b>SUMMARY</b></legend>
						
						<ol>
							<li>Counseling is a process that moves through five stages toward a desired outcome.</li>
							<li>Stage One, rapport building, encompasses the other four stages and is focused on the process goals of counseling.</li>
							<li>Process goals work to ensure that clients experience counseling as positive and affirming.</li>
							<li>Outcomes goals are the specific goals clients bring to the table.</li>
							<li>Understanding the five different types of communication helps counselors to monitor the messages they give and receive.</li>
							<li>Being judgmental, self-centered or overly emotional greatly hinders effective communication.</li>
							<li>Being accepting, attentive, and observant of non-verbal cues helps to improve communication.</li>
							<li>Effective counselors use ”I” messages and avoid ”you should” messages.</li>
						</ol>
												
