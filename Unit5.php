						<legend><b>The Problem-Management/Opportunity-Development Model</b></legend>

						<p>Gerard Egan, author of The Skilled Helper, says:</p>
							
						<p>“Helpers are effective to the degree that their clients, through client-helper interactions, are in better positions to manage their problem situations and/or develop the unused resources and opportunities of their lives more effectively.”</p>
							
						<p>The premise of counseling is to help clients manage problems and utilize resources more effectively. In the case of credit counseling, the problems center around unpaid debt. To resolve debt issues, the client and the counselor collaborate to manage repayment and, at the same time, find ways to optimize the resources available to the client.</p>
							
						<p>The model that describes the overall premise of counseling is The Problem-Management/Opportunity-Development Model. The client presents a problem and the counselor is there to facilitate a solution. However, the problem will not actually be solved by the counselor. The counselor helps the client to discover the opportunities and resources available to deal with the problem.</p>
										   					
					</fieldset>
					
					<fieldset id="space">
						<legend><b>FIVE STAGES OF COUNSELING</b></legend>
	
						<p>Counseling is a process that moves through a series of stages toward a desired outcome. There are five stages:</p>
						
						<p><b>1. Rapport Building.</b> This first step actually encompasses the entire counseling process. Rapport is the essential vehicle for effective communication.
						
						<p><b>2. Gathering Information.</b> The counselor assesses the client’s current situation by asking questions and listening actively.
						
						<p><b>3. Goal Setting.</b> Now the counselor helps the client determine the desired scenario. What do you want to accomplish?
						
						<p><b>4. Strategy Implementation.</b> Implementing the plan of action is often the most challenging stage of counseling because it requires commitment on the part of the client.
						
						<p><b>5. Follow Up.</b> Is the plan working? Were goals accomplished? You will never know unless there is follow up communication.</p>
						
						<p>Rapport building encompasses the entire counseling process from the initial interview to the final follow up. The other stages overlap each other. For example, during strategy implementation, goals may be changed. While gathering information, rapport is being established.</p>					
					</fieldset>
					
					<fieldset id="space">
						<legend><b>PROCESS GOALS AND OUTCOME GOALS</b></legend>
						
						<p>Process Goals. Credit counseling shares goals universal to all types of counseling. These are called process goals. Process goals work to ensure that the counseling process is a positive experience for the client. Are the client’s concerns being addressed? Does the client feel trust and respect? Is there a sense of completeness at the end of the session? These are the things that process goals focus on. It is the counselor’s responsibility to see that the process goals of counseling are met.</p>
						
						<p>Outcome Goals. Outcome goals are the specific goals the client brings to the table. The client communicates a presenting concern which is the initial reason for the consultation. In the case of credit counseling, this is primarily delinquent debt. Other presenting concerns may be how to cut expenses, improve credit, or avoid bankruptcy. Outcome goals are the goals determined in Stage 3 of the counseling process and it is ultimately the responsibility of the client to follow through with the necessary steps to implement those goals.</p>
						
						<b>The Human/Business Model</b>
						<p>A valuable perspective is gained when everyday interactions are seen as operating on two levels. One is the business level where tasks are completed and objectives are met. The other is the human level where an individual’s needs for attention, respect and acceptance are met. For any interaction to be positive, both levels must be addressed.</p>
						
						<p>For example, the mechanic where you took your car was very friendly and courteous toward you but failed to fix the problem. He met your human needs but not your business needs. Conversely, the mechanic down the street fixed the problem at a reasonable price but was rude and disrespectful. He took care of your business needs but not your human needs. The next time your car needs repairs, you will most likely try yet another mechanic because neither one so far has satisfied both levels of need.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>FIVE TYPES OF COMMUNICATION</b></legend>
							
						<p>Understanding the five types of communication helps counselors become aware of and monitor the messages and information they give and receive during client/counselor interaction.<p>
						
						<p><b>1. Social Communication</b> is used in getting to know individuals on a superficial level or to simply acknowledge their presence. Social communication involves many common types of questions and responses that are non-threatening. Examples of this casual small talkare ”How was your weekend?” or ”It’s a beautiful day!”Social communication avoids revealing anything personal.</p>
						
						<p><b>2. Persuasive Communication</b> is used when attempting to influence others. Counselors need to be assertive while at the same time avoiding an aggressive delivery that may make clients feel pressured. Effective counselors refrain from overtly persuasive language. Instead, they work to affect positive change for the client by objectively exploring all available options.</b>
						
						<p><b>3. Non-verbal Communication</b> occurs through gestures, body language, and facial expressions. Our eyes, mouth, hands and posture are all conductors of non-verbal communication. For this reason, in-person contact is highly advantageous. Through face-to-face contact, non-verbal signs provide cues that permit better understanding of clients and their particular needs.</b>
						
						<p><b>4. Expressive Communication</b> is used when individuals express attitudes, feelings, values, morals, as well as many other traits that reveal personality. At times, it is difficult or inappropriate to express our innermost feelings, judgments, or needs about sensitive issues.</b>
						
						<p><b>5. Cognitive communication</b> is used to transmit content or information. It may involve simple messages such as confirming an appointment or giving someone your address. Cognitive communication also includes messages that impart complex instructions and knowledge.</b>
						
						<p>Human interaction consists of a mixture of the different types of communication. For example, simple social communication like ”How are you today?” can reveal information that is cognitive in nature. The client may reveal discouragement by responding, “Same as always — behind the eight ball.” This response then provides information which is cognitively processed by the counselor who then adjusts his/her response to be more affirming and motivational.</p>
			
					</fieldset>
					
					<fieldset id="space">
						<legend><b>COMMON BARRIERS TO COMMUNICATION</b></legend>
							
						<p>At times, the communication process may be hindered by various negative attitudes or inappropriate emotional states. Below is a list of the most common ways that communication is compromised.</p>
						
						<p><b>1. Anxiety and Preoccupation.</b> Counselors who are preoccupied, anxious, agitated, or self-centered do not communicate well. Communication is a two-way process. A counselor must be attentive and focused on what is being said. Failure to respond to key words or phrases could result in loss of confidence by clients, or cause them to become irritated with the counselor. A counselor who is distracted by personal issues may react from his/her own experience rather than the professional training he/she has received, thereby giving poor and inappropriate information.
						
						<p><b>2. Needless Interruption.</b> Besides being rude and inconsiderate, needless interruptions distract the client, resulting in repetition and frustration. Professional counselors need not exhibit their knowledge to impress the client. Rather, they should allow the client to talk, responding only when necessary. Ask questions in a manner that will help the client feel at ease. Clients need the opportunity to finish their thoughts completely. Often, it is appropriate to wait 3 – 5 seconds before responding to the client. This wait time provides the client with a window of opportunity to elaborate or offer additional information without being prompted with further questioning. Once the counselor has processed that information, he/she can interject whatever pertinent responses or questions are necessary.
						
						<p><b>3. Hidden Agenda.</b> When counselors enter a conversation or meeting with special interests or attitudes not evident on the surface, the resulting communication is distorted by a hidden agenda. That’s why it is so important for counselors to maintain objectivity. Effective counselors work to develop an open and inquisitive mind — not one that’s already made up.
						
						<p><b>4. Overreacting to Emotional Words.</b> Overreaction to emotional or derogatory words can often derail effective communication. Counselors avoid using labels, slang, biased phrases, and furthermore avoid reacting to the use of such language. Clients who are upset may be very dramatic or accusatory while describing their situations. Maintaining a professional attitude while assessing the actual facts can help avoid escalation of emotions which can lead to offensive or defensive reactions on the part of the client.
						
						<p><b>5. Passing Judgments.</b> Skilled counselors assess client messages without judging, condemning, or disagreeing with them. A counselor should not use his/her position to make a value judgment about the client. Any impulse to pass judgments will derail positive communication.</b>
						
						<p><b>6. Stereotyping.</b> Stereotyping is the unjustified placement of people in categories based on broad, misinformed generalizations. Professional counselors do not base decisions on preconceived notions or stereotyped profiles.</p>
			
					</fieldset>
					
					<fieldset id="space">
						<legend><b>11 WAYS TO IMPROVE COMMUNICATION</b></legend>

						<p><b>1. Adopt an Accepting Attitude.</b> Unconditional acceptance is critical for creating a positive climate for communication. The term acceptance refers to a basic attitude of trust and positive regard toward others. Clients are more likely to express themselves openly and honestly, thus creating a better counselor/client relationship. The client will also listen more attentively to advice being given. In contrast, negative attitudes such as distrust and suspicion put the client on the defensive, making for limited and guarded communication.</p>
						
						<p><b>2. Express Yourself Clearly.</b> Counselors need to speak clearly and articulate in a manner that makes others want to listen. Being expressive, using voice inflection, and being specific and direct in the information presented will assure accurate interpretation by the listener.</p>
						
						<p><b>3. Listen for the Total Meaning.</b> Individuals communicate to express their attitudes and emotions as well as factual information. Listening for and interpreting the total meaning of client messages means attending to not only WHAT they say but HOW they say it.</p>
						
						<p><b>4. Be Physically Attentive.</b> Whether speaking or listening, a counselor must focus to communicate effectively. Being completely alert when discussing a situation with a client whether it is in person or by telephone is important. If the client is physically present in the office, a counselor must demonstrate attentive behaviors such as maintaining eye contact, sitting or standing at a comfortable distance, and making certain that posture and gestures appropriately support information. Avoid anything that distracts away from client/counselor talk.</p>
						
						<p><b>5. Observe Non-verbal Communication.</b> Body language is often a key to success with a client. Observing facial expressions, eye contact, attentiveness, the inflection in the clients voice, how close or distant the person stands or sits, are all factors which can be considered in determining whether the body language confirms or contradicts what is being communicated.</p>
						
						<p><b>6. Share Responsibility for the Communication.</b> A counselor, whether speaking or listening, should be an active participant. If uncertain about information, he/she need not be apprehensive about asking the client to explain details more clearly. Never presume understanding, but ask the client to clarify the entire meaning and to provide more active feedback. An example would be when a client says Ill get back to you soon. A counselor, rather than assuming follow-up, might suggest, “When is the best time I can reach you?” Persistence in assigning shared responsibilities for specific follow-up will ensure continued sharing of information with little or no misunderstanding.</p>
						
						<p><b>7. Be Expressive, Be Yourself.</b> Don’t feel that it is inappropriate to use your personality in business related communication. Being expressive facilitates all types of communication by building rapport and making those human connections that are a necessary part of any problem solving relationship.</p>
						
						<p><b>8. Be Assertive, Not Aggressive.</b> When counselors project assertiveness, clients gain confidence in their abilities and knowledge. Assertive counselors easily control the pace and direction of the interview. On the other hand, counselors who project aggressiveness may seem pushy, hostile or threatening to clients. The difference is putting the needs of the client first and
						foremost in mind.</p>
						
						<p><b>9. Avoid “You Should” Messages.</b> Skilled counselors avoid ”you should” messages. Such messages tend to put people on the defensive and make them more apt to disagree or want to argue with you rather than consider your suggestion.</p>
						
						<p><b>10. Use “I” Messages.</b> “I” messages are a more effective way to express ideas and options. As a counselor, you want to be able to communicate in an honest manner as to how you feel but in a way that makes the client want to listen to you. ”I” messages are helpful in expressing opinions and alternatives.</p>
						
						<p>“I” messages have four components:</p>
						<ol>
							<li>They contain suggestions which are expressed in an objective,
						non-judgmental way.</li>
							<li>They include tangible outcomes or consequences of an action taken.</li>
							<li>They communicate how the speaker feels.</li>
							<li>They include a positive solution or alternative.</li>
						
						</ol>
						
						<p><b>11. Show Empathy.</b> It is important for the client to feel that the counselor cares about their situation, feels for their circumstance, and wants to help by providing solutions. Showing empathy means listening to information the client offers voluntarily and expressing interest. This does not mean saying, “I know what youre going through.” Rather, it entails being in emotional agreement with the client. Effective counselors acknowledge and validate the way their clients feel.</p>
				
