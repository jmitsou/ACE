		<h4 id="ht">Unit 1: From Credit Cards to Credit Counseling</h4>
			<div class="row">
				<div class="col m9 l9">
					<fieldset id="space">
						<legend><b>OBJECTIVES</b></legend>
						
						<li>To gain a historical perspective on credit cards.</li>
    					<li>To explore how an insolvency event can trigger severe debt issues.</li>
    					<li>To identify destructive behaviors that fuel debt.</li>
    					
    					<p>According to the Federal Reserve, credit-card debt is the third largest source of debt after mortgage debt and student loan debt. The average credit-card debt per household in the United States is $7,281. It is estimated that over half of American households with credit cards carry a credit-card balance from month to month.</p>

						<p>Other sources report that Americans spend over one trillion dollars every year with their credit cards. They owe more than $700 billion of it. Fifteen percent of the disposable income of Americans goes to paying credit card debt.</p>

						<p>With these statistics, it may be hard to believe that the credit card as we know it today has only been around for a little more than fifty years. Within that short amount of time, however, it has made a tremendous, and often devastating, impact on personal finances. How did this explosion in credit use get started and why are so many Americans finding themselves in the credit trap?</p>
						
					</fieldset>
					
					<fieldset id="space">
					<legend><b>A SHORT HISTORY OF THE CREDIT CARD</b></legend>
					<p>Although the idea of “have now, pay later” has been around in one form or another as far back as the 1700s, the use of a credit card originated in the United States in the 1920s when oil companies and hotel chains issued them to their customers. These types of cards and the ones to follow were created for convenience while on the road.</p>

					<p>In 1946, John Biggins of the Flatbush National Bank of Brooklyn invented the “Charge-It” card. It was the first bank-issued credit card. Merchants could deposit sales slips in the bank after which the bank billed the customer who used the card.</p>
					
					<p>Then, in 1951, a fateful event occurred. Frank McNamara had dinner at a restaurant but when he went to pay his bill, he realized he had forgotten his wallet. This embarrassing situation propelled him to invent the Diners Club Card. Initially, 200 of these cards were issued for use in 27 New York restaurants. Technically, they were “charge cards” in that the entire amount had to be repaid in one payment.</p>

					<p>American Express, which began with traveler’s checks, issued its first card in 1958. As with earlier cards, it was primarily designed for on-the-road convenience. During this period, the traveling salesman was an integral part of the American business landscape.</p>
					
					<p>Later in 1958, Bank of America offered the Bank Americard within the state of California. Shortly thereafter, the name of this card was changed to VISA.</p>
					
					<p>The early 1960s saw more companies entering the credit card arena. They were still advertised primarily as time-saving rather than as a form of credit. By 1966, American Express and MasterCard became huge overnight successes marketing mainly to the traveling salesman.</p>
					
					<p>In 1970, the use of credit cards increased dramatically due to the establishment of standards for the magnetic strip. The system of a magnetic strip, originally called a stripe, had been used first by the London Transit Authority in the early 1960s.</p>
					
					<p>The United States Congress began regulating the credit-card industry in the mid 1970s. Practices such as mass mailing active cards to people who never requested them were banned.</p>
					
					<p>The pendulum swung the other way in the 1990s, however, when many restrictions on creditors were lifted. This deregulation allowed creditors to charge high late fees as well as some very high interest rates.</p>
					
					<p>In 1987, American Express offered its first card that could be paid off over time. The Discover Card, part of Sears Corporation, came after that.</p>
					
					<p>Today, it is hard to imagine life without credit cards. They provide us with convenience and a way to get what we want today and pay for it later. Merchants readily accept credit cards because they know consumers buy more on credit. In fact, the average credit-card purchase is 12 — 18% higher than if cash was used. Also, bank cards are welcomed because they are less expensive than merchant-specific cards and safer than accepting cash.</p>
					
					</fieldset>
					
					<fieldset id="space">
					<legend><b>CREDIT COUNSELING</b></legend>
					
					<p>The same year that Frank McNamara invented the first Diners Club Card, the first credit counseling agencies were established. They began as face-to-face counseling services designed to increase consumer financial literacy and help those in debt avoid bankruptcy.</p>

					<p>By the early 1990s, many agencies favored over-the-phone counseling that could reach people nationwide. Debt management plans could effectively be set up and administered through telephone, fax, and email technology. Today, there are a wide array of credit counseling agencies in the United States from local independent operators to national corporate organizations.</p>
					
					</fieldset>
					
					<fieldset id="space">
					<legend><b>DEBT SETTLEMENT</b></legend>
					
					<p>Debt settlement offers a way to avoid bankruptcy for people with significant debt issues who may not qualify for a debt management plan.</p>

					<p>A debt settlement is an agreement between a creditor and a debtor to satisfy a debt for a reduced payoff amount. Since the debtor is unable to fully meet his/her debt obligations, the creditor agrees to reduce the debt and accept the agreed upon sum as a settlement.</p>

					<p>Although the practice of settling debts through a third party began soon after the invention of money, debt settlement as a business service is less than thirty years old. Debt settlement developed in the late 1980s and early 1990s as bank deregulation, coupled with an economic recession, left many consumers facing severe financial hardships. In the late 1980s, banks set up debt settlement departments to address the high number of charge-offs generated from cardholders defaulting on loans. Bank personnel were authorized to negotiate with consumers and settle outstanding balances for payments that ranged from 25% to 65% of the amount owed. From their standpoint, something was better than nothing.</p>

					<p>By the early 1990s, independent agencies were established for the negotiation and settlement of consumer debt. Today, the debt settlement industry continues to grow as individuals and families find themselves in unresolved debt dilemmas.</p>
					
					</fieldset>
					
					<fieldset id="space">
					<legend>THE NEED FOR INTERVENTION</legend>
					
					<p>Why is there such a need for credit counselors and debt settlement specialists? Why do consumers need counseling about their use of credit? Why are consumers getting into so much trouble with their credit cards?</p>
					
					<p>One of the major debates prior to the passage of the Bankruptcy Abuse Prevention and Consumer Protection Act of 2005 was the question of who is to blame for the high levels of consumer debt one sees today. On the one hand are those who accuse consumers of spending beyond their means and not fully understanding how credit and interest works. On the other hand are those who accuse the creditors for aggressively soliciting credit to consumers who easily become trapped by excessive finance charges and late fees.</p>

					</fieldset>

					<fieldset id="space">
						<legend><b>INSOLVENCY EVENTS</b></legend>
					
					<p>More often than not, it is an insolvency event that starts a downward spiral toward debt. These often tragic but common life events include:

    				<li>Job loss</li>
    				<li>Medical emergency</li>
    				<li>Divorce</li>
    				<li>Death in the family</li>

					<p>The insolvency event often creates a panicked reaction in which credit is used as a short-term solution. As an example, let’s consider Ted.

					<p>Ted, like many Americans, has little or no savings in case of an emergency. Then, the emergency occurs; he looses his job due to corporate downsizing. With nothing to fall back on, he starts using more and more of his available credit on essentials such as groceries and car repairs. The credit-card statements start coming but he can’t pay the bills. He’s still out of work. Now the interest starts to compound and the late fees pile up. In a few more weeks, he’s considering bankruptcy.</p>

					<p>It does no good to assign blame. Ted may even consider himself to be credit savvy. He would never have dreamed of using credit to buy groceries. But what choice did he have? Plus, his available credit at the time was quite extensive. After all, he was granted credit back when he was employed. The fact that he has no income now is of little concern to the creditors.</p>
					
					<p>Suddenly, Ted finds himself with a mountain of unpaid debt. The financial strain causes marital fighting. His self esteem plummets and he finds it hard to be upbeat at job interviews.</p>
					
					</fieldset>
 
					<fieldset id="space">
					<legend><b>ECONOMIC FACTORS</b></legend>
					
					<p>Ted’s case is typical in that he had no emergency fund to fall back on when his income stopped. Like many people, he lived paycheck to paycheck. The average percent of consumer income that goes to personal savings in the United States is somewhere below 5% (June 2012). Why is it so hard for people to save money today?</p>
					
					<p>Consider the following: Housing and health care costs have increased substantially in the last fifteen years while real incomes for the vast majority of Americans have remained stagnant or have declined. According to State of Working America, the median income for 2011 was $50,054 — down from a high of $54,841 back in 2000.</p>
					
					<p>Meanwhile, the number of families spending more than 50% of their income on housing expenses grew drastically during the period from the late 1990s to the present.</p>
					
					<p>Add to this the issue of health care. According to the 2004 report of the Commonwealth Fund, 40% of Americans reported that they have gone without needed medical care due to prohibitive costs. Sixteen percent or 45.8 million people have no health care coverage whatsoever. When a medical emergency occurs, the medical bills may go unpaid for months damaging the credit rating and making any use of credit all the more costly.</p>
					
					<p>Rising housing and medical costs plus sinking incomes equal a potentially disastrous debt situation. A job loss, a mortgage payment you just can’t make, or unpaid medical bills could all spark an unhealthy use of credit. For example, if you pay a medical bill you cannot afford with a credit card, you have created new debt but unlike the medical bill, the credit card bill will increase substantially over time.</p>
					
					</fieldset>
					
					<fieldset id="space">
					<legend><b>DEREGULATION</b></legend>
					
					<p>Since the late 1970s, state usury laws prohibiting high interest rates and fees have been relaxed. Two Supreme Court rulings — one in 1978 and one in 1996 — permitted banks to charge the highest interest rate allowed in the bank’s home state. Previously, the highest interest rate was based on the highest rate allowed in the state where the consumer lives.</p>
					
					<p>Other lender practices that some contend keep consumers in debt are:</p>
					
					<p>Aggressive marketing. According to statistics published by CreditCards.com, credit-card companies mailed over 6 billion credit card offers in 2005. That’s an average of 6 offers per household per month.</p>
					
					<p>Higher credit limits. The average credit-card balance was $4,617 in 2004. Average credit lines were $3,500 on each card with an average of six cards for a total of $21,000 in available credit.</p>
					
					<p>Low minimum payments. Minimum payments have been as low as 2% of the balance on an account. Currently, they are twice that but at 4%, minimum payments are a sure way to grow your debt. Paying the minimum payment on a balance of $5000 with a 15% interest rate would take thirty-two years to pay off. In other words, you could be paying for a dinner out long into the future. Forty-five percent of American cardholders were only making the minimum payments in 2004.</p>
					
					<p>Penalties and fees. Late fees are the fastest growing source of revenue for creditors. Along with over-the-limit fees and balance transfer fees, this brought the industry 43 billion in income in 2004. Perhaps the most controversial creditor practice was universal default. Pay late on one card and your interest rate can skyrocket on all your cards. Forty-four percent of credit cards in circulation carried universal default provisions in 2004.</p>
					
					</fieldset>

					<fieldset id="space">
					<legend><b>THE BEHAVIORAL DEBTOR</b></legend>
					
					<p>Given the economic landscape and current lending trends, it is easy to see how well-intentioned consumers can get into insolvent situations. Yet, there is another force at work that contributes to delinquent consumer debt. It is how an individual behaves with his or her money. In many cases, personal financial well-being is compromised by poor money management. Here’s a short list of the common negative behaviors consumers engage in when it comes to money:</p>
					
					    <li>spending too much</li>
					    <li>saving too little</li>
					    <li>not keeping track of bills</li>
					    <li>not balancing the checkbook</li>
					    <li>not talking about spending decisions with spouse</li>
					    <li>not setting financial goals</li>
					    <li>refusing to acknowledge a need to budget</li>
					    <li>using credit cards to buy essentials</li>
					    <li>only paying the minimum on credit card balances</li>
					    <li>ignoring bill collectors</li>
					    <li>spending to alleviate stress</li>
					    <li>spending on impulse</li>
					    <li>rationalizing destructive spending</li>
					    <li>blaming others for financial problems</li>
					
					<p>Often these behaviors occur in conjunction with an insolvency event. For example, an individual who has just lost a job may fail to adjust his or her spending habits even though there is no longer any money coming in.</p>
					
					<p>Divorce is another life event that can propel someone into irresponsible financial behavior. When two people living in one house split, there is literally a doubling of expenses. This includes mortgage or rent, household items, furniture, groceries, separate cars, insurance, and medical plans.</p>
					
					<p>Many people will try and maintain the lifestyle they have grown accustomed to and continue to spend as they did before their financial circumstance changed. More often than not, the power of credit is abused. When cash is short, the credit card carries on the grand illusion of financial security.</p>
					
					</fieldset>
 
					<fieldset id="space">
					<legend><b>SUMMARY</b></legend>
					<ol>
						<li>For all its convenience and benefits, the credit card has the potential to do real damage to personal finances.</li>
						
						<li>In a climate of deregulation in lending coupled with an economic downturn, consumers increasingly use credit for essential expenses.</li>
						
						<li>Severe debt issues are caused by insolvency events, the debtor’s behavior or a combination of both.</li>
						
						<li>The growth of credit counseling and debt settlement is a direct response to increased consumer debt.</li>
					</ol>
