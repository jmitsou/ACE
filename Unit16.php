						<p>Various changes in a consumer’s circumstances could signal the need for a current credit report. Career changes, promotions, or relocation should prompt a review of any information that an employer might view as derogatory. Some job applications request permission to do a credit check.</p>
							
						<p>Purchasing a new home or vehicle are times when a consumer will want to be sure there are no unpaid accounts or inaccurate information. Clients filing for separation or divorce would need to verify accountability on joint accounts and insure proper assignment of liability to outstanding loans and financial responsibilities.</p>	
		
