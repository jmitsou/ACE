						<legend><b>THE FAIR CREDIT REPORTING ACT</b></legend>
							
						<p>Under <b class="teal-text">The Fair Credit Reporting Act</b>, anyone obtaining or reading a consumer’s credit report for reasons other than those listed here may face heavy fines or imprisonment:</p>	
							
							<li>Written permission for employment or other authorized reasons</li>
							<li>Application for government license</li>
							<li>Application for business license</li>
							<li>Credit or insurance transaction</li>
							<li>Court subpoena from a federal grand jury</li>
							<li>Requirement for setting child support</li>
							
						<p><b class="teal-text">The Fair Credit Reporting Act</b> also requires that negative reporting be erased 7 years from the date initiated. This includes a Chapter 13 Bankruptcy. A Chapter 7 Bankruptcy can remain on a credit report for up to 10 years.</p>
						
						<p>Within 60 days of denied credit, employment, insurance, or housing, a consumer may request a free copy of their report. Reports are issued free to consumers on welfare or if there is a reason to suspect fraud.</p>
											
						<p>It is highly advantageous for consumers to monitor their credit reports at least once a year.</p>
												
