						<legend><b>SUMMARY</b></legend>
						
						<ol>
							<li>Consumers should obtain their credit reports at least once a year in order to check for possible errors and to ascertain their credit scores.</li>
							<li>A ruling under the Fair and Accurate Credit Transactions Act allows consumers to obtain one free copy of their credit report from each credit bureau every 12 months.</li>
							<li>The Fair Credit Reporting Act requires that negative items on a credit report including Chapter 13 Bankruptcy be erased seven years from the date initiated. Chapter 7 Bankruptcy must be erased after ten years.</li>
							<li>Credit reports from all three credit-reporting agencies contain four categories of information: personal identification information, account history information. public record information, and inquires.</li>
							<li>Consumers can fix errors on their credit report by following a standard procedure involving phone calls and certified correspondence or they can now access and fix their reports online by visiting the credit reporting agency websites.</li>
							<li>The vast majority of credit reports contain some kind of an error. According to The Public Interest Research Group, 1 in 4 contain errors serious enough to disqualify consumers from making major purchases.</li>
						</ol>
										
