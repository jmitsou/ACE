						<legend><b>READING A CREDIT REPORT</b></legend>
						
						<p>When consumers get their credit reports, they may be surprised about what information is included and what is left out. It is important to realize that a credit report is not a comprehensive picture of the consumer. There are two reasons for this:</p>
							
						<ol>
							<li>The three credit reporting agencies do not get their information from the same subscribers. Different creditors subscribe to different bureaus.</li>
							<li>Many creditors do not report consumer activity on a regular basis. In fact, some do not report information on consumer accounts unless the accounts are in default.</li>
						</ol>	

						<p>Some accounts are listed and some are not. To many consumers, credit reports may not seem fair. For example, mortgage companies and auto dealers sometimes do not report consistent on time payments. However, information will show up if the accounts are past due, in collections, or in legal proceedings.</p>
							
						<p><b>Below is a list of accounts regularly reported:</b></p>	 
							
							<li>Bankcards</li>	
							<li>Merchant cards from large chains</li>
							<li>Travel and entertainment cards</li>
							<li>Federal student loans</li>

						<p><b>The following list shows accounts not normally reflected in a credit report… unless they are in default:</b></p>
							
							<li>Utility bills</li>
							<li>Medical bills</li>
							<li>Mortgage or rent</li>
							<li>Automobile loans</li>
							<li>Merchant cards from small or local retailers</li>

						<p>As you can see, a consumer may be on time over a long period with mortgage and car payments and it will never be reflected by the credit bureaus. However, be one day late with a payment and it will show up on the report.</p>
											
					</fieldset>
					 
