						<legend><b>INQUIRIES</b></legend>
							
						<p>Anytime a request is made to see a consumer’s credit report, it is reported as an inquiry on that consumer’s credit report. If a person applies for a lot of credit cards in a short time, it will produce a lot of inquiries on the report. When a consumer is denied credit because of too many inquiries, there is no legal recourse under existing legislation.</p>	
							
						<p>Consumers should not give their name and address to a merchant until they are ready to apply for credit there. Some merchants illegally run credit checks to get an idea as to what they can try to sell the consumer and how.</p>	
						
						<p>Too many inquiries on a credit report indicate that a person is “credit hungry.” Most inquiries are disregarded if they occurred six or more months ago. All credit inquiries should be removed from a report after two years.</p>	
							
						<p><b>2 Types of Inquiries</b><br />There are 2 types of inquires — hard and soft. <b>Hard inquiries</b> occur when the consumer applies for credit. <b>Soft inquiries</b> are those that come from companies that want to send out promotional information to a pre-qualified group or current creditors who are monitoring your accounts. Soft inquiries also occur when a consumer requests a copy of their credit report.</p>

						<p>Inquiries are what generate credit reports. There is a mistaken idea that credit reports are keep in enormous files and are constantly being updated as you go about your financial life. The reality is that credit reports do not exist until they are requested. Initiating an inquiry is what creates a credit report.</p>	
						
						<p>FICO scoring models (see Credit Scoring) ignore a vast majority of inquiries. For example, these models have buffer periods that ignore inquiries within 30 days of getting a mortgage or car loan. They also count two or more hard inquiries in the same 14-day period as just one.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>RESOLVING CREDIT REPORT ERRORS</b></legend>
							
						<p>It is up to nobody but the individual to make sure the information in his/her credit report is correct. Credit bureaus report what creditors tell them. They do not check for accuracy. Many experts acknowledge that up to 80% of all credit reports have some kind of misinformation.</p>
							
						<b>Common Credit Reporting Errors</b>
							
						<p>
							Information is mixed up with another report belonging to someone else with a similar or identical name.<br />
							The name of a former spouse appears on the report.<br />
							The name is misspelled, the address is wrong, or the Social Security number is incorrect.<br />
							Duplicate accounts show up.<br />
							Account information is inaccurate or incomplete. This includes incorrect account balances, showing closed accounts as opened, or paid-off accounts still showing as delinquent.<br />
							Outdated information is included. Negative account information must be removed after seven years. A Chapter 7 Bankruptcy must be removed after ten years.<br />
							Unauthorized inquiries are listed.<br />
							There is a failure to show that a tax lien has been released.
						</p>
							
						<b>Two things to keep in mind when fixing credit report errors:</b>	 
							
						<ol>
							<li>There is a standard procedure for getting rid of mistakes on a report under the terms of the Fair Credit Reporting Act.</li>
							<li>Erasing report errors by telephone or letter exchanges can take time. New online procedures can offer faster results.</li>
						</ol>	

						<b>Here’s what to do:</b>	
							
						<ol>
							<li>Complete the special investigation request form that will come with the report. Follow all the form’s instructions.</li>
							<li>Attach a letter to the completed form along with any documentation that helps prove the error. This includes copies of cancelled checks, receipts, account statements, as well as copies of previous correspondence between the consumer and the creditor involved.</li>
							
							<li>Maintain a copy of the completed investigation request form, letter, and all accompanying documentation. The date on the letter will signal when a response should come from the credit bureau. According to guidelines of the Fair Credit Reporting Act, the credit bureau must respond within 30 days of receiving an investigation request form.</li>
							
							<li>Send the request by certified mail with a request for a return receipt. When you get the receipt, file it with the other documents. Below is a sample letter for correcting errors on a credit report. Be sure to attach a copy of the credit report to the letter. Have the specific errors highlighted.</li>
						</ol>	

