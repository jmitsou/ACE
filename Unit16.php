						<legend><b>Credit Report Mistakes are Common</b></legend>
						
						<p>According to a report issued in 2004 by The Public Interest Research Group, 1 in 4 reports contain errors serious enough to disqualify consumers from making major purchases. Over 75% of the reports from the study contained some kind of error. More than half of these errors involved personal information like misspelled names or outdated information.</p>
							
						<p>Individuals with common last names or suffixes (Smith, Jones, Brown, or Sr., Jr., II, III) often become confused with other people. Also, identity theft is on the rise. Individuals have been misrepresented or have had accounts opened in their name without their knowledge.</p>
																		
						<p>Should a client discover any disputable items, a claim may be filed with the credit reporting agency which supplied the report. The client will need to fill out the special investigation request form that is included with the credit report. According to <b class="teal-text">the Fair Credit Reporting Act</b>, the credit bureau is required to resolve the problem in a reasonable amount of time — generally 30 days. During that period of time, a Statement of Dispute will be added to the credit history. It is advisable for clients to request their report once a year to safeguard against errors that may damage their credit rating.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>What the Credit Bureaus Must Do</b></legend>
						
						<p>After completing their investigation, each credit bureau has 5 business days to send you written notice of its findings. It must delete or modify the contested information immediately if:</p>
							
						<ol>
							<li>the information being disputed has been determined to be inaccurate</li>
							<li>the information cannot be verified.</li>
						</ol>	
								
						<p>However, if the credit bureau later receives proof that the disputed information is accurate, it can reinsert the information into the report. Along with the credit bureau’s written notice of its findings, the consumer should also receive a revised copy of the credit report to verify that the requested changes have been made.</p>
							
						<p>After receiving the corrected report, wait a couple of months and then request another copy of the report. This is to make sure that the error does not reappear due to a glitch in the system.</p>
							
						<p>If a consumer disagrees with the credit bureau’s findings, he/she may insert a statement free of charge. The statement must be included every time the report is sent out. Send the letter by certified mail and keep the receipt and a copy of the report. As long as the charge is in dispute, the dispute will appear on the credit report.</p>
							
						<p><b>Your Rights Under <b class="teal-text">The Fair Credit Reporting Act</b></b><br />
						The credit bureau is required to resolve the problem in a reasonable amount of time — generally 30 days.</p>	
											
						<p>If a consumer feels that the credit bureau has not responded promptly and fairly to his/her situation, contact the State Attorney General or The Federal Trade Commission in Washington at (202) 326-2222.</p>
											
					</fieldset>
					 
