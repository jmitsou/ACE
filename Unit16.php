						<legend><b>4 CATEGORIES OF INFORMATION</b></legend>
						
						<p>Deciphering a credit report is not as hard as cracking a secret enemy code, but it can seem like it. It is important to remember that, although the three credit reporting agencies use different formats, they all contain the following four categories of information:</p>
							
						<ol>
							<li>Personal identification information</li>
							<li>Account history information</li>
							<li>Public record information</li>
							<li>Inquiries</li>
						</ol>	

						<p>The order in which they are presented differ among the credit reporting agencies. A breakdown of the three formats appears below.</p>
							
							<b>TransUnion</b>
