					<fieldset id="space">
						<legend><b>OBJECTIVES:</b></legend>
							
							<li>To understand the importance of ethics in the workplace.</li>
						    <li>To determine the difference between credit counseling and providing therapy.</li>
						    <li>To identify the two types of crisis situations that can occur in the workplace.</li>
						    <li>To identify behaviors that may indicate the need for intervention.</li>
						    <li>To establish a Code of Ethics.</li>
						    
					</fieldset>
										
					<fieldset id="space">
					   <legend><b>WHAT EXACTLY ARE ETHICS?</b></legend>
					   
					   <p>Ethics are quality control for people. People do not become professionals because they wear business suits, have offices, drive company automobiles or possess the keys to the executive washroom. People become professionals when they understand and follow a standard of intention and behavior.</p>
						
						<p>Human beings have survived thus far as a direct result of our ability to cooperate with one other. The common good has been proved, again and again, to benefit the individual. Generally, ethics are based on the values (innate and/or instilled) of honesty and fairness. We treat others as we would like to be treated, we do not lie, or cheat, or steal.</p>
						
						<p>Certain parameters of ethics remain constant through the ages. They stand as permanent laws that describe honorable conduct — DO NOT DECEIVE, DO NOT TAKE ADVANTAGE OF SOMEONE, TELL THE TRUTH, etc. Other aspects of ethics are in a constant state of change and growth. For example, RESPECT DIVERSITY is a relatively new idea that author Rene Dubois characterizes as, not only a virtue, but a “requirement.”</p>	
						
						<p>For this reason, ethics need to be, on the one hand, preserved and adopted from the past and, on the other hand, discussed and debated, changed, and amended as times and conditions warrant. The formation of ethics literally requires full participation from the individual. The individual needs to own his/her ethics and the only way to truly own something is to help create it.</p>				
					</fieldset>
 
					<fieldset id="space">
						<legend><b>BUSINESS ETHICS ARE BASED ON PERSONAL ETHICS</b></legend>
											
							<p>We all create our version of personal ethics. These personal ethics in turn become the basis for good business ethics. Whenever we are selling hamburgers, computers, or legal services, we are operating in the world of commerce. In this special environment of doing business and exchanging goods, we take on new roles that require that we expand the guidelines of our personal ethics. Now that you have taken on the responsibility to provide a product or service to a customer, you must deliver. Thus, DO NOT PROMISE WHAT YOU MAY NOT DELIVER might be one of the first laws carved in the pedestal of your business enterprise.</p>
							
							<p><b>Business ethics provides guidelines regarding two groups of people who depend on the quality, viability, and growth of your organization:</b></p>
							
							<ol>
								<li>Customers. You have heard the well-worn phrase, “The customer is always right.” That may be true some of the time or even most of the time but what do you do when the customer is not right? Maybe the customer does not understand, is overreacting, or even suicidal (see Crisis Intervention). Successful organizations build specific guidelines for serving customers and accommodating to their needs.</li>
								<li>Employees. Employees have certain ethical obligations to their employers. ADHERE TO COMPANY POLICY and GIVE TWO WEEKS NOTICE IF YOU QUIT are two that come to mind. Likewise, employers have certain obligations to their employees – none more important than to ensure the continued success of the organization. The bottom line requires a profit and so does ethics. Without profits and a plan for future profits, there can be no ethics.</li>
							</ol>
												
					</fieldset>

 					<fieldset id="space">
 						<legend><b>PROFESSIONAL ETHICS</b></legend>
 							
 							<p>The development and implementation of professional ethics comes into play with the idea of the client. The client is not just a customer seeking a product or service but an individual seeking specialized assistance. He/she seeks assistance from what are assumed to be experts whether they are lawyers, doctors, or financial analysts. Professional ethics ensures that a special relationship is preserved in which the client is attended to, accepted, and shown respect.
							
							<p><b>Ethics in business and in the professions have two primary benefits:</b></p>

							<ol>
								<li>Ethics provide guidelines for negotiating the often tough decisions that arise in the workplace.</li>
								<li>The establishment of ethics enhance professional status.</li>
							</ol>
 																		
 					</fieldset>

					<fieldset id="space">
						<legend><b>Ethics Start from the Top</b></legend>
										   
					   <p>Without leadership, ethics is just an idea. Successful implementation of ethics relies on Team Leaders to initiate the standard. They set the tone of the entire organization. Either there are clear values that are upheld or corners get cut and a slippery slope of deceit ensues.</p>
					   
					   <p>Managers and supervisors will rise or sink to the level of behavior that is modeled by the leader of the organization. This, in turn, is transferred to everyone in the workplace. Leadership is all about motivation and ethics is an easy sell. Without ethics, there is no product or service to sell; there are no guarantees. Ethics provides for a way of doing things that are grounded in values.</p>
					   					
					</fieldset>

 					<fieldset id="space">
 					   <legend><b>COUNSELING VS. THERAPY</b></legend>
 					   
 					   <p>It is important to differentiate counseling from the practice of therapy. Credit counselors are not licensed therapists. They need to know where to draw the line in their relationship with clients. They need to know when it is appropriate to refer clients who may be experiencing emotional distress over their outstanding debt.</p>
 					   
 					   <p>Credit counselors assess client debt, client resources, and client attitudes and emotions. Attending to clients is a special way of giving undivided attention and assistance. However, credit counselors do not diagnose clients as suffering from specific psychological problems. Also, credit counselors seek to facilitate solutions. They do not, in any way, attempt to cure clients.</p>
 						
 					   <p>Therapy is a form of treatment. The word therapy originally meant “to heal.” Credit counselors do not heal patients; they attend to clients.</p>
 					   					
 					</fieldset>

					<fieldset id="space">
					   <legend><b>CRISIS INTERVENTION</b></legend>
					   
					   <p>Effective organizations prepare for crisis situations through the establishment of specific guidelines. Emergencies involving imminent danger require the notification of 911 dispatchers, emergency medical services (EMS) and/or the police. How crisis situations are handled is a direct reflection of the professionalism of the organization.</p>
					   
					   <p>As you know, financial distress is a major reason for depression and stress in our society. Those working in the debt management industry are bound to interact, sooner or later, with individuals who exhibit behavior that may require referral – sometimes immediate referral. Practitioners of crisis intervention must undergo special training that enables them to effectively stop or de-escalate life-threatening emergencies. Our purpose here is to give an overview of the signs that should cause concern and/or require action. There are two types of crisis situations — imminent danger emergencies and non-imminent danger emergencies.</p>
					   
					   <p><b>Imminent danger emergencies</b> override guidelines of client/counselor confidentiality and require the involvement of a 911 dispatcher, police, or EMS. In these cases, behavior is exhibited that is dangerous, threatening, violent, self-harming, destructive, or suicidal.</p>

						<p><b>Non-imminent danger emergencies</b> that make up the vast majority of crisis situations require appropriate referrals for effective interventions. These cases involve signs of depression, severe stress, on-going spousal abuse, child abuse, and anger management problems. Again, credit counselors are not licensed therapists and cannot diagnose depression, stress overload, or rage disorders. They can, however, learn to understand some common behaviors associated with these conditions and provide clients with the resources that can help them find solutions to these problems.</p>
						
						<p><b>What follows is a list of some of the common signs that a client may be potentially violent or suicidal:</b></b>
						
						    <li>The client expects, without sufficient basis, to be exploited or taken advantage of.</li>
						    <li>The client reads hidden demeaning or threatening meanings into benign remarks or events.</li>
						    <li>The client is reluctant to confide in others.</li>
						    <li>The client is easily slighted or quick to react with anger.</li>
						    <li>The client rarely reciprocates gestures of facial expressions.</li>
						    <li>The client relates odd or strange beliefs.</li>
						    <li>The client is unable to sustain consistent employment due to job abandonment, repeated absences, or conflict.</li>
						    <li>The client repeatedly fails to honor financial responsibilities.</li>
						    <li>The client exhibits reckless behavior.</li>
						    <li>The client lacks remorse or feels justified to mistreat others.</li>
						    <li>The client has unstable and/or intense relationships. The client exhibits rapidly shifting moods.</li>
						    <li>The client exhibits frequent displays of temper.</li>
						    <li>The client’s style of speech is excessively impressionistic and lacking in detail to support impressions.</li>
						    <li>The client exhibits a grandiose sense of importance. The client is unable to see other perspectives.</li>

						<p>Many of these client behaviors do not, in and of themselves, signify a serious problem that must be addressed. However, it is important to keep the above list in mind when communicating to clients. Financial dilemmas do cause stress and, in some cases, desperation.</p>
						
						</fieldset>
						
					<fieldset id="space">
							<legend><b>CALLING 911</b></legend>
											   
							<p>We have all seen 911 dispatchers on T.V. shows that dramatize emergency situations. Few of us, however, appreciate that the individuals who respond to 911 calls perform a very demanding and complex job. Personalities, attitudes, and professionalism of 911 dispatchers vary considerably. Keep in mind that 911 calls are recorded to ensure that dispatchers follow prescribed guidelines. However, a significant number of dispatchers are overworked or severely distressed by their jobs. They must manage the powerful emotions that go with any crisis situation while performing multiple tasks simultaneously.</p>
							
							<p><b>Here are tips when calling 911:</b></p>
							
							<ol>
								<li>Answer all questions of the dispatcher.</li>
								<li>Do not argue or disagree with the dispatcher.</li>
								<li>Always request the incident number of your 911 call. This makes call backs and follow ups much easier.</li>
								<li>Always remember 911 calls are NOT confidential. They are a matter of public record.</li>
							</ol>
												
							<p>Individuals resort to violent acts including suicide for many reasons. One of these reasons is the loss or potential loss of the ability to meet financial obligations. It is important for credit counselors to be aware of and sensitive to the potentially volatile nature of credit problems and financial distress in general.</p>
												
						</fieldset>

					<fieldset id="space">
					   <legend><b>CODE OF ETHICS</b></legend>
					   
					   <p><b>Preamble</b><br />The code of ethics developed specifically for the debt management and debt settlement industries is based on the professional ethics and standards of counseling. It seeks to promote and maintain the idea that the client/counselor relationship is founded on trust, respect, and acceptance. Any code of ethics in no way supersedes the policies of an organization. Rather, they stand as a framework for the basis of true professionalism.</p>
										
					</fieldset>

					<fieldset id="space">
					 	<legend><b>SECTION I: THE CLIENT/COUNSELOR RELATIONSHIP</b></legend>
					 					   
					 	<p><b>A. CLIENT WELFARE</b><br /><b>1. Prime directive.</b> The primary directive of credit counselors is to assist clients in the successful management of their debt.</p>
					 						
					 	<p><b>2. Respect.</b> Counselors respect clients and attend to all issues relating to personal finance.</p>
					 					   
					 	<p><b>3. Fostering independence.</b> Counselors encourage client growth and development; counselors avoid promoting a client/counselor relationship of dependency.</p>	
					 					   
					 	<p><b>B. RESPECT FOR DIVERSITY</b><br /><b>1. Non-discrimination.</b> Counselors do not engage in or promote in any way discrimination based on age, color, culture, disability, ethnic group, gender, race, religion, sexual orientation, marital status, or socioeconomic stature.</p>
					 						
					 	<p><b>2. Value differences.</b> Counselors actively accept and attempt to understand the diverse cultures represented by clients.</p>
					 					   
					 	<p><b>3. Identity impact.</b> Counselors actively seek to understand and take into account their own cultural/ethnic/racial identity and how it may influence their values and beliefs related to the counseling process.</p>
					 					   	
					 	<p><b>C. RIGHTS OF THE CLIENT</b></p>
					 						
					 	<p><b>1. Full disclosure.</b> Counselors inform clients truthfully and accurately concerning the goals, procedures, and expected outcomes of the counseling process. Likewise, the client is privy to billing procedures and documentation regarding the client.</p>
					 					   
					 	<p><b>2. Freedom of choice.</b> Counselors understand that clients enter the counseling process through their own informed decision. They are in no way coerced by counselors or third parties to enter into the counseling process.</p>	
					 					   
					 	<p><b>D. DUAL RELATIONSHIP</b></p>
					 	
					    <p>Whenever possible, counselors avoid dual relationships with clients that may compromise professional judgment. Examples of such relationships include, but are not limited to, familial, social, financial, business or close personal relationships with clients. Whenever a dual relationship cannot be avoided, counselors take the necessary precautions to ensure that judgment is not impaired and no exploitation occurs. Such precautions include, but are not limited to, informed consent, consultation, supervision and documentation.</p>
					 					   
						<p><b>E. CLIENT NEGLECT</b></p>
					 						
					 	<p><b>1. Abandonment prohibited.</b> Counselors do not abandon or neglect clients in counseling.</p>
					 					   
					 	<p><b>2. Inability to assist client.</b> If a client does not qualify for credit counseling or the counselor is unable to assist the client, the counselor provides information, referrals, resources and suggests appropriate alternatives.</p>
				
					 </fieldset>
					 
					<fieldset id="space">
					    <legend><b>SECTION II: CONFIDENTIALITY</b></legend>
					    
					    <p><b>A. RIGHT TO PRIVACY</b><br /><b>1. Respect for privacy.</b>Counselors avoid illegal and unwarranted disclosures of confidential information. Counselors respect their clients’ rights to privacy.</p>
					    
					    <p><b>2. Exception.</b> Confidentiality does not apply when disclosure is necessary to prevent clear and imminent danger to the client or others or when legal requirements necessitate that confidential information be disclosed. Exceptions are determined through consultation with other counselors or professionals. Examples of exceptions include, but are not limited to, threats of suicide, threats to inflict bodily harm or property damage and revelation of contagious, fatal diseases.</p>
					    
					    <p><b>B. RECORDS</b><br /><b>1. Maintenance of records.</b>Counselors keep and maintain client records as required by the counseling organization.</p>
					    
					    <p><b>2. Confidentiality of records.</b> In the creation, maintenance, transfer, or destruction of client records, counselors understand that a client’s right to privacy extends to client/counselor documentation.</p>
					    
					    <p><b>3. Client access.</b> Clients have the right to request records relating to their debt management or settlement agreement.</p>
					 					
					 </fieldset>

					<fieldset id="space">
					    <legend><b>SECTION III: PROFESSIONAL RESPONSIBILITY</b></legend>
					    
					    <p><b>A. Knowledge of Ethics.</b>Counselors are responsible for reading, and adhering to the Code of Ethics.</p>
					    
					    <p><b>B. Competency.</b>Counselors understand and are aware of their limitations concerning knowledge and competency based on education, training, and credentials. Counselors refer clients to other counselors or supervisors when uncertainties arise or knowledge is insufficient to answer to client circumstances. Counselors do not improvise or infer information.</p>
					    
					    <p><b>C. Review Effectiveness.</b>Counselors consistently evaluate the performance and outcome gains of their counseling. When necessary, counselors seek to improve their performance through continuing education, peer evaluation and supervision.</p>
					    
					    <p><b>D. Continuing Education.</b>Counselors understand the importance of continuing education in a dynamic field that is affected by new developments in counseling, consumer rights, the economy, current legislation and current lending trends.</p>
					 					
					 </fieldset>

					<fieldset id="space">
					   <legend><b>SECTION IV: EMPLOYEE RELATIONS</b></legend>
					   
					   <p><b>A. Reporting Problems.</b> It is of universal interest that conditions or behaviors that may be disruptive, damaging or illegal are brought to the attention of a supervisor.</p>
					   
					   <p><b>B. Professionalism.</b>It is of universal interest that counselors display and maintain high standards of professional conduct.</p>
					   
					   <p><b>C. Employee Policies.</b>Accepting employment with an organization infers that counselors are in agreement with the organization’s policies and objectives.</p>
										
					</fieldset>

					<fieldset id="space">
					<legend><b>SUMMARY</b></legend>
						
						<li>Ethics is a necessary part of conducting business.</li>
						<li>Effective counselors who interact with consumers in debt recognize crisis situations and provide referrals when appropriate.</li>
						<li>The Code of Ethics developed for credit counselors is based on professional counseling ethics.</li>		
						
