						<legend><b>ROTTER’S LOCUS OF CONTROL</b></legend>
						
						<p>According to J.B. Rotter, each of us operates with either an internal or external locus of control. Those with an internal locus of control believe that the circumstances and events surrounding them depend on their own ability or behavior. Conversely, those with an external locus of control see events and circumstances as outside their direct control. Things happen by chance.</p>
											
						<p>Possessing an internal locus of control empowers the individual whereas an external locus of control subordinates the individual to outside forces which act irrespective of whatever the individual might do. Locus of control determines to a large extent how clients view the positive and negative circumstances in their lives. It informs their belief in what they can and cannot do and what they are responsible for. Therefore, it directly impacts the willingness to act.</p>
						
						<p>Individuals with an external locus of control may view creditors as preying on and victimizing consumers who end up in debt because they were fooled and confused by lending agreements they could not understand. In their view, these consumers are well-intentioned pawns in a game of intricate deception. However, those with an internal locus of control are more likely to view themselves as ultimately responsible for their own financial problems.</p>
						
						<b>An internal locus of control is advantageous for 2 reasons:</b>
						
						<ol>
							<li>Internal locus of control fosters responsibility. Consequences and outcomes are seen as direct results of the individual’s actions. The individual is less likely to blame outside forces for negative outcomes. Also, the individual is more likely to take credit for positive outcomes. This, in turn, garners self-respect and motivation to accomplish more.</li>
							<li>Internal locus of control engenders self-efficacy. The individual possesses the power to affect change. Obstacles can be overcome and necessary resources can be accessed and utilized to solve problems.</li>
						</ol>	
												
