						<legend><b>OVERCOMING SELF-DEFEATING BEHAVIORS</b></legend>
							
						<p>Albert Ellis believed that thoughts come before emotions when it comes to making decisions. Therefore, if the thinking is corrected, the emotion can be controlled. Clients finding themselves under financial strain are likely to listen to that inner voice that chants, “I MUST have this,and ”I MUST buy that.” The reality is that these “musts”cannot always be accommodated by the client’s income.</p>
								
						<p><b>Don’t fall for irrational musts.</b> Think about the word must. Is saying”I must have a lambskin jacket for the winter season”a rational statement? Obviously it isn’t. Absolutely no harm will come to someone from not having a lambskin jacket. Leave the “musts” for referring to things like hot meals and shelter. Consumer luxuries are never a must. Encourage clients to change this season’s musts to this season’s must nots.</p>
								
						<p><b>Use rational coping and self-statements.</b> A good cognitive exercise is to trade the irrational “must” for a rational statement that moves the client toward financial well-being:</p>
												
						<p>“If I went out on the town one less night a week, I could put that money toward credit-card debt without radically changing my lifestyle.”</p>
							
						<p>“It will take time and discipline to stick to a spending plan, but it will be worth it to get my finances under control.”</p>
								
						<p><b>Take the bad with the good.</b> People who overspend often focus on the supposed benefits of purchasing items while ignoring the pitfalls. If overspending is a problem, concentrate and write down the disadvantages of the behavior. Similarly, concentrate and write down all the reasons to stick to the spending plan.</p>
								
						<p><b>Never underestimate the power of distraction.</b> To combat feelings of disappointment and frustration, the simple directive to get your mind off things works wonders. Keep the mind occupied with hobbies, exercise, spending time with friends, watching movies, doing chores, or just about anything else besides money and expenses.</p>
												
					</fieldset>
						 
