						<legend><b>REFRAMING</b></legend>
							<p>Reframing involves a change in perception that activates possibilities. It takes a situation out of its old context and places it into a new context that offers a positive outcome. For example, a client may focus on the negative aspects of excessive credit-card debt. By reframing the situation, the client can view the problem as an opportunity to organize finances and change destructive spending habits.</p>
												
							<p>A laid-off worker focuses on the dismal economy and the lack of jobs. In contrast, the focus could be on gaining new skills and/or career exploration. A student who has accumulated a mountain of credit card debt can, through reframing, use his or her personal experience with debt to initiate a campus-based education program concerning the proper use of credit.</p>
							
							<b>Let’s look at two techniques based on the concept of reframing which are utilized in Solution-Focused Counseling.</b>
							
							<p><b>The Miracle Question</b><br />
							This is a counseling technique that helps clients visualize a future in which their problems no longer exist. It begins by asking the client the following question:</p>
							
							<p>“Let’s imagine that tomorrow you wake up and are free from the burden of excessive debt. It is a miracle but since you were asleep when it happened, you are not immediately aware that anything has changed. What will you begin to notice about yourself and your surroundings that will clue you in that a miracle has occurred? How will you know the miracle has taken place? What will be different?”</p>
							
							<p>By identifying the outward effects of the miracle, the client can begin to visualize the specific conditions that created it. For example, the miracle of being free of outstanding debt may include a home where there is little or no stress, where creditors are no longer calling, and where family members are not in conflict over limited financial resources. Now, what specifically has the client
							done to create this scenario? Does the client have a different job? Has the client moved? What kind of car does the client drive? Most importantly, how does the client act within the miracle scenario?</p>
							
							<p><b>The Exception Question</b><br />
							Another technique, The Exception Question, helps clients recognize those times when things were better. The counselor asks the client to recall a time when debt was not a problem. What was different about his or her situation or behavior that created a manageable debt scenario?</p>
							
							<b>Both The Miracle Question and The Exception Question have 3 benefits:</b>
							
							<ol>
								<li>They help clients explore what it is they want different in their lives.</li>
								<li>They help clients identify the strengths and resources to make these desired differences a reality.</li>
								<li>They situate a desired future squarely in the present moment.</li>
							</ol>
												
