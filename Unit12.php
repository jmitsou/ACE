						<legend><b>BANDURA’S SELF-EFFICACY</b></legend>
						<p>Albert Bandura theorized that an individual’s willingness to take action depends on the presence of two necessary conditions:</p>
						
						<p><b>1. Outcome expectations.</b><br /> In order to act, the individual must believe that the action will result in a desired outcome. For example, the individual understands that maintaining a budget is crucial for getting control of spending.</p>
						
						<p><b>2. Self-efficacy expectations.</b><br /> This is the can-do attitude. For action to take place, the individual must believe that he/she can successfully engage in the necessary behavior to achieve the desired outcome. For example, the individual is reasonably sure he/she can do what it takes to resolve outstanding debt. This means making consistent payments as well as creating all necessary conditions to ensure sufficient funds by cutting expenses, increasing income, etc.</p>
						
						<p>If either of these two conditions is missing, taking action will be avoided. On the one hand, the client may wholeheartedly believe a particular plan of action is the best option given the current circumstances. However, the client may question his or her ability to actually do what it takes to complete the necessary actions. The client will then resist taking action and find reasons — real or imagined — to continue to do nothing.</p>
						
						<p>On the other hand, the client may possess the can-do attitude but resist taking action because the action plan is not fully understood as the best course to take. Again, action will be avoided in favor of doing nothing.</p>
						
						<p><b>INCREASING SELF-EFFICACY</b><br />Help clients acquire necessary skills. Self-efficacy is based on ability. You believe you can do something because you have the skills and the resources to achieve it. Therefore, increasing self-efficacy means increasing the skills required to accomplish a desired outcome. A client may resist applying for a better paying job because he/she lacks interviewing skills. Interviewing skills can be acquired through practice. This then positively impacts the client’s self-efficacy with regards to the hiring process.</p>
						
						<p><b>Monitor progress.</b> Clients need to see the positive outcomes of the actions they take. Acknowledgment of small successes can help bolster the confidence to tackle bigger problems.</p>
						
						<p><b>Inspire by example.</b> Seeing is believing and believing is what self-efficacy is all about. Tell clients about others who overcame obstacles and resolved their problems.</p>
						
						<p><b>Encourage and challenge.</b> ”You can do it!”often leads to ”I can do it!” Self-defeating beliefs need to be challenged and replaced by the idea that desired outcomes are not only possible but also realistic.</p>
						
						<p><b>Help clients overcome anxiety.</b> Fear of failure is a sure recipe for non-action. Help clients by listening to and acknowledging their fears. Then help them to dispel them with good, solid information.</p>					
												
					</fieldset>		
						
