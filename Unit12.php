						<p>Clients and counselors may understand the current scenario, identify desired outcomes, and come up with a viable plan for addressing an issue or goal but unless action is taken, the goal will remain out of reach.</p>
												
						<p>Taking action may be hindered by states of inertia or other barriers to positive change. Some of these obstacles to personal progress result from self-defeating behaviors that serve to sabotage success. It is important for counselors to consider the dynamics behind a client’s lack of motivation or failure to act even when specific goals are defined.</p>
						
						<p>Individuals who need to get control of their financial situation may need to develop a new and empowering perspective that will engender positive change. What is preventing clients from actually solving problems and acting on viable solutions could be the result of underlying belief systems about how much control and power they have over the world around them.</p>
						
						<p>Let’s look at two psychological models which address this issue of the power we have over the world around us: Bandura’s Self-Efficacy and Rotter’s Locus of Control.</p>
											
