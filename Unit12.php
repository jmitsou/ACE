						<legend><b>CUTTING CUTS</b></legend>
						
						<p>When you stop to think about it, there are many ways to economize — that is, to reduce expenses. There’s coupon and sale shopping, careful meal planning with utilization of leftovers for lunches and snacks, substituting generic products for name brand ones, watching household electricity consumption — the list is endless.</p>
											
						<p>When clients experience a loss or reduction of income, they must re-examine their current expenditures to determine which ones are essential and which ones are non-essential. In many cases, a short-term adjustment in spending may be all that is necessary. In other cases, sacrifices over the long term may be in order. The important thing is to understand how to economize and to see that cutting costs can be a source of personal satisfaction and accomplishment.</p>
						
						<p>Let’s begin by defining what it means to economize. Economizing is allocating spending so that it has the maximum benefit for everyone in the household. During times when money is scare, economizing also helps to minimize hardship. It does not necessarily mean spending less. It simply means making use of available resources for the greatest benefit.</p>
						
						<p>In fact, there are circumstances in which economizing warrants spending more. For example, loss of employment may necessitate that the client invest in his or herself through job training, education, re-locating, or purchasing new clothes for interviewing. Spending more may also be necessary when a client decides to utilize a particular skill and turn it into a way to make money. For example, someone who can bake or sew would need to invest in the materials from which to make the products they would sell.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>4 WAYS TO ECONOMIZE</b></legend>
						
						<ol>
							<li>Substituting products or services for less costly alternatives. This does not necessarily mean giving up quality. For example, substituting dry cleaning services for do-it-yourself dry cleaning does not mean you will be wearing dirty clothes.</li>
							<li>Conserving resources to get the most out of them, avoiding waste, and using what you have efficiently. Part of conserving is maintaining the things you own and keeping them in good working order. This includes your possessions, your skills, and your health.</li>
							<li>Cooperating with others to reduce expenses. Common examples include carpooling and sharing baby-sitting, food and housing costs.</li>
							<li>Utilizing community resources. Communities everywhere offer a wealth of free services and recreation like parks, museums, and libraries. A picnic lunch at a public park often provides a more spectacular view than most restaurants. This category of economizing also includes all the various assistance programs designed to help those who need it.</li>
						</ol>
										
