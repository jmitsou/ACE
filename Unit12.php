						<legend><b>CREATING A SPENDING PLAN</b></legend>
							
						<p>Many clients will tend to equate budgeting with denial, sacrifice, and the postponement of gratification. They may find the act of recording expenditures and incomes a mundane chore. In fact, clients with substantial credit debt may have avoided budgeting for fear of being overwhelmed with the truth.</p>
						
						<p>It is important for counselors to help their clients gain a new perspective about budgeting. One strategy they can use right away is to avoid the word budget with all its connotations of belt tightening and instead use the term spending plan. Creating a spending plan sounds better than making a budget. It implies freedom while the other implies restriction.</p>
											
						<p>Another thing counselors can stress is that a personal budget or spending plan, far from being a mundane chore, is key to realizing financial goals. It is nothing less than an indispensable tool for mapping out a course toward increased financial freedom.</p>
						
						<b>Creating a spending plan has 4 primary benefits:</b>
						
						<ol>
							<li><b>Helps to identify overspending.</b> Once all the numbers are on paper (or on the computer screen), the client and counselor can see exactly where the money is going. Problem areas can be detected. For example, what percentage of income is going to housing costs? What is the client’s debt-to-income ratio?</li>
							
							<li><b>Promotes honest interpersonal communication and accountability.</b> Clients in debt don’t live in a vacuum. Their debt affects everyone around them. As you may know, money and how it is spent is a leading cause for arguing and discontent between partners and family members. A solid spending plan is an objective starting place for making the adjustments necessary to reach financial goals.</li>
							
							<li><b>Increases motivation.</b> When clients identify what they really want from the money they make, a tangible goal has been created. Yes, resolving financial problems can sometimes require short term sacrifices. People generally do not do without the products and services they have come to value simply because self-denial is virtuous.It is because they know there is a light at the end of the tunnel. The spending plan is like a map leading toward future rewards.</li>
							
							<li><b>Tracks your success.</b> Without a plan, there is no measuring stick to use to tell whether or not you are reaching your goals. With a spending plan in place, you can easily monitor the level of your success.</li>
						</ol>	
						
						<p>When a spending plan is utilized correctly and client motivation continues, it will function as an on-going action plan. Make sure the client keeps the plan up-to-date for each new month. Also, itis important to keep the plan flexible. Spending plans that are too strict are destined to failure.</p>
						
						<p>At the beginning of each month, the client will allocate spending for each of the categories under the appropriate column. At the end of that month, the actual amount spent will be recorded. These two amounts are then compared to see if the client’s goals for that month have been achieved.</p>
							
						<p>There are many types of spending plans including computer-based programs with built-in calculators. Keep in mind that some of the best spending plans are formulated on plain legal pads or, in some cases, even on the backs of envelopes!</p>					
					</fieldset>

