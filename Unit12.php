						<legend><b>BECOMING SOLUTION-FOCUSED</b></legend>
							
						<p>You might ask him what he ordered from the menu but, at the present moment, he can’t speak. Maybe he has a medical condition. Possibly the panicked woman at his table can tell you if he has had a history of heart problems.</p>
												
						<p>You might even confer with the other diners in the restaurant; perhaps they’ve seen this sort of thing before. Or you could save the man’s life by performing the Heimlich Maneuver.</p>
							
						<p>The Solution-Focused Approach to counseling espouses the positive notion that change is a natural, everyday process, that people are not destined to be stuck in a rut, and that solutions to problems can result from simple, sometimes small adjustments made by the individual.</p>
							
						<p>The key is to help the client access what he/she already knows. The man in the illustration above who is choking on food knows the answer to his problem;  the obstruction must be removed so he can breathe again. But he needs help. This is where the Solution-Focused Approach comes in.</p>
							
						<b>This approach is particularly valuable in financial-based counseling for the following reasons:</b>
							
						<ol>
							<li><b>Financial-based counselors are not therapists.</b> The word therapy comes from the Greek word meaning “to cure.” This implies that the client is a patient that needs to be treated rather than a capable individual who requires assistance with current financial issues. The Solution-Focused Approach departs from the idea of therapeutic care. Instead, the counselor helps the client discover and activate his or her own strengths and resources.</li>
								
							<li><b>Solution-Focused Counseling is goal-oriented.</b> Clients are encouraged to identify their short and long-term goals and to identify the obstacles that are keeping them from reaching those goals. Then, it’s a matter of removing the obstacles. This can be a highly motivational exercise.</li>
								
							<li><b>The Solution-Focused Approach highlights client strengths rather than weaknesses.</b> Any real, long-term solution to a clients problem must come from the client. Thus, the counseling session becomes positive and self-affirming for the client.</li>
								
							<li><b>Solution-Focused Counseling focuses on the future.</b> Little time is spent on the past, the mistakes and the patterns of self-deception. The present moment — the counseling session is used as a springboard to discover and actualize a positive future.</li>
						</ol>
												
					</fieldset>						 

