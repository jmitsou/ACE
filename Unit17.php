					<fieldset id="space">
						<legend><b>FICO SCORES</b></legend>
						
						<p>A credit score is a number lenders use to help them decide: If I give this person a loan or credit card, how likely is it that I will get paid back on time? A score is a snapshot of a person’s credit risk picture at a particular point in time.</p>
						
						<p>There are many types of credit scores but the most commonly used are credit bureau scores. Credit bureau scores are based solely on information gathered from consumer credit reports that are maintained at one of the credit-reporting agencies. The most widely used credit bureau score is based on a statistical model developed by Fair, Isaac. It is commonly known as a FICO score.</p>
						
						<p>Understanding credit scoring can help people manage their credit. A FICO score looks at the same information in a credit report that a lender will also use. By knowing how credit risk is evaluated, a consumer can take actions that will lower credit risk, and thus, raise the score over time.</p>
		
					</fieldset>
					
					<fieldset id="space">
						<legend><b>Your Credit Report – The Basis of Your Score</b></legend>
						
						
						<p>Credit-reporting agencies process information on millions of borrowers. Lenders making credit decisions buy credit reports on their prospects, applicants, and customers from the credit-reporting agencies.</p>
						
						<p>Your report details credit history as it has been reported to the credit-reporting agency by lenders who have extended consumer credit. A credit report lists what types of credit are used, the length of time accounts have been opened, and whether or not bills have been paid on time. It tells lenders how much credit has been used and whether the consumer is seeking new sources of credit. It gives lenders a broader view of credit history than do other data sources, such as a bank’s own customer data.</p>
						
						<p>A credit report does not really exist until the consumer or a lender asks for it. It is then compiled by the credit-reporting agency based on the information stored in that agency’s files. This information is supplied by lenders, by court records and by the consumer.</p>
						
						<p>Tens of thousands of credit grantors — retailers, credit-card issuers, banks, finance companies, credit unions, etc. — send updates to each of the credit-reporting agencies, usually once a month. These updates include information about how their customers use and pay their accounts.</p>
						
						<p>A credit report reveals many aspects of borrowing activity. All pieces of information are considered in relationship to other pieces of information. The ability to quickly consider all this information is what makes credit scoring so useful.</p>
										
					</fieldset>
					
					<fieldset id="space">
						<legend><b>HOW SCORING WORKS</b></legend>
						
						<p>Along with the credit report, lenders can also buy a credit score based on the information in the report. That score is calculated by a mathematical equation that evaluates many types of information from the credit report at that agency. Exactly what formula is used to calculate a score is shrouded in mystery and protected by the Federal Trade Commission. By comparing this information to the patterns in hundreds of thousands of past credit reports, the score identifies a consumer’s level of credit risk.</p>
						
						<p>In order for a FICO score to be calculated from a credit report, the report must contain at least one account that has been opened for six months or more. In addition, the report must contain at least one account that has been updated in the past six months. This ensures that there is enough informationand enough recent informationin the report on which to base a score.</p>
						
						<p><b>More About FICO Scores</b><br />Credit bureau scores are often called FICO scoresbecause most credit bureau scores used in the United States and Canada are produced from software developed by Fair, Isaac and Company (FICO). FICO scores are provided to lenders by the three major credit-reporting agencies: Equifax, Experian and Trans Union.</p>
						
						<p>FICO scores have different names at each of the three credit-reporting agencies. All of these scores, however, are developed using the same methods by Fair Isaac, and Company and have been rigorously tested to ensure they provide the most accurate picture of credit risk possible using credit-reporting data. Fico score names for each credit bureau are listed below:</p>
						
