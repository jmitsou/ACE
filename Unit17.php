						<p>At present, FICO scores provide the best guide to future risk based solely on credit report data. The higher the score, the lower the risk. No score, however, can predict whether a specific individual will be a goodor badcustomer.</p>
						
						<p>Also, while many lenders use FICO scores to help them make lending decisions, each lender has its own strategy, including the level of risk it finds acceptable for a given credit product. There is no singlecutoff scoreused by all lenders.</p>
										
					</fieldset>
					
					<fieldset id="space">
						<legend><b>More Than One Score</b></legend>
						
						
						In general, when people talk about your score, they are talking about your current FICO score. However, there is no one score used by lenders to make decisions about you. Here’s why:
						
						Credit bureau scores are not the only scores used. Many lenders use their own scores which may include the FICO score as well as other information about the consumer.
						
						FICO scores are not the only credit bureau scores. There are other credit bureau scores although FICO scores are by far the most commonly used. Other credit bureau scores may evaluate a credit report differently than FICO scores. In some cases, a higher score may mean more risk, not less risk as with FICO scores.
						
						An individual’s score may be different at each of the three main credit-reporting agencies. The FICO score from each credit reporting agency considers only the data in the credit report at that agency. If a person’s current scores from the credit-reporting agencies are different, it is probably because the information those agencies have on that person differs.
						
						An individual’s FICO score changes over time. As the data changes at the credit-reporting agency, so will any new score based on that credit report. A FICO score from a month ago is probably not the same score a lender would get from the credit-reporting agency today.					
					</fieldset>
					
					<fieldset id="space">
						<legend><b>WHAT A FICO SCORE CONSIDERS</b></legend>
						
						<p>There are 5 main categories of information that are used to calculate a FICO score. The categories have different levels of importance.</p>
						
						<ol>
							<li>Payment History: What is the track record?</li>
							<p>Approximately 35% of a score is based on this category.</p>
							
							<p>The first thing most lenders want to know is whether a consumer has paid past credit accounts on time. This is also one of the most important factors in a credit score.</p>
						
							<p>However, late payments are not an automatic score-killer. An overall good credit picture can outweigh one or two instances of late credit-card payments. By the same token, the absence of late payments in a credit report does not necessarily translate into a perfect score. Approximately 60 – 65% of credit reports show no late payments at all. Payment history is just one piece of information used in calculating the score.</p>
							
							<b>Payment history takes into account:</b>
							
							<p>Payment information on many types of accounts. These will include credit cards (Visa, MasterCard, American Express and Discover), retail accounts (department store credit cards), and installment loans (loans where you make regular payments, such as car loans, finance company accounts and mortgage loans).</p>
							
							<p>Public record and collection items — reports of events such as bankruptcies, foreclosures, lawsuits, wage attachments, liens and judgments. These are considered quite serious, although older items and items with small amounts will count less than more recent items or those with larger amounts.</p>
							
							<p>Details on late or missed payments (delinquencies) and public record and collection items — specifically, how late they were, how much was owed, how recently they occurred and how many there are. A 60-day late payment is not as risky as a 90-day late payment, but recency and frequency count too. A 60-day late payment made just a month ago will count more than a 90-day late payment from five years ago. Note that closing an account on which there was a previously missed payment or satisfying a judgment or collection item does not make the late payment or item disappear from the credit report.</p>
							
							<p>How many accounts show no late payments. A good track record on most of your accounts will increase a credit score.</p>
							
							<li>Amounts owed: How much is too much?</li>
							<p>Approximately 30% of the score is based on this category.</p>
						
							<p>Having credit accounts and owing money on them does not mean a person is a high-risk borrower with a low score. However, owing a great deal of money on many accounts can indicate that a person is overextended and is more likely to make some payments late or to not pay at all. Part of the science of scoring is determining how much is too much for a given credit profile.</p>
						
							<b>Amounts owed takes into account:</b>
							
							<p>The amount owed on all accounts. Note that even if credit cards are paid in full every month, a credit report may show a balance on those cards. The total balance on the last credit card statement is generally the amount that will show on the credit report.</p>
							
							<p>Whether there is a balance on certain types of accounts. In some cases, having a very small balance without missing a payment shows a consumer has managed credit responsibly and may be slightly better than no balance at all. On the other had, closing unused credit accounts that show zero balances and that are in good standing will not generally raise a score.</p>
							
							<p>How many accounts have balances. A large number can indicate higher risk of over-extension.</p>
							
							<p>How much of the total credit line is being used on credit cards. Someone closer to maxing out on many credit cards may have trouble making payments in the future.</p>
							
							<p>How much of installment loan accounts is still owed compared with the original loan amounts. Paying down installment loans is a good sign that a consumer is able and willing to manage and repay debt.</p>
							
							<li>Length of Credit History: How established is an individual’s credit history?</li>
							
							<p>Approximately 15% of the score is based on this category.</p>
							
							<p>In general, a longer credit history will increase a score. However, even people who have not been using credit long may get high scores, depending on how the rest of the credit report looks.</p>
							
							<b>Length of credit history takes into account:</b>
							
							<p>How long the credit accounts have been established. The score considers both the age of the oldest account and an average age of all accounts.</p>
							
							<p>How long specific credit accounts have been established. Different accounts are evaluated in different ways.</p>
							
							<p>How long it has been since certain accounts were used. FICO scores consider not only how long an account has been opened but also whether or not it is active.</p>
							
							<li>New Credit: Is an individual taking on more debt?</li>
							<p>Approximately 10% of the score is based on this category.</p>
							
							<p>People tend to have more credit today and to shop for new credit more frequently than ever. Fair, Isaac and Company scores reflect this fact. However, research shows that opening several credit accounts in a short period of time represents a greater credit risk — especially for consumers who do not have a long credit history.</p>
							
							<b>New credit takes into account:</b>
							
							<p>Recent requests for new credit. Inquiries remain on a credit report for two years, although FICO scores only consider inquiries from the last 12 months. Note that if a credit report is ordered from a credit reporting agency or www.myfico.com, the score does not count this, as it is not an indication that a person is seeking new credit. Also, the score does not count requests a lender has made for a credit score in order to make someone a pre-approvedcredit offer, or to review an account, even though these inquiries may be seen on a credit report.</p>
							
							<li>Types of Credit in Use: Is it a healthy mix?</li>
							<p>Approximately 10% of the score is based on this category.</p>
							
							<p>The score will consider the mix of credit cards, retail accounts, installment loans, and finance company accounts. It is not necessary to have one of each, and it is not a good idea to open credit accounts just to have them in the mix. The credit mix usually will not be a key factor in determining a score, but it will be more important if a credit report does not have a log of other information on which to base a score.</p>
							
							<b>Types of credit in use takes into account:</b>
							
							<p>What kinds of credit accounts there are and how many of each. The score also looks at the total number of accounts. For different credit profiles, how many is too many will vary.</p>
							
						</ol>
											
					</fieldset> 
					
