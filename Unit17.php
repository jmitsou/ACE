							<li>The most widely used credit bureau scores use a statistical model developed by Fair, Isaac and Company and are known as FICO scores.</li>
							<li>Lenders purchase consumer credit reports of loan applicants in order to assess a prospective consumers past credit performance.</li>
							<li>At present, FICO scoring is the best way to determine future risk based
							solely on credit report data.</li>
							<li>There are 5 main categories of information that are used to calculate a FICO score:</li>
							<ol>
								<li>payment history</li>
								<li>amounts owed</li>
								<li>length of credit history</li>
								<li>new credit</li>
								<li>types of credit in use</li>
							</ol>
							<li>In addition to a credit bureau risk score, consumers and lenders also receive up to four score reason codes. These explain the top reasons for the score and help consumers to locate errors and determine how to improve their score.</li>
							<li>Tips for raising your score include: pay bills on time, keep credit-card balances low and negotiate with creditors if you are having difficulty staying current with payments.</li>
							<li>U.S. law prohibits scoring based on race, color, religion, national origin or marital status.</li>
