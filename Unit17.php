						<legend><b>The Credit/Mortgage Connection</b></legend>
						
						<p>Mortgage lenders and brokers use FICO scores to determine mortgage rates. The FICO score will also determine how a client applies and qualifies for a mortgage. A FICO score is a three-digit number between 300 and 850 that tries to gauge a consumers creditworthiness.</p>
											
						<p>If a score is higher than 700, a client will not only qualify for the best loan terms, but lenders may also grant the loan without asking questions about income and assets. These loans are referred to as no-docsand are fast and easy to get.</p>
						
						<p>If a score is 660 or better, a client will get the best rates available from virtually all lenders. However, full documentation of income, assets, and current debt must be provided.</p>
						
						<p>If a score is between 620 and 660, a client may get a preferred-rate loan but will have fewer lenders to choose from. Lenders will look more closely at any transgressions on the credit report to determine whether they can push the client into the dreaded subprime market.</p>
						
						<p>If a score is less than 620, lenders will consider that client a subprime borrower. Subprime borrowers pay at least 1% more in interest on a home loan and may have to accept features like pre-payment penalties. ”What’s one percentage point?”,you might ask. Just one percentage point difference costs $23,410 for each $100,000 borrowed over thirty years!</p>
									
					</fieldset>
					
					<fieldset id="space">
						<legend><b>TOP 10 REASONS FOR A LOW SCORE</b></legend>
						
						<p>Here are the top 10 most frequently given reasons for low scores. Note that the specific wording given by a lender may be different from these:</p>
						<ol>
							<li>Serious delinquency</li>
							<li>Serious delinquency and public record or collection filed</li>
							<li>Derogatory public record or collection file</li>
							<li>Time since delinquency is too recent or unknown</li>
							<li>Level of delinquency on accounts</li>
							<li>Number of accounts with delinquency</li>
							<li>Amount owed on accounts</li>
							<li>Proportion of balances to credit limits on revolving accounts is too high</li>
							<li>Length of time accounts have been established</li>
							<li>Too many accounts with balances</li>
						</ol>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>RAISING THE FICO SCORE</b></legend>
						
						<p>It is important to note that raising a FICO score is a bit like getting in shape — it takes time and there is no quick fix. In fact, quick-fix efforts can backfire. The best advice is to manage credit responsibly over time.</p>
											
						<p>One general tip is to make sure the information in the credit report is correct. Check the credit report for accuracy at least 90 days before planning any major purchases such as applying for a mortgage. If errors are detected, have them corrected by the lender as well as any credit-reporting agency involved.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>Specific tips for raising a score include:</b></legend>
						
						<li>Pay bills on time. Delinquent payments and collections can have a major negative impact on a score.</li>
						
						<li>If there have been missed payments, get current and stay current. The longer a consumer pays bills on time, the better the score.</li>
						
						<li>Be aware that paying off a collection account will not remove it from the credit report. It will stay on the report for seven years.</li>
						
						<li>If an individual is having trouble making ends meet, encourage him/her to contact current creditors to negotiate an easier payment schedule. This will not improve the score immediately, but if credit begins to be managed with bills paid on time, the score will get better over time.</li>
						
						<li>Keep balances low on credit cards and other revolving credit. High outstanding debt can affect a score.</li>
						
						<li>Pay off debt rather than move it around. The most effective way to improve a score in this area is by paying down revolving credit.</li>
						 
						<li>Do not open a number of new credit cards just to increase available credit. This approach could backfire and actually lower the score.</li>
						
						<li>If the credit history is not long, do not open a lot of new accounts too rapidly. New accounts will lower the average account age, which will have a larger effect on the score when there isn’t a lot of other credit information. Also, rapid account buildup can be seen as risky business.</li>
						
						<li>FICO scores distinguish between a search for a single loan and a search for many new credit lines, in part by the length of time over which inquiries occur. Therefore, do rate shopping within a focused period of time.</li>
						
						<li>Note that it is OK to request and check one’s own credit report and one’s own FICO score. This will not affect a score as long as the credit report is ordered directly from the credit-reporting agency or through an organization authorized to provide credit reports to consumers.</li>
						
						<li>Apply for and open new credit accounts only as needed. Do not open accounts just to have a better credit mix — it probably will not raise a score.</li>
						
						<li>Have credit cards but manage them responsibly. In general, having credit cards and installment loans (and making payments on time) will raise a score.</li>
						
						<li>Note that closing an account that contains a balance does not make it go away. A closed account will still show up on a credit report and may still be considered by the score.</li>
								
					</fieldset>
					
					<fieldset id="space">
						<legend><b>Interpreting the Score</b></legend>

						<p>When a consumer or a lender receives a Fair, Isaac and Company credit bureau risk score, up to four score reason codesare also delivered. These explain the top reasons why the score was not higher. If the lender rejects a request for credit, and the FICO score was part of the reason, these score reasons can suggest why your score was not higher.</p>
											
						<p>These score reasons are more useful than the score itself in helping a consumer determine whether a credit report might contain errors and how the consumer might improve the score over time. However, if the score is already in the mid – 700s or higher, some of the reasons may not be very helpful, as they may be marginal factors related to the last three categories described previously (length of credit history, new credit, and types of credit in use).</p>					
					</fieldset>
					 
					<fieldset id="space">
						<legend><b>What Does NOT Go into a FICO Score?</b></legend>
						
						<li>Race, color, religion, national origin, sex or marital status. U.S. law prohibits scoring from considering these facts, as well as information concerning public assistance or the exercise of any consumer right under the Consumer Credit Protection Act.</li>
						
						<li> Age is not a consideration. Other types of scores may consider your age but FICO scores cannot under <b class="teal-text">the Equal Credit Opportunity Act.</b></li>    
						   
						<li>Salary, occupation, title, employer, date employed or employment history. Lenders may consider this information, however, as may other types of scores.</li>
						
						<li>Place of residence.</li>
						
						<li>Any interest rate being charged on a particular credit card or other account.</li>    
						
						<li> Any items reported as child/family support obligations or rental agreements.</li>
						    
						<li>Certain types of inquiries. The score does not count requests the consumer has made for his/her credit report. It also does not count promotional inquiries — requests made by lenders in order to offer pre-approved credit or administrative inquiries — requests made by lenders to review an account. Requests that are marked as coming from employers are not counted either.</li>    
						    
						<li>Any information not found in the credit report.</li> 
						
						<li>Any information that is not proven to be predictive of future credit performance.</li>
																	
					</fieldset>
					
					<fieldset id="space">
						<legend><b>FALLACIES AND FACTS</b></legend>
							
						<p>Fallacy: A poor score will haunt a person forever.</p>	
						
						<p>Fact: Just the opposite is true. A score is a snapshotof a person’s risk at a particular point in time. It changes as new information is added to bank and credit bureau files. Scores change gradually as a person changes the way he/ she handles credit. For example, past credit problems impact a score less as time passes. Lenders request a current score when a consumer applies for credit, so they have the most recent information available.</p>	
							
						<p>Fallacy: Credit scoring is unfair to minorities.</p>
							
						<p>Fact: <b class="teal-text">The Equal Credit Opportunity Act</b> prohibits lenders from considering race, nationality, gender or marital status. Independent research has monitored and continues to monitor the effectiveness of the ECOA .</p>
							
						<p>Fallacy: Credit scoring infringes on privacy.</p>
							
						<p>Fact: FICO scores evaluate a credit report alone which lenders already use to make credit decisions. A score is simply a numeric summary of that information. In fact, lenders using scoring can often ask for less information about a consumer. They may have fewer questions on the application form for example.</p>
							
						<p>Fallacy: A score will drop after applying for new credit.</p>
							
						<p>Fact: Probably not much. If several credit cards are applied for within a short period of time, multiple requests for credit report information (called inquiries) will appear on the report. Looking for new credit can equate with higher risk, but most credit scores are not affected by multiple inquiries from auto or mortgage lenders within a short period of time. The FICO score treats these as a single inquiry which will have less impact on the credit score.</p>
							
						<p>Fallacy: The score determines whether or not a consumer gets credit.</p>
											
						<p>Fact: Lenders use a number of facts to make credit decisions, including the FICO score. Lenders look at information such as the amount of debt a person can reasonably handle given his/her income, employment history, and credit history. Based on their perception of this information as well as their specific underwriting policies, lenders may extend credit although the score is low, or decline a request for credit although a score is high.</p>					
					</fieldset> 
					
