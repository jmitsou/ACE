			<div class="row">
				<div class="col m9 l9">
					<fieldset id="space">
						<legend><b>OBJECTIVES</b></legend>

					    <li>To recognize the importance of trust as the foundation for positive interaction.</li>
					    <li>To determine the three essential elements for building rapport.</li>
					    <li>To explore ways to convey empathy, genuineness, and positive regard.</li>
					    
					    <p>Establishing a positive relationship with the client is a necessary prerequisite for viable problem solving and decision making to take place. Counselors act as a bridge between client dilemmas and workable solutions if and only if the necessary conditions exist that serve to create rapport and confidence between people. Effective counselors apply skills that help clients feel comfortable, attended to, and in control.</p>

						<p>All positive interpersonal relationships begin when a bond of trust and acceptance is established. Without this bond, the relationship cannot move forward. Just how is trust established between two people who have no prior knowledge of each other? How are reluctant clients put at ease? What do counselors do that allows for positive interpersonal communication to take place? This unit will explore these counseling issues as well as specific techniques for building viable relationships with clients.</p>
					
					</fieldset>
					
					<fieldset id="space">
						<legend><b>BUILDING TRUST</b></legend>
							
						<p>Have you ever met someone who won your trust right away? If you think about it, it was probably because the person seemed genuine, respectful, and caring. These are the qualities counselors strive to convey to their clients right from the start. In the initial interview, effective counselors communicate empathy, genuineness, and positive regard to clients who are often uneasy or inexperienced with the counseling process.</p>
						
						<b>Skilled helpers work to establish a bond of trust between themselves and their clients by understanding the 3 elements that go into rapport development:</b>
						
						<p><b>1. Empathy.</b> Empathy is the ability to understand what another individual is going through. You relate to their situation as if it were your own. However, you never loose sight of the “as if.” You remain objective and professional while showing the client that you care about the problem being presented and you want to help solve the problem. You convey that you are involved with helping the client in human terms. There is debate as to whether empathy is something that can be learned or if it is simply a personality trait. It is possible, nonetheless, to increase one’s level of sensitivity and awareness about another person’s circumstance.</p>
						
						<p><b>2. Genuineness.</b> Genuineness is the quality of being yourself with the client beyond your role as a skilled helper. It’s easy to forget that you can and should use your personality in professional interactions. Clients need to know they are talking to another human being — not just an efficient, knowledgeable professional. Effective counselors convey spontaneity and honesty that go beyond a business-like facade.</p>
						
						<p><b>3. Positive Regard.</b> Counselors convey positive regard by showing respect for the client as an individual with inherent value and dignity regardless of any external factors the client may demonstrate. Positive regard is giving affirmation to the client. Counselors strive to remain non-judgmental. They work to encourage the client through an often difficult situation by demonstrating respect and acceptance.</p>
																
					</fieldset>
					
					<fieldset id="space">
						<legend><b>DEVELOPING EMPATHY</b></legend>
							
						<p>Empathy is a communication skill that can be developed and increased by considering the following 2 points:</p>
						
						<p><b>1. Understand the Effects of Counseling on Clients.</b> Increasing empathy begins by increasing your awareness and understanding of who the client is and how he/she relates to the counseling process. Let’s begin by recognizing that counseling is not always an easy process for clients. Clients who seek out counseling may feel defeated or embarrassed. Many contact a counselor as a last resort when the effects of severe outstanding debt can no longer be ignored. Others are in crisis situations regarding money and inter-family conflict.</p>
						
						<p>Yet, they face a daunting challenge. Some face the stigma that going through any form of counseling is a sign of weakness or inadequacy. They feel as though they are relinquishing their sense of independence and self-reliance. Humbled and uneasy, they must search for someone they can trust with their finances, someone who has their best interest in mind, who possesses the competencies and expertise to assist them.</p>
						
						<p>Clients straddle an emotional fence between hope and doubt. During the assessment stage of the interview, clients experience a range of both positive and negative effects. On the positive side, they may feel a sense of relief as they disclose their situation to someone else. Finally, someone is listening! Finally, I am doing something about this problem! Psychologically, they are removing debt from their lives as they giveit (accounts and amounts) to the counselor.</p>
						
						<p>On the negative side, clients may feel anxious and vulnerable during the interview. If the questioning is too rapid or if the counselor exhibits aggressiveness, the client may feel interrogated and evaluated. Clients come to counseling with conflicting emotions. It is important to remain aware of the
						difficult challenge they face in counseling. Be sensitive to their circumstances.</p>
						
						<p><b>2. Increase Awareness and Sensitivity of Cultural Differences.</b> When we think of culture, we usually confine its meaning to differences in race and/or ethnicity. However, the word culture conveys a much broader meaning as it relates to counseling. Culture includes:</p>
						
						<p><b>Demographic variables</b> — age, sex, and place of residence</p>
						<p><b>Status variables</b> — level of education, income bracket</p>
						<p><b>Ethnographic variables</b> — race, nationality, language, religion</p>
						
						<p>With this view of culture, all counseling is multicultural. It crosses parameters of different, sometimes opposing, world views, values, attitudes, and expectations. Even if the client and counselor are both Hispanic, for example, one may be a middle-aged female and the other one a male in his early twenties. Or, you and your client may be of the same sex and age group but your level of education is different. It is important for counselors to be aware of all these differences and the impact they have on how clients see problems, make decisions, set goals, and accomplish tasks.</p>
						
						<p>Effective counselors strive to avoid what is referred to as cultural encapsulation which is perceiving reality through only one set of cultural assumptions. Someone who is culturally encapsulated is insensitive to cultural differences and sees the values and perceptions of their culture as the only true and legitimate one. Acceptance of client diversity is an essential ingredient for productive client/counselor interaction to take place.</p>
						
						<p>It’s important that counselors understand who they are in the cultural mix. Who are you and how does your identity impact how you see the world around you? Accepting others begins by accepting the fact that you represent just one variation in a world of cultural possibilities.</p>
									
					</fieldset>
					
					<fieldset id="space">
						<legend><b>CONVEYING GENUINENESS</b></legend>
						
						<p>Counselors convey that they are genuine by making sure that what they say and how they feel are the same. This is known as congruence. Your statements and actions reflect how you truly feel. Counselors who demonstrate congruence are aware that clients are sensitive to mixed messages. It is important for counselors to be able to evaluate their own feelings and behaviors during the counseling session.</p>

						<p>Genuineness is also demonstrated through openness to self-disclosure. Though the focus of the interview is on the client, sometimes the tables get turned when the client asks personal questions of the counselor. This is a natural part of the process as clients seek to find out to whom they are talking. For example, a client may ask, ”Are you married?”, “Do you go to school?” or “How long have you been counseling?” It is best to answer such direct questions with a direct answer. All self-disclosure should be brief and honest. Providing clients with a little personal information (when they ask) serves to increase the comfort level of the session.</p>
						
						<p>Finally, counselors convey genuineness through immediacy. Immediacy is a special kind of spontaneous communication. Counselors use immediacy to respond to events as they occur in the helping process. In other words, the process of counseling — following the prescribed steps — can and should be interrupted to allow the counselor to communicate what is observed at the moment. Phrases that contain counselor immediacy begin with “I see that you are…” or ”I get the feeling that…”.Immediacy in the counseling session works to overcome feelings of distance, guardedness, or hesitancy in client/counselor communication.</p>
										
					</fieldset>
					
					<fieldset id="space">
						<legend><b>SHOWING POSITIVE REGARD</b></legend>
						
						<p>Counselors demonstrate positive regard by attending to the client through both verbal and non-verbal behaviors. Non-verbal behaviors are associated with a smiling or interested facial expression, relaxed posture, and good eye contact. Surprisingly, these elements of communication can be transmitted during telephone contact.</p>
											
						<p>Verbal behaviors that convey positive regard are the use of a soft or soothing tone of voice as well as the use of enhancing statements. Enhancing statements serve to provide what all clients need from a skilled helper –  acceptance. Often a client may communicate information that prompts disapproval. However, the counselor avoids making judgments concerning client values and attitudes since this would be counter-productive to a positive change. Instead, effective counselors give enhancing statements that focus on the positive and affirming aspects of the client. The goal is always to provide encouragement and support to the client. Counselors refrain from instructing, correcting, or chastising clients.</p>
												
					</fieldset>
					
					<fieldset id="space">
						<legend><b>Let’s review the counselor actions that help create positive and productive relationships with clients:</b></legend>
							

						<p><b>1. Establish trust.</b> Trust is the foundation on which the client/counselor relationship is built.</p> 
						
							<p>Trust is established when:</p>
							<ol>
								<li>He counselor communicates to the client that he/she has the client’s best interests in mind.</li>
								<li>The client feels confident that the counselor has the expertise and knowledge necessary to help with their dilemma.</li>
							</ol>
						
						<p><b>2. Show empathy.</b></p> 
						
							<p>Professional counselors strive to:</p>
							<ol>
								<li>Understand how the counseling process affects clients.</li>
								<li>Gain awareness and sensitivity about client cultural differences.</li>
								<li>Gain understanding of themselves among diverse groups.</li>
							</ol>
						
						<p><b>3. Be genuine.</b> It is important for counselors to meet the human needs of the client as well as the business needs.</p> 
						
							<p>Effective counselors:</p>
							<ol>
								<li>Convey openness and spontaneity.</li>
								<li>Avoid hesitancy, inconsistency, and defensiveness.</li>
								<li>Are comfortable expressing their personality with clients.</li>
							</ol>
						
						<p><b>4. Demonstrate positive regard.</b> 
						
							<p>Effective counselors:</p>
							<ol>
								<li>Avoid making judgments about clients</li>
								<li>Convey acceptance toward clients</li>
								<li>Focus on the positive aspects of the client</li>
							</ol>
						
						<p>When the human needs of the client are attended to, a positive relationship is created. The client becomes a willing participant in the counseling process.</p>				
					</fieldset>
				
					<fieldset id="space">
						<legend><b>SUMMARY</b></legend>
					
						<ol>
							<li>Effective counselors exhibit empathy, genuineness and positive regard to establish rapport and trust with their clients.</li>
							<li>Clients may experience a range of emotions during the counseling session from anxiety and doubt to relief and hopefulness.</li>
							<li>Counseling is multicultural in that counselors often crosses parameters of different world views, values and attitudes by interacting with diverse clients.</li>
						</ol>
						
