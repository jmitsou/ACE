						<legend><b>4 TYPES OF REFLECTING STATEMENTS</b></legend>
						
						<p>There are many obstacles in the encoding process. Because of this, the client may fail to communicate clearly what he/she actually means. At times, the client may not be sure what he/she is trying to say, let alone communicate the thought properly.</p>
						
						<p>To avoid misinterpreting what the client is saying, the counselor must decode the clients message through non-judgmental feedback. The client then has the opportunity to clarify his/her statement so that it more closely coincides with the intended message. Reflective listening consists of a give and take between client and counselor.</p>
						
						<b>Reflective listening involves the following four techniques:</b>
						
						<p><b>1. Clarifying.</b>This technique helps to gather accurate data, facts, or additional meanings. Asking clients to clarify a statement will alert them to the fact that they need to explain details more clearly. Encourage clients to say or do more to clarify communication:</p> 
						
						”Would you please clarify that?”<br />
						   ”I’m not sure I understand what you just said.”
						
						<p><b>2. Paraphrasing.</b> When counselors paraphrase client responses, clients gain assurance that what they said was communicated accurately. This does not mean parroting the client. Counselors communicate back client messages in their own words preceded by phrases such as:</p>
						
						”As I understand you…”<br />
						   ”Correct me if Im wrong…”<br />
						   ”In other words …”
						
						<p><b>3. Summarizing.</b>Summing up client responses helps to connect various segments of the conversation into a meaningful whole. Effective counselors determine the main point behind the details of client talk and give back a statement that puts it in a nutshell.Summarizing gives a sense of closure to a conversation:</p>
						
						”The point is…”<br />
						”In general, what you’re saying is…”<br />
						”Summing up what you said…”
						
						<p><b>4. Reflecting Feelings.</b> The focus of this method is on the tone of the client’s message. What the counselor now wants to do is reflect or mirror back the client’s feelings, attitudes, and personal meanings. In effect, the counselor tries to get emotionally in tune with the client. Avoid attempts to pamper or sympathize with the client by saying”I know what you are going through.” This is a sure way to alienate or frustrate the client. The client should sense that the counselor understands by reflective expression. Show empathy without patronizing the client:</p>
						
						”It appears that you feel…”<br />
						”It sounds like you feel…”						
						
						<p>When you begin listening reflectively, you may feel that you are not being yourself or you are being artificial. This is a natural response. As you acquire any new skill, you will experience an initial stage of awkwardness. This is true with reflective listening. Integrating these new techniques into your everyday communication takes time and practice. Remember that acquiring good listening skills is not only important in counseling but extremely useful for all types of interpersonal communication.</p>
						
						<p><b>Non-Reflective Listening</b> consists of verbal affirmations that encourage the client to continue communicating. These include short phrases such as “I see”, “Yes”,”Go on”,and “Mm-mm.” It is important to avoid any verbal phrase that is negative or judgmental like”Ugh!”, “Thats terrible!” or “Are you kidding?”</p> 
						
						<p>Non-reflective listening is particularly useful when a client appears hesitant during communication. Simple verbal encouragements go a long way toward helping a client relax and feel assured that what he/she has to say is important for the counselor to hear. Remember, you are gathering information. Being nonjudgmental and affirming are key ingredients for allowing the client to open up with the genuine and accurate information you will need to facilitate solutions.</p>
						
						<p><b>Example 1</b><br />
						Ray: My wife used to do our finances, but… (sighs)</p>
						
						Counselor: Go on.<br />
						
						<p>Ray: Well, now that she’s working and I’m not, the responsibility falls on me to keep track of the bills.<br /></p>
						
						Counselor: I see.<br />
						
						<p>Ray: But I guess I haven’t done such a good job. I mean…<br /></p>
						
						Counselor: Yes.<br />
						
						<p>Ray: I’m just not a detail-orientated guy.</p>
						
						Notice how the use of non-reflective listening helped the counselor to uncover a potentially useful bit of information, namely, the client’s belief that he’s not good with details.
						
						<p><b>Example 2</b><br />
						Amy: Basically, my parents have cut me off financially.</p>
						
						Counselor: I see.<br />
						
						<p>Amy: But…</p>
						Counselor: Please continue.<br />
						
						<p>Amy: They said I could move back with them until I get this straightened out, but that’s… I dont know.</p>
						
						Counselor: Go on.<br />
						
						<p>Amy: I really want to stay where I am. I’ve got a really fun job at a great restaurant and all my friends live around here and…</p>
						
						Counselor: Yes.<br />
						
						<p>Amy: The money’s supposed to be really good.</p>
						
						<p>Here again, the counselor uncovers some key information about the client’s situation using non-reflective listening. Specifically, her job as a waitress is supposed to bring in good money.</p>
						
						<p>Both reflective and non-reflective listening are excellent techniques for achieving the process goals of counseling. They both work to establish a non-threatening environment where interest and acceptance are expressed. Consider how the negative, judgmental phrases “Ugh!”, “Gee!”, or “That’s crazy!”could inhibit genuine and accurate communication.</p>
									
