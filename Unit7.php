					<fieldset id="space">
						<legend><b>THE 3 COMPONENTS OF ACTIVE LISTENING</b></legend>
											
						<p>There are three components to active listening: what you do, what you say, and what you observe during the counseling session. Let’s look at the specific strategies for each of these processes.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>WHAT YOU DO</b></legend>
							
							<p>This component consists of four things that effective counselors do to create a caring and respectful communicative environment for the client:</p>
							
							<p><b>1. Maintain an upright, relaxed posture.</b> Turn so you are completely facing the client. This demonstrates that he/she has your undivided attention. Avoid crossing your arms in front of you as this suggests an aloof, judgmental attitude. Also, avoid propping your head up with your hand. This is a sure indication of either boredom or tiredness.</p>
							
							<p><b>2. Establish eye contact.</b> Always look directly into the clients eyes whenever you are speaking to the client or he/she is speaking to you. However, you will want to avoid a cold, unwavering stare.</p>
							
							<p><b>3. Use appropriate facial expressions.</b> Smile during social communication. Show seriousness (usually mouth closed and eyebrows slightly squeezed together) when the topic is of a serious nature.</p>
							
							<p><b>4. Provide non-verbal feedback.</b> This includes nodding your head to acknowledge what the client is saying. It communicates that you are paying close attention and that you fully understand what is being related.</p>
				
					</fieldset>
					
					<fieldset id="space">
						<legend><b>How Does What You Do Apply to Telecommunications?</b></legend>
													
						<p>You may be wondering how the physical actions of counselors have any effect during telephone communication. Surprisingly, what you can’t see, you can often hear.</p>

						<p>Vocal tone and delivery can alter dramatically depending on posture and facial expression. Have you ever answered the phone while lying in bed? An observant caller will notice a change in your voice and may comment, “You sound funny”or “Did I wake you?” Similarly, clients who need your energized assistance will pick up on clues that you are not sufficiently engaged should you be slouched over your desk or leaning back in your chair with your feet up.</p>

						<p>And is it not possible to”hear”a smile over the telephone? Try this simple experiment: Say, “Hello, how are you”first while frowning and then while smiling broadly. The words are the same but the effect is completely different.</p>

						<p><b><i>But how can I make eye contact with someone on the phone?</i></b><br />
						Here, its a matter of focus. Keep a small pad next to the telephone and write out the client’s name. This provides a visual aid that keeps your eyes from wandering around the room. By focusing on something visual that relates to the client, you can avoid becoming distracted.</p>
						
						<p><b><i>What about the use of non-verbal feedback?</i></b><br />
						Who does not make use of visual signals such as nodding and hand gestures while on the telephone? These physical movements help with the flow and effectiveness of your vocal delivery. Words that require emphasis get a boost from talking hands.</p>

						<p>One final point on the doing component of active listening. Today we often find ourselves doing more than one thing at a time. We read the paper while eating breakfast, shave or apply make up while driving, and go through our mail while watching television. While multitasking saves time in our busy schedules, it really has no place in the realm of counseling. Counseling requires that you give clients YOUR FULL ATTENTION.</p>					
