						<legend><b>REFLECTIVE AND NON-REFLECTIVE LISTENING</b></legend>
							
						<p>The two most important counseling techniques that optimize listening are reflective and non-reflective listening. These two techniques provide specific ways that counselors respond to clients as they speak.</p>
						
						<p><b>Reflective listening</b> is giving back to the client the messages received by the counselor. It involves reflecting statements that paraphrase, clarify, or summarize what the client has communicated. Reflective listening also involves responding to the clientsemotional state. Most importantly, reflective listening is a way of responding to client talk in a non-judgmental and accepting way. Instead of saying “Calm down” to an irate client, effective counselors say,”It sounds like you’re really upset about this.” Instead of trying to change a client’s behavior, the emotional state of the client is acknowledged.</p>
						
						<p><b>Non-reflective listening</b> is the simplest form of listening. This type of listening involves attentive silence with minimal vocal response. Appropriate nodding, verbal affirmation, and facial expressions encourage the client to continue talking. When possible, counselors should use encouraging responses that are nonjudgmental in nature. Examples of these short responses include “Please continue”, “All right”, and “Yes.”<br />
						
							  Many times the client will render cues which suggest serious hidden details. Non-reflective listening can also be utilized to draw out information which the client is hesitant to disclose. It is an important strategy for prompting the client forward with sometimes sensitive details. Money is a sensitive issue. Remember to be non-judgmental!</p>
						
					</fieldset>
					
