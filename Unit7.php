						<p>There is no better way to communicate empathy, genuineness, and positive regard than to actively listen to the client. All the techniques utilized by counselors hinge upon your ability to give this form of undivided attention to the individual seeking help. What is active listening and how does it work?</p>
						
						<p>Active listening is showing respect and demonstrating interest. It is the process by which the human needs of the client are met. It allows the client to relax and therefore, communicate more information more accurately. This, in turn, benefits the counseling process as client situations, obstacles, and needs become better understood. Through the process of active listening, the barriers of communication that often cause clients to withhold crucial information are removed.</p>
						
						<p>Everyone loves a good listener. In a world where we are constantly bombarded by outside messages, the good listener is like an island of tranquility. Someone is listening to you! They are attentive and interested in what you have to say. They do not interrupt you even as you pause to form your thoughts. For many people, being attended to by a good listener is a rare experience.</p>
						
						<p>Counselors take good listening to the next level. They do this by incorporating specific techniques that serve to ensure total and accurate transmission of their clients messages. We will learn these essential techniques as we explore the components of active listening.</p>

