						<legend><b>WHAT YOU SAY</b></legend>
							
						<p>The saying component of active listening can be divided into reflective and non-reflective listening techniques. With reflective listening, counselors reflect back the content as well as the feelings of client messages.</p>
						
						<p><b>Example 1</b><br />Al: I’ve always worked for myself, but now the cost of the medical care my daughter needshas got me thinking about a job that provides benefits. Still, I can’t stand the idea of working 9-5.</p>
						
						<p>Counselor: It sounds like you’re torn between the freedom of working for yourself and the security that a benefits package can offer.</p>
						
						<p><b>Example 2</b><br />Sarah: Right now, I’m receiving child support payments which just barely cover the monthly cost of day care. But there’s still all the other expenses of running a family. I mean I have to work long hours every day.</p>
						
						Counselor: So, you’re concerned you still have to work so much even though you are receiving child support.
						
						<p>In both examples, the counselor reflected back to the client the content and emotion of the message. Using this technique facilitates communication in two ways:</p>
						
						<ol>
							<li>1. It assures the client that you received the correct message.</li>
							<li>2. It shows empathy and caring while avoiding any judgmental comments.</li>
						</ol>
						
