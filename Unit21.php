			<h4 id="ht">Unit 21: Getting Out of Debt</h5>
			<div class="row">
				<div class="col m9 l9">
					<fieldset id="space">
						<legend><b>OBJECTIVES</b></legend>
						
						<li>To explore consumer options for dealing with debt.</li>
						<li>To examine the difference between Chapter 7 and Chapter 13 Bankruptcy.</li>
						<li>To identify what debts CANNOT be discharged in a Chapter 7 Bankruptcy.</li>
						
						<b>Consumers who are struggling with unresolved debt have 2 basic options:</b>
						
						<ol>
							<li>They can resolve the debt.</li>
							<li>They can file bankruptcy.</li>
						</ol>
						
						<p>Most consumers will wish to avoid bankruptcy if at all possible. Bankruptcy is available to the consumer as a last resort when the amount of unpaid debt is simply beyond the consumers ability to pay back.</p>
						
						<p>Therefore, it is important to carefully consider all the options available to resolve debt issues with the least detrimental effects to both the consumer’s current spending plan and his or her credit rating. You will see that there are several options for avoiding bankruptcy but some are misleading and may even exacerbate a situation that is already desperately out of control.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>RESOLVING THE DEBT</b></legend>
							
						<p>For the most part, clients will want to resolve their debt issues through some kind of workout. That means paying back their creditors all or at least an agreed upon portion of the debt. This plan of action presents several alternatives. Arrangements for repayment include a lump-sum discount or reduced payment amounts with an increased term of repayment. Reduction or elimination of interest may or may not be a feature with these arrangements.</p>
							
						<p>The options available to the consumer facing unresolved debt will depend on his or her financial circumstance. Generally, the more dire the circumstance, the fewer the options. Let’s take a look:</p>
							
						<p><b>Consumer Workouts</b>. Consumer workouts occur when those who are having trouble paying their bills contact their creditors directly and attempt to negotiate an agreed upon arrangement for payment. Usually creditors accommodate sincere consumers when they try to remedy their difficulty or inability to pay a bill. However, as soon as consumers fall behind in payments — even by a month or two — the creditor has the upper hand. Creditors can easily penalize those who do not pay on time by charging late fees and adding blemishes to a credit report. In other words, negotiating with creditors is not always easy to say the least. Resolving debt without professional assistance can be a viable option for those experiencing a temporary financial setback. However, negotiating with creditors requires motivation, commitment and a spending plan.</p>
							
						<p><b>Debt Consolidation Loans.</b> Debt consolidation loans give consumers the ability to pay off their high-interest accounts typically through lowered monthly payments. However, the term of the loan agreement is longer. This type of repayment plan requires that the consumer apply and qualify for the loan. That means the consumer must have the ability to make on time payments. It also means the consumer’s credit rating has to be in relatively good shape.</p>
							
						<p>The most common type of consolidation loans today are home equity loans. Homeowners who find themselves in debilitating debt have the option to pay off their accounts by using the equity they have earned as collateral to procure a loan. The downside, of course, is that consumers are trading their unsecured debt for secured debt. If they default on a home equity loan, the lender could initiate the foreclosure process. Consumers who opt for a home equity loan must have self control so they do not continue to use credit as they have in the past. A survey conducted by Consumer Bankers Association found that within one year, 70% of the people who got home equity loans were again running up credit debt.</p>
							
						<p>Another type of debt consolidation is O% transfers. These offers sound good but can be extremely costly for the consumer. Your unpaid balance from a high-interest loan is transferred to a new O% interest account. This could be beneficial if the consumer is truly committed to making consistent payments. However, there are some startling disadvantages to this plan. First, any new purchases are still charged interest. Furthermore, any payments the consumer makes go only to the transferred balance amount. So, you can’t take care of the balances arising from new purchases until the transferred amount is paid off. O% transfers typically come with severe penalties. For example, if the consumer makes one late payment, that O% is history. Consumers who choose this method of dealing with debt must restrain themselves from making new credit purchases until the transferred account is paid in full.</p>
							
						<b>Chapter 13 Bankruptcy is a court approved consolidation.</b>
							
						<p><b>Credit Counseling.</b> Credit counselors may enroll clients in debt management plans or DMPs that involve one monthly payment. That payment is handled by the agency and dispersed to the various creditors. They receive what is known as fair sharefrom the creditors which is a percentage of the collected payment. To qualify for credit counseling, consumers must have a steady source of income in order to make regular monthly payments. They also must be committed to resolving their outstanding debt as a DMP may take up to four years to complete depending on the amount of debt.</p>
							
						<p><b>Debt Settlement.</b> Debt settlement, sometimes referred to as debt negotiation, offers consumers the option to resolve their debts with a lump-sum payment. The debtor must come up with the agreed upon amount in order to pay off the debt all at once. This could require a period of time in which to save the necessary funds. In some cases, a client in debt settlement pays into an account. As cash accumulates, the debt negotiator works out a reduced settlement amount with the creditors. This alternative holds promise for consumers with sizable debt issues who wish to avoid a declaration of bankruptcy. Typically, clients in debt settlement must endure a period of increased debt collection activity while they cease making regular payments to their creditors. However, for many debtors, this is a viable alternative to a Chapter 7 Bankruptcy.</p>
							
						<p><b>Budget Counseling.</b> Many individuals who are experiencing financial stress would benefit from budget counseling. This type of counseling is offered by agencies to help consumers analyze their monthly expenses and develop a spending plan so they can pay their bills on time. In some cases, budget counseling is also offered to support clients who enroll in debt management and debt settlement plans.</p>
							
						<p>Consumers in debt must wrestle with a number of issues if they are to resolve overdue bills. First and foremost is coming up with the money to cover the debts. Many individuals and families are simply over their financial heads with unpaid debt. Basically, they must cut their expenses, increase their income, or both.</p>
							
						<p>Another big issue to consider is the consumer’s credit rating. How will it be affected by a particular repayment option? This is especially important when consumers have maintained a good payment history and their credit rating is in good standing. However, some consumers will have been delinquent for an extended period of time causing their credit rating to have already been severely damaged.</p>
							
						<b>In all cases, dealing with debt requires 3 things:</b>
							
						<ol>
							<li>The client must fully understand his or her financial situation. That includes knowing all incomes and monthly expenses as well as his or her current credit score.</li>
							
							<li>The client must develop a plan of action based on personal goals and a complete evaluation of all options appropriate for the client.</li>
								
							<li>The client must be committed to paying off delinquent debt. Paying off debts needs to be seen as a way of taking control of life and getting free from financial stress. Resolving debt can literally take anywhere from several months to several years. Clients need a stick-to-it attitude to succeed.</li>
						</ol>
												
					</fieldset>
					
					<fieldset id="space">
						<legend><b>Reasons NOT To Go It Alone</b></legend>
						
						<p>Many consumers are reluctant to seek help with their debt issues. They may feel that their personal financial problems are a private matter. They may also feel that reaching out for assistance is a sign of weakness. However, there are a couple of points to consider that may help clients realize that personal debt issues, especially severe debt, can best be resolved with professional help.</p>
						
						<p>First, dealing with debt is not pleasant. It requires time on the telephone talking to creditors who may or may not wish to negotiate with you. It takes a certain level of tenacity that many people just do not possess. Also, going it alone can create a lot of aggravation and stress.</p>
						
						<p>Debt management and debt settlement agencies are often in a much better position to negotiate with creditors. They have developed working relationships with lenders and credit-card issuers. Also, because of the large number of accounts that organizations handle, they have the opportunity to receive volume discounts. Finally, agencies know about the latest industry trends and special offers for resolving delinquent accounts.</p>
						
						<b>Effective counselors help clients to consider all the alternatives.</b>
									
					</fieldset>    
					
					<fieldset id="space">
						<legend><b>A CLOSER LOOK AT CREDIT COUNSELING</b></legend>
									
						<p>According to the Pension Protection Act of 2006, credit counseling agencies must tailor their services to meet the individual needs of the client. The best agencies already adhere to this now mandatory standard.</p>
							
						<p>Effective agencies strive to emphasize the counseling aspect of credit counseling — especially during the initial interview. Gathering incomes and expenses from vulnerable clients takes time. It also takes time to determine the type of service most appropriate for the client. Effective counselors focus on the client’s complete financial picture.</p>
							
						<p>In some cases, a client‘s debt situation can be addressed through a debt management plan. A debt management plan or DMP is the implementation of an agreement between the agency and the client’s creditors. It allows the client to make one single payment that is then distributed to the creditors participating in the plan. Typical plans can last from three to five years.</p>
							
						<p><b>How a Debt Management Plan Works</b><br />A DMP begins when the creditors accept an individualized proposal for repaying delinquent loan amounts. An accepted proposal is known as a Debt Management Agreement. For participating in a repayment plan, clients will often receive concessions from the creditors.</p>
							
						<p><b>TYPICAL CREDITOR CONCESSIONS</b><br />Interest rates are lowered or eliminated. After the client establishes regular payments, many creditors will reduce or eliminate interest fees. More of the client’s money goes towards paying off the principle as opposed to finance charges.</p>
							
						<p>Accounts are re-aged. Even before a client has paid off an account, the creditor will often re-age the account so that its status is current. Usually, this benefit occurs after the client has made three or four on-time payments to the DMP. In some cases, the account is re-aged when the proposal is accepted by the creditor.</p>
							
						<p>Late and over-the-limit fees are eliminated. Depending on the creditor, late and over-the-limit fees are eliminated at the point when the proposal is received or when the account is re-aged (made current). Some creditors do not impose late fees as long as the client is in a DMP. Others, however, will continue to charge late fees in the event that payments are not on time.</p>
							
						<p>In some cases, these benefits to the client are automatic. In other cases, the client receives the concession after the payment plan has been established through regular, on-time payments.</p>
												
					</fieldset> 
					
					<fieldset id="space">
						<legend><b>SUPPORT AND MAINTENANCE OF THE PLAN</b></legend>
							
						<p>Throughout the DMP, clients can expect assistance from the agency. Large agencies usually have a customer service department to handle any problems or issues that might come up. Throughout the process, the counselor or customer service agent acts as a liaison between creditor and client to resolve problems. Issues that may arise include:</p>
							
						<p><b>Billing errors.</b> A late fee or other type of fee may appear on a billing statement that is not listed in the agreement.</p>	
							
						<p><b>Payment not received by creditor.</b> Payments involve money orders, cashiers checks, or automatic account withdrawals. It is a priority that these payments are received on time.</p>	
							
						<p><b>Changes in the DMP.</b> During the course of the plan, various changes may occur. Creditors may be added or, when an account is paid off, removed from the plan. Also, the client may initiate various changes as to when and how they will make their monthly payments.</p>		
												
					</fieldset>
					 
					<fieldset id="space">
						<legend><b>APPROPRIATENESS OF A DMP</b></legend>
											
						<p>Whether the program is appropriate for a particular client depends on:</p>
						
						<p><b>The amount of debt.</b> The debt load must be within the parameters set according to the policies of individual agencies. If the amount of debt is lower than the threshold amount, other options may present themselves during the counseling session. Over a certain amount, the plan may not accommodate it.</p>	
						
						<p><b>The type of debt.</b> Generally speaking, debt management helps in the resolution of unsecured debt. Creditors of secured loans do not need to negotiate with borrowers. Their loans are backed by collateral.</p>	
							
						<p><b>The client’s current financial situation.</b> The client’s circumstance must be considered holistically. In other words, it’s not just about their unsecured debt dilemma. Other factors include their level of secured debt, their monthly budget, and the client’s short and long-term financial goals.</p>	
							
						<p>Agencies often determine a minimum debt amount in order to enroll in a DMP. The debts generally must be unsecured although some agencies assist with other types of debt. Also, the client must show a level of monthly income sufficient to maintain the proposed payment plan.</p>
								
					</fieldset>
					
					<fieldset id="space">
						<legend><b>BENEFITS TO THE CREDITOR</b></legend>
						
						<p>From the creditor’s point of view, customers who enroll in debt management plans are doing something about their debt. Though they may be in default of a loan, the creditor is open to negotiation because, with a commitment to debt management, the customer in default has pro-actively taken the first step. Both creditor and borrower are linked in their desire to resolve the debt.</p>
						
						<p>A debt management plan is a cost effective alternative to the creditor selling a delinquent account to a collection agency often for as little as $.020 on the dollar. Instead, the creditor makes a fair share contribution to the credit counseling agency while recouping a larger percentage of the original debt.</p>
						
						<p>The debt management plan is also a viable alternative to bankruptcy for many delinquent borrowers. Unsecured creditors have less power than secured creditors when it comes to a default. There is no collateral to seize. Unsecured debts are usually wiped out in a Chapter 7 bankruptcy.</p>
						
						<p>Another benefit to the creditor is that participation in a DMP shows that the creditor is willing to work with consumers in financial trouble. It helps the creditor from a public relations standpoint.</p>
										
					</fieldset>					

					<fieldset id="space">
						<legend><b>RESPONSIBILITIES OF THE CLIENT</b></legend>
						
						<p>The Federal Trade Commission has recommended that clients participating in a debt management plan do the following:</p>
							
						<p><b>Make regular, timely payments.</b> Usually, clients can choose what time of the month their payment becomes due. They can also opt for an automatic deposit of their monthly payment.</p>
							
						<p><b>Monitor your monthly statements.</b> Clients should not relinquish their responsibility to check and read mailings from their creditors. Participation in a DMP does not mean you get to throw away you billing statements! Alert the agency immediately if the creditor is not receiving payments.</p>
							
						<p><b>Inform the agency about any changes in your circumstance.</b> Just as you would contact a creditor if you are having difficulty making a payment, clients need to let their counselors know when there is a problem. Often the agency will offer budget counseling or the plan can be temporarily adjusted to accommodate the client.</p>					
					</fieldset>
					 
					<fieldset id="space">
						<legend><b>FREQUENTLY ASKED QUESTIONS</b></legend>
							
						<p>While it is important for counselors to assess the client’s financial situation, it is also crucial that credit counselors provide answers to questions that clients will have regarding the process of debt management. Here are some of the more frequently asked questions fielded by counselors.</p>
							
						<p><b>How can a debt management plan help me with my monthly budget?</b><br />By consolidating all your payment dates to one date that is most convenient, you will be able to set up a simpler, more efficient monthly budget.</p>
							
						<p><b>Will a debt management plan stop collection letters and telephone calls?</b><br />Some creditors may continue to contact you until you establish a regular payment history. If you do experience collection activity after several on-time payments to the DMP, call the credit counseling agency’s customer service.</p>
							
						<p><b>How will I know that my payments are going to my creditors?</b><br />You will receive monthly statements listing the accounts on the plan.</p>
							
						<p><b>If I enroll in a debt management plan, can I still use my credit cards?</b><br />Generally, accounts over a certain balance will be closed. The goal of the DMP is to get you out of debt as soon as possible. Incurring more debt will only prolong the process. In some cases, however, clients are allowed to maintain one card.</p>
							
						<p><b>How does a debt management plan affect my credit report?</b><br />This depends on your current credit rating. If you have perfect credit, than participation in a DMP may have a negative effect. However, if your credit is already blemished from late payments or charge-offs, a DMP can have a positive effect. Some creditors report reduced payments as late or they may insert a line stating “payments administered by credit counseling agency.” If, in the course of the DMP, accounts are re-aged, then the effect will be positive. Successfully completing a DMP will help in re-establishing credit.</p>
							
						<p><b>What if I file bankruptcy?</b><br />That depends on whether you file a Chapter 7 or a Chapter 13. In a Chapter 7 bankruptcy, you may be required to surrender all assets not exempt in your state. You may receive a discharge where you debts are wiped out but the bankruptcy could remain on your credit report for up to 10 years. With a Chapter 13 bankruptcy, you get to keep your assets while paying into a re-payment plan that may last for up to 5 years. As with a Chapter 7, it may remain of your report for 10 years. Keep in mind that several types of debt are non-dischargeable through bankruptcy. These include child support, alimony, taxes and certain student loan obligations.</p>
							
						<p><b>What’s the difference between a debt management plan and a Chapter 13 bankruptcy?</b><br />Through a DMP, consumers maintain a positive relationship with their creditors. A Chapter 13 bankruptcy may discharge any unpaid portion of your debts at the end of the payment period. With a DMP, creditors are not forced to write off debts owed to them.</p>
							
						<p><b>Will a DMP protect me from creditor legal action?</b><br />Creditors will retain the right to take you to court even if you are enrolled in debt management. In the event that a creditor notifies you of a legal action, the credit counseling agency can provide you with records of your payment history which can serve as evidence of your good faith effort to repay the debt.</p>
							
						<p><b>Can I withdraw from a debt management plan at any time?</b><br />Participation in a DMP is completely voluntary. However, if you drop out of the program, you will lose the benefits of the agreement. Your monthly payments will no longer be consolidated and your overall monthly amounts will increase. Also, collection activity will resume.</p>
											
					</fieldset>

					<fieldset id="space">
						<legend><b>A CLOSER LOOK A DEBT SETTLEMENT</b></legend>

						<p>Debt settlement offers a way to avoid bankruptcy for people with significant debt issues who have run out of options. What is debt settlement and how exactly does it work? Let’s find out.</p>
						
						<p><b>History</b><br />A debt settlement is an agreement between a creditor and a debtor to satisfy a debt for a reduced payoff amount. Since the debtor is unable to fully meet his/her debt obligations, the creditor agrees to reduce the debt and accept the agreed upon sum as full payment.</p>
						
						<p>Although the practice of settling debts through a third party began soon after the invention of money, debt settlement as a business service is relatively new. Debt settlement developed in the late 1980s and early 1990s as bank deregulation, coupled with an economic recession, left many consumers facing financial hardships.</p>
						
						<p>In the late 1980s, banks set up debt settlement departments to address the high number of charge-offs generated from cardholders defaulting on loans. Bank personnel were authorized to negotiate with consumers and settle outstanding balances for payments that ranged from 25% to 65% of the amount owed. From their standpoint, something was better than nothing.</p>
						
						<p>By the early 1990s, independent agencies were established for the negotiation and settlement of consumer debt. Today, the debt settlement industry continues to grow as individuals and families find themselves in unresolved debt dilemmas.</p>
											
					</fieldset>
					 
					<fieldset id="space">
						<legend><b>THE DEBT SETTLEMENT PROCESS</b></legend>
							
						<p>Debt settlement is process wherein the debtor saves money earmarked for a particular creditor, a settlement amount is negotiated, and a lump-sum payment is made to the creditor. It is repeated for each outstanding account until all accounts are considered “settled-in-full.”</p>
							
						<b>There are essentially 3 steps to this process which involves 3 players — the debtor, the creditor and the negotiator:</b>
						
						<ol>
							<li>Notifying the creditors. The negotiator will contact each creditor and notify them that, due to financial hardship, the debtor cannot make payments on the account. Instead, the debtor is seeking a negotiated settlement to resolve the debt.</li>
							
							<li>Establishing a savings account. A special savings account is established in the debtor’s name into which monthly payments are accumulated. The funds remain in the account until a formal agreement to settle with one creditor is reached.</li>
							
							<li>Paying the creditor. When the funds in the account are sufficient to settle the debt, the creditor is paid.</li>
						
						</ol>	

						<p>This process is repeated for each remaining creditor. Some accounts are settled with one lump-sum payment while others are paid through short-term installment payments depending on the debt amount and the creditor’s policies.</p>
										
					</fieldset>
					 					
					<fieldset id="space">
						<legend><b>THE DEBT SETTLEMENT CLIENT</b></legend>
									
						<p>Debt settlement is an alternative available for consumers who are facing bankruptcy but do not qualify for debt consolidation. Often they must choose between paying off bills and buying essentials such as groceries and heating fuel. Debt settlement offers to lower the amounts owed typically by 25 – 65% and therefore resolve debts faster.</p>
							
						<p>To enroll in a debt settlement program, the consumer must have a substantial amount of unsecured debt. Usually, this amount is at least between $7,500 and $10,000 although some companies will work with smaller debt loads. Secured debts such as mortgage and car payments cannot be negotiated because the creditors can simply repossess the property that was purchased.</p>
							
						<b>Clients are often advised to:</b>
							
						<p><b>Communicate with creditors.</b> If a creditor calls, it may be in order to inform you that an account has been moved by the creditor to a different agent or collector. Clients help the process by monitoring account changes and reporting them to the debt settlement company.</p>
							
						<p><b>Utilize budget counseling</b>. Many companies offer budget counseling or financial coaching to assist clients in the development of a viable spending plan that supports consistent payments to the savings account.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>DEBT SETTLEMENT VS. DEBT MANAGEMENT</b></legend>
							
						<p>Though often confused as synonymous, debt settlement is very different from debt management plans (DMPs) serviced by the consumer credit counseling industry. With a DMP, credit counseling agencies work to lower the interest rate on a credit card account to lower monthly payments and ensure more funds are directed toward paying down the principal instead of interest charges. Since the debtor typically has more than one account, the debt accounts are consolidated into one payment to the credit counseling agency who is responsible for distributing the payments to each creditor based on a scheduled repayment plan. This usually lasts three to five years.</p>
											
						<p>Debt settlement programs involve the debt settlement company negotiating with the creditor to lower the outstanding balance of the debt. Lower interest rates are usually not negotiated, and the companies do not distribute monthly payments to creditors. The debt settlement companies, typically through a third party payment processor, arrange for paying off debts once a reduced balance settlement is agreed upon. Though the client does make monthly deposits into their settlement fund, it is managed by an independent, third-party processor.</p>
												
					</fieldset> 
					
