					 		<li>Consumers experiencing debilitating debt have two primary options: resolving the debt or file bankruptcy.</li>
					 		<li>Consumer options for resolving debt include credit counseling, debt settlement, and debt consolidation loans. Different financial circumstances require different plans of action.</li>
					 		<li>Many consumers can benefit from budget counseling.</li>
					 		<li>Paying off debt requires commitment.</li>
					 	</ol>
					 					
