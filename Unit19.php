						<legend><b>SUMMARY</b></legend>
						
						<ol>
							<li>The three types of representatives authorized to collect debt are credit grantors, collection agencies and attorneys.</li>
							<li><b class="teal-text">The Fair Debt Collection Practices Act</b> helps to protect consumers from the harassment of debt collectors.</li>
							<li>Effective counselors are familiar with consumer rights and provide assurance to clients who are facing agency scare tactics.</li>
							<li>Lenders of unsecured credit have the power to revoke credit privileges, negatively affect a consumer’s credit report and initiate legal action.</li>
							<li><b class="teal-text">The Fair Credit Billing Act</b> helps provide consumer protection with regards to billing errors.</li>
						</ol>
											
