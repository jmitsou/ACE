					<fieldset id="space">
						<legend><b>OBJECTIVES</b></legend>
						
						<li>To determine the different types of collectors.</li>
						<li>To learn about <b class="teal-text">the Fair Debt Collection Practices Act.</b></li>
						<li>To learn what methods collection agencies may not use in collecting debts.</li>

						<p>A consumer’s account becomes delinquent when no payments are received by the end of the billing period. Usually, a letter will be sent by the creditor reminding the consumer that payment is due either immediately or by a specific date after which time late charges or other fees will be added to the outstanding balance. The majority of delinquent accounts are paid at this point by consumers who probably forgot to pay or who misplaced the billing statement. The relationship between consumer and creditor remains congenial and mutually productive.</p>
						
						<p>When no payments are received after the second billing cycle and no positive negotiation has been worked out, the original creditor will mail out formal warnings to pay. These are, for the most part, not threatening but firm in tone. The creditor walks a thin line here because a consumer’s business is to be valued and hopefully developed over time as a source of profit. At the same time, payment must be collected.</p>
						
						<p>Generally, the lender does everything in its power to collect debt without resorting to tactics that may ultimately end a positive consumer/creditor relationship. Phone calls will be made asking for payment. Irate or evasive customers will be noted. Still, the creditor will probably handle the situation in a polite manner.</p>
						
						<p>Debt delinquency becomes more serious after about three unpaid billing cycles. Something is wrong. Lenders know that the longer a debt goes unpaid, the less likely collection will be successful.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>3 TYPES OF COLLECTORS</b></legend>
						
						<ol>
							<li><b>Credit Grantors.</b> Most original creditors have a collections department in which delinquent payments are segmented into buckets. A bucket identifies the billing cycle the account falls under. For example, bucket one indicates 30 days late while bucket two means 60 days late, etc. If the client is late within the first few cycles, he/she may be able to rescue the account from going to a collection agency. Creditors will re-age the account or mark currentafter several consistent payments.</li>
							
							<p>However, once accounts have been charged-off (written off as a loss), the balance due may be outsourced to a collection agency or to an office owned by the creditor under another name. Sometimes a client will receive an alarming letter from an unknown collector. However, this may simply be the result of an overwhelming number of accounts being handled by the in-house collection department. The creditor simply hired out a low cost operation off-site.</p>
							
							<li><b>Collection Agencies.</b> Anytime an account falls between bucket 3 and bucket 6, a creditor may contract an outside agency to assume responsibility for collecting the unpaid debt. Collection agencies are given authorization by the credit grantor to collect money owed on a commission basis. Generally, they keep 50% – 60% of all the funds collected — not a percentage of the actual debt.</li>
							
							<p>Collection agencies also charge creditors a fee each time they contact the debtor. This, in part, accounts for the frequency of calls and letters a debtor may receive. Collection agencies may be hired out by the credit grantor or they may purchase the debt at discount. If the agency owns the debt, they have the right to sue for the amount of that debt. In the former case, the credit grantor reserves the right to implement legal action.</p>
							
							<li><b>Attorneys.</b> When a collection agency uses every possible legal tactic and is still unsuccessful in collecting a debt, the account may be referred to an attorney. This is usually done only when the amount of the debt is substantial since attorney fees can be quite high. Consumers who ignore letters from credit grantors or phone calls from collection agencies generally take notice of a formal letter from a lawyer. Attorneys often have special means for tracking debtors and confiscating missed payments. These practices often entail obtaining information from voter registration records, credit bureaus, utility and post offices, family or acquaintances, and former employers or landlords.</li>
							
							<p>The attorney may take legal action against a debtor by filing a lawsuit. A summons is then served to the debtor. Most states have laws to deal with individuals who try and avoid being served a summons. If the process server is unable to find the debtor after three legitimate attempts, the summons may then be served to a substitute. This may be a relative, neighbor, friend, or co-worker. A duplicate summons is then mailed to the debtor relating that the summons has been served.</p>
						
						</ol>
						
					</fieldset>
					
					<fieldset id="space">
						<legend><b>WHAT CAN THEY DO?</b></legend>
						
						<p>Compared with mortgage companies and other lenders of secured credit, unsecured credit grantors and hired collection agencies cannot do nearly as much harm. Basically, there are three things they can do:</p>
						
						<ol>
							<li><b>Revoke credit privileges.</b> Unless the credit extender has a monopoly on a product or service the debtor desires, this, in and of itself, should not cause alarm.</li>
							
							<li><b>Damage credit rating.</b> Some collectors may threaten damage to the debtor’s credit in order to prompt immediate payment. In reality, if a debtor is behind on payments, it has already been reported to the credit bureau.</li>
							
							<li><b>Sue to collect the debt.</b> Many credit grantors are reluctant to sue for the collection of debt because litigation is expensive and time consuming. Most often, creditors threaten lawsuits in an effort to scare a debtor into paying delinquent bills.</li>
						</ol>
										
					</fieldset>
					
					<fieldset id="space">
						<legend><b>DEALING WITH COLLECTORS</b></legend>
						
						<ol>
							<li><b>Prevent harassment before it begins.</b> It is never advantageous to completely ignore an unpaid bill. The creditor should be contacted and the situation should be explained politely and truthfully. Never agree to a payment negotiation that is unrealistic. Once a creditor knows you are sincere in wanting to pay, the account will most likely not be turned over to a collection agency.</li>
							
							<li><b>Request termination of contact.</b> Once an account is handled by a collection agency, a letter requesting that all contact cease will eliminate harassing phone calls and letters. A reason for non-payment or description of previous harassment may be included. The collector may then send a final notice stating any further action to be taken. Always maintain a copy for personal records. If the collection agency continues contact, the collection agency could be in violation of <b class="teal-text">The Fair Debt Collection Practices Act.</b></li>
							
							<li><b>Dispute all billing errors.</b> According to <b class="teal-text">The Fair Credit Billing Act</b>, consumers have 60 days after the receipt of a bill to dispute it in letter form. They must send a dispute letter to the address of the customer service department usually located on the back of the billing statement. Consumers include their name, account number, and why they believe the bill is in error. Copies of receipts of other purchases made within the period in question may be helpful in clarifying the billing error. As with all correspondence, the consumer should keep a copy for a personal file.</li>
							
							<p>Within 30 days, the creditor must respond with some explanation of the charge. The consumer cannot be reported to the credit bureau as delinquent until the dispute is settled. (The creditor can, however, continue to add finance charges on the amount.) The dispute must be resolved by the creditor within two billing cycles and no later than 90 days. (See 4:3 Using Credit Wisely for sample dispute letter.)</p>
							
							<li><b>Dispute the debt.</b> Within 5 days of the initial contact, a collection agency must send a written statement informing the consumer of the nature of the debt and the right to dispute the debt. Because the collection agency may attempt to hide this statement of rights within their letter, a consumer must thoroughly read all correspondence. If a client decides the debt is not justified, he/she needs to respond immediately by registered or certified letter.</li>
							
							<p>Has the consumer ever been billed for merchandise returned or never received? Has a credit-card issuer ever charged the consumer twice for the same item or failed to credit a payment? While frustrating, these errors can be corrected. It takes a little patience and knowledge of the dispute settlement procedures outlined in <b class="teal-text">the Fair Credit Billing Act.</b></p>
							
							<li><b>Correspond in writing and keep records.</b> Communicate all requests, responses and complaints by letter and keep copies of all correspondence. Use registered or certified mail for documentation.</li>
							
							<li><b>Do no succumb to scare tactics.</b> Never let threats mar judgment where financial decisions are at stake. Knowledge of consumer rights mitigates fear.</li>
						</ol> 
											
					</fieldset>    
										
					<fieldset id="space">
						<legend><b>WHAT DEBT COLLECTORS CAN AND CANNOT DO</b></legend>
						
						<p><b class="teal-text">The Fair Debt Collection Practices Act</b> <b>(FDCPA)</b> was passed in 1977 as a means to protect consumers and debtors from harassment or unfair treatment by debt collectors. In 1986, an amendment was made to include attorneys who collect debts as a regular part of their practice. Debt collectors must abide by FDCPA rules when attempting to collect delinquent debts.</p>
						
						<p>Although this law has afforded debtors some protection, private collectors may still resort to underhanded scare tactics. If the collector is from the original retailer or lending institution, the FDCPA holds no restriction to debt collection practices. In this case, complaints of excessive telephone calls within a short time period or threatening correspondence are often hard to restrict.</p>
						
						<b>Collection Agencies May Not:</b>
						
						<ol>
							<li>Give misleading or false information about the debt to others.</li>
							<li>Call after 9:00 p.m. or before 8:00 a.m. within client’s time zone.</li>
							<li>Interrupt the work routine of a clients employment.</li>
							<li>Make excessive calls to clients at home or work as a method of harassment.</li>
							<li>Send any letter that may appear to be an official government document.</li>
							<li>Threaten a client or family member in a physical manner.</li>
							<li>Imply that physical damage may occur to client’s property.</li>
							<li>Deposit a post-dated check before the date on the check.</li>
							<li>Misrepresent themselves as a government agency or a law practice.</li>
							<li>Continue to harass a client after being notified in writing to cease.</li>
						</ol>
					
						<b>Collection Agencies Must Abide by the Following:</b>
						
						<ol>
							<li>The initial collection call must be made between 8:00 a.m. and 9:00 p.m. at the debtor’s home. The agent cannot harass, threaten physically or annoy the debtor. However, the agent can be forceful and explain in a normal tone the consequences to the debtor if he/she does not meet his/her obligation.</li>
							<li>If the agent contacts the debtor, he must identify his employer and relate the reason for the call. The debtor is entitled to know the identity and reason for the call.</li>
							<li>The agent can attempt to motivate the debtor by offering incentives for partial or full payment of the debt.</li>
							<li>The agent can ask for personal information but the consumer does not have give it.</li>
							<li>The agent has the right, using a normal speaking voice, to let the debtor know the legal options that are available to the agent and that the agent may use any legal action available. The agent also can tell the debtor the consequences if a judgment is won in favor of the collector.</li>
							<li>An agent can file a proof of claim and request a meeting with the debtor. As a debtor, remember if the agent is making a secured claim, the debtor has the right to redeem certain secured merchandise by paying the creditor only the fair market value as determined by the court.</li>
							<li>The agent can ask the debtor to renegotiate a new note. The debtor does not have to comply but an agent has the right to ask.</li>
							<li>If the debtor is deceased, the agent may file a creditor’s claim against the deceased debtor’s estate, if any estate exists.</li>
							<li>The agent, when attempting to contact the debtor at home and is informed that the debtor is not at home, may ask permission to telephone the debtor at work or wherever he may be at the time. If permission is given by the person answering the telephone, the agent may proceed and call the debtor at that number.</li>
						</ol>
						
						<p>Remember, the collection agencies have rights, but they also have responsibilities. Make sure that they abide by their responsibilities and they do not abuse your client’s rights.</p>
						
						<p>Collection agencies will use every method available to them under the law. Agencies may try to confront the client in person. Another tactic employed by agencies is to call the client continuously at home or work. The agency will use this tactic to harass the client and disrupt his work routine. This could cause embarrassment in front of family members or co-workers.</p>
						
						<p>As a counselor, give support and reassurance to the client. Don’t let your client become intimidated. You must also, when establishing a plan, set up ways to prevent the collection agency from harassing the client. Familiarize yourself with the rights and responsibilities of debtors and take the measures necessary in assisting the client concerning confrontational collectors.</p>
						
						<p>Determine the date of the first notice the client received from the collection agency and the last date the client made any payment on his/her debt. Most agencies will try to get the client to respond to its notices or calls within 60 days. This is the time frame when the probability of collecting the amount owed is highest. The longer the time period from the initial contact, the less chance there is of collecting the debt.</p>
										
					</fieldset> 
					
