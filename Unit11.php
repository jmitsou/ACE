						<legend><b>MAKING DECISIONS</b></legend>
						
						<p>The helping process involves many decision points. Effective counselors facilitate client decision making by understanding the dynamics and pitfalls that arise when confronted with choices.</p>
							
						<p>Clients who voluntarily seek out counseling have already gone through one decision-making process by the time the initial interview begins. The other major decision points that clients and counselors encounter together are:</p>
						
						<ol>
							<li>Deciding what goals to work on.</li>
							<li>How to achieve those goals.</li>
						</ol>						
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>3 STEPS TO RATIONAL DECISION MAKING</b></legend>
						
						<ol>
							<li>Information gathering. Gathering information is the basis for deciding what the problem is, what needs work, what could be better.</li>
							<li>Processing the information. Once the information is gathered, the client must analyze it and draw conclusions from it.</li>
							<li>Choice and execution. This is the point of commitment when the client decides to take action. That’s the way it happens theoretically.</li>
						</ol>
						
						<p><b>Client Variables</b><br />However, in real life, it is rarely that straightforward and objective. For example, clients may give or receive inaccurate information during Step 1. People, as you know, hear what they want to hear and this bias creates blind spots.</p>
												
						<p>Likewise, Step 2 is subject to the client’s feelings, beliefs, and attitudes. As we know, the same information can be interpreted differently by different people.</p>
						
						<p>Step 3 is perhaps the most unpredictable step in the process. For example, many clients will skip the analysis (Step 2) and jump right into a choice (Step 3). Think impulse buying! Sometimes clients go through the analysis of the information halfheartedly having already made up their minds.</p>
						
						<p><b>Other client variables that may compromise rational decision making:</b></p>
						
						<p>confusing quick decision making with being competent<br />
						making a choice just to end the ordeal<br />
						putting off choices by rationalizing or shifting responsibility and<br />
						deciding one thing and doing another</p>
						
						<p>Counselors help clients make better decisions by ensuring that information received is accurate and an objective assessment of the current scenario is discussed and agreed upon. From there, it’s a matter of client commitment to act on the plan.</p>

						<p><b>On to Stage Three…</b><br />Once the client and counselor have gathered all information related to the client’s financial situation and identified where it is the client wants to go, it is time to act. Counselors work with clients to develop a bias toward action. They emphasize the importance and value of doing as opposed to doing nothing. Next, we will explore some of the dynamics behind taking action as well as some concrete strategies for helping clients realize their goals.</p>					
											
