						<p>What is the definition of financial success? For some, it may mean paying off the mortgage or saving enough for their children to attend the college of their choice. For others, financial success may mean keeping a steady source of income and staying afloat between paychecks.</p>
						 
						<p>As a counselor, it is important to remember that clients may view financial success from vastly different perspectives. Therefore, it is best to define financial success in broader terms: Financial success is obtaining maximum benefits from existing financial resources.</p>
						
						<p>In order to gain the maximum benefits from existing resources, planning is necessary. Let’s take a look at seven areas of financial planning that occur throughout the life-cycle.</p>
						
					</fieldset>	 
						
					<fieldset id="space">
						<legend><b>7 PHASES OF LIFE-CYCLE PLANNING</b></legend>
						
						<p>People pass through various phases in their lives and their financial goals shift and evolve as time goes on. Life-cycle planning can be broken down into seven areas: career planning, savings planning, debt planning, insurance planning, investment planning, retirement planning and estate planning.</p>
							
						<ol>
							<li><b>Career Planning</b><br />Career planning typically starts when the individual is young and peaks in the college years, but it can continue throughout life. People may choose to change careers in mid-life or to re-enter the workforce after their children reach a certain age, after divorce or after additional education.</li>
							
							<li><b>Savings Planning</b><br />No matter what the income, consumers must decide how much money to use for current expenditures and how much will be saved for the future. The funds set aside for saving is what will be used for investments, and investments are an important way to achieve lifelong financial goals. Savings planning is best accomplished using financial statements like net worth and spending plans.</li>
							
							<li><b>Retirement Planning</b><br />In the past, people relied on pensions and Social Security after retirement. Today, there are few guarantees and individuals need to calculate the amount of income theyll need and choose from a wide array of available options – IRAs, 401(k), or other types of investing.</li>
							
							<li><b>Debt Planning</b><br />Debt is a part of life. Mortgages, car loans and lines of credit make it possible for us to attain life’s necessities when we don’t have tremendous amounts of cash at our disposal. It is important to keep our debt in check and not let it get out of hand. Shopping and qualifying for the best low interest loans and credit cards with consumer-friendly terms is key in debt planning.</li>
							
							<li><b>Insurance Planning</b><br />Insurance planning also plays an important role in the life-cycle. For instance, when people are young, their ability to work and earn income is their most valuable asset, so they must be prepared should they lose that ability. Disability insurance is a way to guard against this. Later in life, things like life insurance and health insurance are paramount to the financial plan. Additionally, as consumers gain more and more assets, they need the proper insurance to protect what they own.</li>
							
							<li><b>Investment Planning</b><br />Saving a percentage of ones income every year is only part of the retirement equation. The money saved will do little good stuffed in a mattress or piggy bank. Consumers must decide how to invest it. The choices can be overwhelming, but the sooner money is invested, the greater the dividend will be.</li>
							
							<li><b>Estate Planning</b><br />Financial planning is seemingly endless. Even after life is over, financial obligations can linger and plague family and loved ones. Preparing a will and a tax strategy is best handled by a licensed attorney.</li>
						
						</ol>
						
						<p><b>REASONS FOR SAVING</b><br />Financial planning is necessary first and foremost in order to spread out resources throughout the life span. We may have our current consumption needs met but what about our future consumption needs? Saving is putting money aside for those future needs. Saving is also important if you wish to leave anything to your heirs. Last but certainly not least, saving lets you increase your investment assets; more money is making even more money.</p>
				
					</fieldset>	
						
					<fieldset id="space">
						<legend><b>FINANCIAL GOALS: THE “BIG 3″</b></legend>
						
						<p>The top three financial goals (generally speaking) are:</p>
							
						<p><b>Paying for an education.</b> With the cost of a college education rising yearly, it is impossible for most people to pursue higher education without some type of financial assistance. Student loans are properly considered investments toward higher earning potential.</p>
							
						<p><b>Buying a home.</b> For many, home ownership is the hallmark of financial success. It requires saving for a down payment and qualifying for a mortgage you can live with. It offers a sense of stability and security that renting cannot. However, owning a home is not for everyone. Renting allows for mobility while careers are being developed. Plus thereis no property tax to pay.</p>
							
						<p><b>Saving for retirement.</b> Retirement income is of great concern today. Few people can rely on a pension or Social Security. More than ever, people need to fund their retirements through savings and investments. Options include 401(k)s, Individual Retirement Accounts (IRAs), and mutual funds. It is important to know and plan for the amount of income you will need after your working years.</p>
							
						<b>PLANNING IN 4 STEPS</b>		
						<ol>
							<li>State goals in specific terms.</li>
							<li>Create a plan on how to achieve each goal.</li>
							<li>Evaluate progress toward the goal.</li>
							<li>Decide whether to keep working toward the goal or to focus on a new one.</li> 
						</ol>
									
					</fieldset>	 
						
					<fieldset id="space">
						<legend><b>KEYS TO ATTAINING GOALS</b></legend>
						
						<p>The desire to resolve financial dilemmas becomes a goal through counseling. A specific plan is decided upon, a measurable outcome is monitored and solutions to obstacles are explored.</p>							
							
						<p>Setting goals is merely a means, not an end. Simply setting goals is not nearly as difficult as attaining those goals. A series of experiments and studies conducted by Gary Locke and Edward Latham have lead researchers to believe that there are fundamental aspects of goal setting that often enable people to achieve their desired outcome. These include setting short and long-term goals, making the goal-setting process participative, and using feedback.</p>	
						
						<p><b>Short-term goals.</b> Researchers find that people are better able to reach long-term goals when they set short-term goals that are along the same path. These goals should be thought of as sections of the path towards the overall goal.</p>	
						
						<p><b>Long-term goals.</b> Short-term goals should be set with the long-term goal in mind. Without a desired long-term goal in mind, the short-term goals have no final destination. The overall long-term goals should be the main concern, while the short-term goals are the means of getting there.</p>	
							
						<p><b>Participative Goal Setting.</b> People are more often able to achieve goals when they have taken part in setting those goals. Commitment to goals is also higher when participative goal setting is applied. The client should take an active role in setting goals. It is more advantageous to facilitate goal setting rather than to suggest goals to the client, thus giving them a more active role.</p>	
							
						<p><b>Feedback.</b> Without feedback, people will not know if they are on their way toward achieving their goals. Consistent feedback has been shown to positively influence goal attainment. It may be beneficial for clients to be aware of sources of feedback and seek those out.</p>	
												
						<p>All of these are important elements of goal setting. While they are not fail safe determinants of goal achievement, research suggests that they are often highly beneficial. Integrating these elements into the client’s plan may increase the likelihood that they will be more committed to, as well as more likely to achieve, their desired goals.</p>
											
					</fieldset>	
						
					<fieldset id="space">
						<legend><b>TURNING WANTS INTO GOALS</b></legend>
						
						<p>Effective counselors work with clients to turn wants and desires into realistic outcome goals. An outcome goal contains three elements:</p>
							
						<ol>
							<li><b>It is specific.</b> Clients usually begin by communicating general or vague desires for change. For example, a client may say, “I need to cut my monthly expenses.” Using a budget analysis sheet, the counselor will help the client decide which expenses can be either reduced or eliminated to achieve a specific reduction.</li>
							<li><b>It is measurable.</b> Without some way to objectively measure the results of an action, it is impossible to monitor its success. If the client wants to cut monthly expenditures, he/she needs to identify by what percent expenses will be reduced. The client will also need to determine an acceptable time frame to achieve the desired outcome. For example, “In three months, I want to establish and maintain a 15% reduction in my monthly expenses.”</li>
							<li><b>It is viable.</b> Goals must be realistic. If objectives are made which are not likely to be met, frustration will ensue. For example, it is unrealistic to try to reduce all expenditures within one month. Instead, it is important to break client goals down into viable tasks that can produce successive accomplishments.</li>
						</ol>
										
					</fieldset>
						
					<fieldset id="space">
						<legend><b>NEEDS VS. WANTS</b></legend>
						
						<p>According to psychologist Abraham Maslow, we all have various levels of needs. His Hierarchy of Needs is a good starting point for identifying some of the motivations behind client consumption. Some needs are universal and quite legitimate like needs for shelter, food and clothing. Other needs such as gold watches, cashmere sweaters and lattes may require a measure of scrutiny.</p>

						<p>Maslow’s Hierarchy of Needs can be visualized as a pyramid made up of five successive tiers each representing human needs ranging from the most basic to the higher-level needs for esteem and self-actualization.</p>
							
							 
						<img src="maslows-hierarchy.gif" alt="maslows-hierarchy" />	
							
							
						<p><b>Physiological Needs.</b> These are the basic survival needs such as food, air, water and warmth. These are lifes essentials! However, how these essentials are procured may vary widely from one individual to the next. For example, drinking water can either be delivered by a service or simply poured from the tap. Food can either come from gourmet dining or frugal grocery shopping.</p>
							
						<p><b>Safety Needs.</b> The need for security becomes active once the basic needs for life maintenance have been met. The need for safety/security can influence such spending decisions as exclusive vs. modest housing or luxury vs. economy vehicles. Many products on the market today are designed to give an illusion of security.</p>
							
						<p><b>Needs for Love, Affection, and Belonging.</b> Once a feeling of security is established, a need for love and a sense of belonging takes precedence. Many people buy items in order to fit into a perceived status quo or in order to attract love or affection from others. A large portion of a clients debt can accumulate from this form of emotional spending. The need to own a certain vehicle or live in a certain type of housing may stem from the underlying need for acceptance within a desired group. Attracting love and affection in others can result in erroneous purchases that include expensive clothing, jewelry or beauty treatments. Advertisers have been exploiting this human need for decades.</p>
							
						<p><b>Needs for Esteem.</b>Once an individual has met the first three levels of needs, the need for esteem becomes dominant. This includes gaining self-esteem and esteem from others. To satisfy this need, a consumer may buy in order to look or feel good about himself/herself or to project a certain image of success or authority. Such purchases may include designer clothing, a prestigious watch or other conspicuous items.</p>
							
						<p><b>Needs for Self-Actualization.</b> At the top of the Needs Hierarchy is the need to realize one’s true potential. Needless to say, this is the hardest need to satisfy because it is often unclear exactly what it means to actualize your true potential. However, if this need is not met, the symptoms are quite identifiable. If an individual has met all the prior needs but lacks satisfaction in this one, the condition manifests itself in a certain restlessness, tension and edginess. The individual may spend in order to compensate for this particular feeling of emptiness. Purchases that arise from this need would then include merchandise or experiences that add diversion to life such as frivolous toys, trips or any purchase made to escape.</p>
							
						<p><b>Self-actualization:</b> To reach your full potential.</p>
						<p><b>Esteem:</b> To be appreciated and heard.</p>
						<p><b>Social group:</b> To belong and be accepted.</p>
						<p><b>Security:</b> To be safe from physical harm.</p>
						<p><b>Physiological:</b> To have food, clothing and shelter.</p>
				
					</fieldset>	 

