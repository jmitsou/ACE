 					   <legend><b>ROLES OF THE CREDIT COUNSELOR</b></legend>
 					   
 					   <p>Effective counselors:</p>
 					   
 					   <p><b>Affect positive change.</b> The premise of all counseling is to help clients resolve problems and manage their resources more efficiently. Credit counselors guide clients through the problem-solving process.</p>
 					   
 					   <p><b>Promote financial literacy.</b> Credit counselors teach their clients about the proper use of credit, budgeting, and the consequences of unpaid debt.</p>
 					   
 					   <p><b>Provide support.</b> Being in debt can present many challenges for clients. Counselors are non judgmental and supportive toward clients expressing fear and frustration over their debt.</p>
 					   
 					   <p><b>Link clients to resources.</b> Counselors anticipate the needs of the clients and provide them with any resources they may need to ensure the successful resolution of their debt issue.</p>
 										
 					</fieldset>

					<fieldset id="space">
					   <legend><b>PROMOTING FINANCIAL LITERACY</b></legend>
					   
					   <p>A primary role of the credit counselor is to help clients develop financial literacy. But what exactly is financial literacy? Let’s examine the components that make up financially literate consumers:</p>
					   
					    <li>They understand basic money management principles such as budgeting resources, saving, investing, and balancing a checking account.</li>
					    <li>They read and understand credit and loan agreements.</li>
					    <li>They use credit wisely by avoiding high interest rates and paying bills on time.</li>
					    <li>They make short and long-term goals with their money.</li>
					    <li>They understand their consumer rights with regards to getting credit, disputing billing statements, and fixing credit reporting errors.</li>
					    <li>They check their credit report at least once a year for accuracy.</li>
					    <li>They check their credit reports prior to making a major purchase.</li>
					    <li>They know their rights concerning debt collectors.</li>
					    <li>They understand that a reduction in income calls for adjusting spending.</li>
					    <li>They communicate with their spouses’ or family members regarding financial goals and substantial purchases.</li>
					    <li>They avoid impulse buying; they can delay gratification and adhere to financial goals.</li>
					   
					   <p>In 2003, FleetBoston Financial sponsored a national survey of consumer knowledge on basic financial issues. Nearly, three-fours of the respondents (73%) said that parents should be the ones primarily responsible for teaching children about basic finances. However, less than half of the parents participating in the survey believed themselves to be competent to teach basic finances. Also, only about one-third of the respondents described themselves as in control of their own finances.</p>
										
					</fieldset>
					
