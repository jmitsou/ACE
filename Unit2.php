						<legend><b>OBJECTIVE:</b></legend>
						
						<li>To identify the proper role of the credit counselor.</li>
    					<li>To determine the components of financial literacy.</li>
						
						<p>If someone asks, “What do credit counselors do?”, you could tell them The “7 Cs” — Credit Counselors Counsel Consumers Concerning Credit Cards.</p>
						
						<p>However, more explanation may be required. Whereas consumer and credit card may be well-known terms, what exactly does it mean to counsel?</p>						
						<p>According to the American Heritage Dictionary, counsel has the following meanings: “To give counsel; advise, to recommend.”</p>
						<p>In its noun form, counsel means “advice or guidance, especially as solicited from a knowledgeable person.” It can also mean “a plan of action.”</p>
						
						<p>So we have an idea now that credit counselors advise or give guidance to consumers about their use of credit and help in devising a plan of action to address problems resulting from credit card use — namely, debt.</p>

					</fieldset>
					
					<fieldset id="space">
						<legend><b>CREDIT COUNSELORS DO NOT PROVIDE THERAPY</b></legend>
						
						<p>However, a clear distinction must be made between credit counselors and other types of counselors who provide assistance that goes beyond giving advice. As we know, there are several varieties of counseling requiring licensure. Mental health counseling, marriage counseling, substance abuse counseling, and anger management counseling are just a few. These types of counseling often involve the diagnosis of a psychological condition or a certain interpersonal dynamic and various forms of therapy to address the condition or dynamic.</p>
						
						<p>The word therapy comes from the Greek therapeia meaning “healing” or “curing.” Credit counselors do not offer their clients therapy. If a client might benefit from therapy or a specialized form of counseling, the credit counselor refers that client to information that will point them in the right direction.</p>
					
					</fieldset>
					
					<fieldset id="space">
					   <legend><b>UNIVERSAL COUNSELING SKILLS</b></legend>
					   
					   <p>The credit counselor’s expertise lies primarily with consumer credit and debt issues. What credit counselors do have in common with other forms of counseling is the use and practice of specific communication skills that serve to facilitate the counseling process. These essential counseling skills include:</p>
					   
					   <p><b>Rapport building skills.</b> Techniques for creating a bond of trust include conveying positive regard to the client, demonstrating empathy, and being genuine and open to self disclosure.</p>
					   
					   <p><b>Listening skills.</b> These skills involve attentiveness and interest in what the client is saying. Responses to client talk are non-judgmental. Specific questions are designed to ensure the accurate transmission of information.</p>
					   
					   <p><b>Interviewing skills.</b> Although the client directs the desired outcomes of the counseling session, the counselor remains in control of the flow and direction of the interview. Techniques include the use of open and closed-ended questions.</p>
					   
					   <p><b>Problem solving skills.</b> Counseling helps to fast-forward problem solving by following specific steps designed to gather accurate data, consider the options, and work toward a chosen plan of action.</p>
					   
					   <p>Doctors, lawyers, teachers, real estate agents, bankers, accountants, and marketers utilize these skills every day. They are universally beneficial even when applied to personal and social situations.</p>
										
					</fieldset>
					
					<fieldset id="space">
					   <legend><b>CREDIT COUNSELORS DO NOT GIVE LEGAL ADVICE</b></legend>
					   
					   <p>The other side of credit counseling – the “credit” side, however, opens up another issue. Consumer credit, lending, debt, and collections by their very nature all have a legal dimension to them. Clients in debt may be facing court judgments involving garnishments or seizures. Some are being harassed by collection agencies. Others are unsure about the new bankruptcy laws. How far can a credit counselor go as they advise their clients?</p>
					   
					   <p>Consider the following from Jeffrey S. Tenebaum and Jonathan L. Pompan from their article, Credit Counseling and Avoiding the Unauthorized Practice of Law: Suggestions for Minimizing Risk:</p>
					   
					   <p>“For those who work in the credit counseling industry, an issue often forgotten in the midst of counseling sessions is that the credit counseling agency (CCA) is not a law firm and that its counselors are not lawyers.” Their September 19, 2006 article appearing in the trade publication Venable, examines ways to ensure that credit counselors do not engage in the potentially liable unauthorized practice of law (UPL). First, they define legal advice:</p>
					   
					   <p>“Legal advice is the application of legal principles and judgment to the circumstances or objectives of another person or entity. In other words, under most state laws, a non-lawyer cannot tell a client anything that, if acted upon, would change the client’s legal rights or legal status.”</p>
					   
					   <p>Concerns arise primarily with regards to bankruptcy counseling. Examples of UPL in the context of bankruptcy include:</p>
					   
					   	<li>Determining when and whether to file bankruptcy</li>
					   	<li>Deciding whether to file a Chapter 7 or a Chapter 13 petition</li>
					   	<li>Filling out or helping a client complete court documents or schedules</li>
					   	<li>Providing clients with definitions of legal terms</li>
					   	<li>Advising clients which exemptions they should claim</li>
					   	<li>Advising clients about whether or not certain debts are dischargeable</li>
					   	<li>Speculating on how a bankruptcy might affect a foreclosure</li>
					   	<li>Speculating on the tax consequences of filing for bankruptcy</li>
					  		
					</fieldset>
					
					<fieldset id="space">
					   <legend><b>STATE LAWS</b></legend>
					   
					   <p>Some states such as Rhode Island and Massachusetts have specifically authorized credit counseling agencies to engage in certain services without being subject to UPL liabilities. These services include the primary job of the credit counselor:</p>
					   
					   <ol>
					   		<li>giving financial and budgetary advice</li>
					   		<li>helping create a spending plan</li>
					   		<li>administering a repayment plan</li>
					   		<li>providing education about the use of credit.</li>
					   </ol>
					   
					   <p>In this way, some states provide protection to CCAs from ambiguous interpretations of what constitutes an unauthorized practice of law. In fact, there is no universally accepted definition of UPL. Furthermore, the various state laws regulating UPL are not preempted by the state laws that regulate credit counseling agencies. In other words, a CCAs compliance with state credit counseling regulations does not guarantee immunity from UPL liability.</p>
					   
					   <p>Recommendations for Minimizing Risk of UPL Liability It is then up to the CCA to implement steps to help avoid UPL. Tenebaum and Pompon recommend the following safeguards that pertain to individual credit counselors:</p>
					   
					   <p>Avoid definitive statements regarding a course of action. A counselor should not say, “You are ineligible for Chapter 7 Bankruptcy” or “Bankruptcy is your best option.” The counselor should instead say, “You might be ineligible for Chapter 7″ or “Bankruptcy might be your best option.” The counselor should then recommend that the client consult an attorney for a definitive answer.</p>
					   
					   <p>Discuss bankruptcy in general terms. Avoid any discussion of how bankruptcy could affect a particular client. If and when the subject of bankruptcy comes up, focus on general, factual information without relating it to the specifics of the client’s situation. Primary attention should go to financial/budgetary analysis.</p>
										
					</fieldset>

