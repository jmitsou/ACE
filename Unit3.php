					   <ol>
						   	<li>The Uniform Debt Management Services Act addresses both the credit counseling and debt settlement industries.</li>
							<li>These industries are closely related in that they both provide services to consumers in debt but differ in the processes by which consumer debt is resolved.</li>
							<li>The UDMSA represents a set of standards on the national level under which these two industries continue to operate.</li>
