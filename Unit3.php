			<div class="row">
				<div class="col m9 l9">
				<fieldset id="space">
						<legend><b>OBJECTIVES:</b></legend>

					    <li>To survey the evolution of credit and debt counseling services.</li>
					    <li>To appraise the main provisions of the UDMSA.</li>
						
						<p>In July 2005, the National Conference of Commissioners on Uniform State Laws finalized and issued the Uniform Debt-Management Services Act (UDMSA). It is the first effort on the federal level to provide a uniform set of rules to govern the two primary services available for consumers in debt — credit counseling and debt settlement. Issues such as debt adjusting, debt management, debt pooling, debt settlement and consumer credit counseling have already been addressed by nearly every state but the scope and content of this state legislation varies considerably.</p>
						
						<p>What follows is a short summary of the rise and development of debt management services in the United States.</p>
						
					</fieldset>
				
				<fieldset id="space">
					   <legend><b>FOUR GENERATIONS OF DEBT MANAGEMENT</b></legend>
					   
					   <p><b>1. Debt Adjusters</b><br />Consumer credit counseling originated in the early 1900s in the form of debt adjusters. Also known as debt poolers, debt consolidators, debt managers and debt pro-raters, they represent the first generation of debt management services and operated as for-profit agencies. These early debt managers communicated with the consumer’s creditors to negotiate a partial payment of the debtor’s obligation. Once a settlement amount was agreed upon, the debt manager would collect monthly payments from the consumer and forward portions of it to each of the creditors.</p>
					   
					   <p>These early for-profit entities came under scrutiny by legislators due to instances involving exorbitant fees, deception and, in some cases, outright theft of the payments collected from consumers. By the 1950s, more than half of the United States outlawed the operation of these companies. Approximately two-thirds of the remaining states imposed requirements and restrictions. Meanwhile, many states exempted non profit agencies from these statutes.</p>
					   
					   <p><b>2. Non profits</b><br />This led to the growth of the second generation of debt management services – non profit entities. The 1950s saw a dramatic growth of these agencies in part due to the formation of the National Foundation of Consumer Credit (later called the National Foundation for Credit Counseling). Created by retailers and banks that issued credit cards, the objectives set forth were:</p>
					   
					   <li>to enable consumers to repay their debts in full</li>
					   <li>to help consumers avoid bankruptcy</li>
					   <li>to provide education and budget counseling</li>
					   <li>to enroll consumers in debt management plans when appropriate.</li>
					   
					   <p>A consumer enrolled in a debt management plan received concessions from the creditors which may have included elimination of late fees or reduction of interest. The agencies received a “fair share” contribution from the creditors amounting to 15% of the payments received. This second generation continues to operate today.</p>
					   
					   <p><b>3. Trade Organizations</b><br />The third generation arose during the late 1980s and 1990s, a time when consumer debt was skyrocketing. It was also a time when consumer income increased and standards for creditworthiness decreased. New agencies were formed that were unaffiliated with NFCC. The Association of Independent Consumer Credit Counseling Agencies (AICCCA) and the American Association of Debt Management Organizations (AADMO) formed as new trade organizations whose affiliates saw dramatic growth in the late 1990s. Their share in the debt management industry grew from 20% in 1996 to 80% by 2001 (NCCUSL). At the same time, services were increasingly conducted offered over the phone and on the Internet as opposed to in person.</p>
					   
					   <p><b>4. Debt Settlement</b><br />Another segment of the debt management industry formed during this time thus creating the fourth generation — debt settlement agencies. In 2004, trade organizations merged into the United States Organization for Bankruptcy Alternatives (USOBA). In a sense, this fourth generation represents a return to the first generation of debt managers whose goal it was to negotiate to settle a consumer’s debt for less than the full amount. However, unlike their predecessors, this new generation of debt negotiators work with consumers first to set up a savings account and once there is a target amount saved, the debt counselor will then negotiate with the consumer’s creditors.</p>
										
					</fieldset>
					
				<fieldset id="space">
					   <legend><b>FEDERAL STANDARDS:</b></legend>
					   
					   <p>The draft committee of the UDMSA characterize the history of debt management as “somewhat checkered.” While there have been benefits to consumers including education, assistance with budget creation and relief from debts involving multiple fees and high interest rates, both the Consumer Federation of America and the National Consumer Law Center have sited deceptive practices and excessive costs to the consumer.</p>

						<p>By January 2003, a drafting committee was authorized to develop the UDMSA. It became an increasingly important initiative since the majority of debt management agencies operate in multiple states and were subject to multiple and sometimes conflicting requirements.</p>
						
						<p>The UDMSA defines “debt management services” as encompassing both credit counseling agencies and debt settlement agencies. With very few exceptions, the provisions of the Act apply equally to both types of services.</p>
						
						<p>The UDMSA is neutral on the question of whether for profit entities should be permitted to provide services to the consumer and leaves that up to individual states.</p>
						
						<p>Bankruptcy reform in 2005 has only increased the need for both credit counseling and debt negotiation. For a consumer to file a Chapter 7 bankruptcy, the consumer must, in most cases, show that credit counseling and debt education has been sought and attempted. Since the bankruptcy reform legislation is federal, this creates yet another reason why a uniform set of guidelines and restrictions are necessary for agencies that, for the most part, operate nationwide.</p>
										
					</fieldset>
					
				<fieldset id="space">
					   <legend><b>SUMMARY OF THE UDMSA*:</b></legend>
					   
					   <p><b>Purpose</b><br />This Act provides guidance and regulation to the consumer credit counseling and debt settlement industries. The Act applies to both consumer credit counseling services and debt settlement services. The Act is a comprehensive statute that provides rules for, among other things, registration requirements, bond requirements, certification requirements, disclosure requirements and penalties for non-compliance.</p>
					   
					   <p>UDMSA may be divided into three basic parts: registration of services, service-debtor agreements and enforcement. Each part contributes to the comprehensive quality of the Uniform Act.</p>
					   
					   <p><b>Registration</b><br />No service may enter into an agreement with any debtor in a state without registering as a consumer debt-management service in that state. Registration requires submission of detailed information concerning the service, including its financial condition, the identity of principals, locations at which service will be offered, form for agreements with debtors and business history in other jurisdictions. To register, a service must have an effective insurance policy against fraud, dishonesty, theft and the like in an amount no less than $250,000.00. It must also provide a security bond of a minimum of $50,000.00 which has the state administrator as a beneficiary. If a registration substantially duplicates one in another state, the service may offer proof of registration in that other state to satisfy the registration requirements in a state. A satisfactory application will result in a certificate to do business from the administrator. A yearly renewal is required.</p>
					   
					   <p><b>Agreements</b><br />In order to enter into agreements with debtors, there is a disclosure requirement respecting fees and services to be offered, and the risks and benefits of entering into such a contract. The service must offer counseling services from a certified counselor or certified debt specialist and a plan must be created in consultation by the counselor for debt-management service to commence. The contents of the agreements and fees that may be charged are set by the statute. There is a penalty-free three-day right of rescission on the part of the debtor. The debtor may cancel the agreement also after 30 days, but may be subject to fees if that occurs. The service may terminate the agreement if required payments are delinquent for at least 60 days.</p>
					   
					   <p>Any payments for creditors received from a debtor must be kept in a trust account that may not be used to hold any other funds of the service. There are strict accounting requirements and periodic reporting requirements respecting funds held.</p>
					   
					   <p><b>Enforcement</b><br />The Act prohibits specific acts on the part of a service including: misappropriation of funds in trust; settlement for more than 50% of a debt with a creditor without a debtor’s consent; gifts or premiums to enter into an agreement; and representation that settlement has occurred without certification from a creditor. Enforcement of the Uniform Act occurs at two levels, the administrator and the individual level. The administrator has investigative powers, power to order an individual to cease and desist; power to assess a civil penalty up to $10,000.00, and the power to bring a civil action. An individual may bring a civil action for compensatory damages, including triple damages if a service obtains payments not authorized in the Uniform Act, and may seek punitive damages and attorney’s fees. A service has a good faith mistake defense against liability. The statute of limitations pertaining to an action by the administrator is four years, and two years for a private right of action.</p>
					   
					   <p>Banks as regulated entities under other law are not subject to the Uniform Act, as are other kinds of activities that are incidental to other functions performed. For example, a title insurer that provides bill-paying service that is incidental to title insurance is not subject to it.</p>
					   
					   <p>The UDMSA provides comprehensive regulation of debt counseling and debt settlement services. It becomes an essential part of the law of creditor and debtor as bankruptcy reform enacted by Congress in 2005 continues to take effect.</p>
					   
					   <p>*From the National Conference of Commissioners on Uniform State Laws</p>
										
					</fieldset>
					
