						<legend><b>CHAPTER 13</b></legend>
						
						<p>Sometimes called the wage earner’s plan, Chapter 13 is a reorganization bankruptcy whereby the debtor repays all or a portion of what is owed over a five year period. While the debtor is under court supervision, the court provides protection from creditor actions such as garnishments and seizures.</p>
						
						<p><b>Eligibility</b><br />To be eligible for a Chapter 13 bankruptcy, you must show the court, through a repayment plan, how you will pay off your debts. You must have sufficient disposable income to maintain a monthly payment plan. Disposable income is that portion of income left over after meeting basic living expenses.</p>
						
						<b>Below is a summary of a Chapter 13 bankruptcy proceeding.</b>
						
						<p>A Chapter 13 proceeding begins when a debtor files a repayment plan with the court. Included are forms listing your assets, your income, your monthly expenses and the nature and amount of your debt.</p>
						
						<p>Payments begin within 30 days of the filing. The debtor makes payments to the trustee who then distributes the money to the various creditors. In this way, Chapter 13 resembles a debt management plan. And like debt management, Chapter 13 requires a high level of commitment due to the extended time frame and the fact that on-going money pressures continue as always.</p>
						
						<p>The debtor can have the case modified at any time. Life goes on. During a five year repayment plan, things happen. You may suddenly find yourself without a job. Whether temporary or permanent, the trustee has the discretion to modify your plan to accommodate your needs. Depending on the situation, the trustee may give you a grace period or reduce the monthly payment.</p>
						
						<p>In the event that the bankruptcy court will not let your plan be modified, you have the right to:</p>
						
						<ol>
							<li>Convert to Chapter 7</li>
							<li>Have the court dismiss your case.</li>
						</ol>
						
						<p>Certain remaining debts at the end of the five year repayment plan are wiped out in a super-discharge. Under the new law, debts incurred through fraud, theft or malicious acts are no longer included in the super-discharge.</p>
										
					</fieldset>
					
