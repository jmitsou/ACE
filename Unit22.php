					<fieldset id="space">
						<legend><b>EVALUATING THE ALTERNATIVES</b></legend>
						
						<p>Let’s explore Step Three of the guidelines by considering the options. Which debt-relief alternative is best for the client both financially and emotionally?</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>CONSUMER WORKOUT</b></legend>
							
						<p>Consumers should contact their creditors whenever they are having trouble making a payment. Generally, creditors will want to maintain a positive relationship with customers by allowing a temporary or permanent modification for repayment.</p>
							
						<p><b>Financial side:</b> Is the setback temporary? Will the consumer have regular income in the future? How long has the debt been delinquent? Is it in collections? In the best case scenario, a consumer should contact his or her creditor in anticipation of paying a bill late. However, if the consumer is late by a month or two, negotiation might work. Success is also contingent on the specific creditors involved and their policies concerning payments.</p>
							
						<p><b>Human side:</b> Negotiating with creditors can be time consuming and frustrating. A client should understand that this option requires assertiveness and perseverance.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>CREDIT COUNSELING</b></legend>
							
						<p>Many people simply do not have the time or skills necessary to negotiate with all the creditors they owe money to. Credit counseling provides a liaison between consumers and lenders.</p>
							
						<p><b>Financial side:</b> A debt management plan is a viable option if the client can show money left over each month after paying for expenses. The income must be sufficient and regular enough to maintain the payment plan worked out.</p>
							
						<p><b>Human side:</b> Maintaining the plan, which could last from three to five years, takes commitment. Individuals in a debt management plan will need to change their spending habits and their use of credit.</p>					
					</fieldset>
					
					<fieldset id="space">
						<legend><b>DEBT CONSOLIDATION</b></legend>

						<p>This option could make sense for individuals who owe several creditors and wish to manage their debt by making one monthly payment.</p>
							
						<p><b>Financial side:</b> Taking out a loan only makes sense if the individual has a steady source of income and a fair to good credit rating. If the debtor is a homeowner, they might consider a home equity loan. Other types of consolidation such as 0% transfers come with severe penalties that could exacerbate the debt situation.</p>
							
						<p><b>Human side:</b> Taking on more debt, especially trading in unsecured debt for secured debt in the case of a home equity loan, can mean added financial stress. Unless the issues that caused the debt in the first place are addressed, this is probably not a good option.</p>					
					</fieldset>
					
					<fieldset id="space">
						<legend><b>CHAPTER 13</b></legend>	
											
						<p>If the individual needs asset protection while paying off debts, this may be the best option. However, did you know that the vast majority of debtors who file for a Chapter 13 never complete their payment plan?</p>
							
						<p><b>Financial side:</b> As stated earlier in this chapter, a debtor must submit to the court a repayment plan that shows adequate money each month to maintain payments.</p>
							
						<p><b>Human side:</b> Like debt management, this option takes commitment. Also, you must also consider that, during the five-year plan, your finances are subject to court supervision.</p>
												
					</fieldset>
					
					<fieldset id="space">
						<legend><b>DEBT SETTLEMENT</b></legend>
						
						<p>If you are deep in debt and wish to avoid bankruptcy, debt settlement could be a way to resolve delinquent debt by paying a reduced, negotiated portion of the debt.</p>
							
						<p><b>Financial side:</b> The debtor must raise funds to pay the negotiated amount. This could mean selling an asset or asking for help from a family member. Also, to be considered is the damaging effect this will have on the credit rating.</p>
							
						<p><b>Human side:</b> This option promises a quicker way of getting out from under debt and it may help some individuals avoid bankruptcy. However, creditor actions to collect what is owed will not stop until the reduced amount is paid.</p>
												
					</fieldset> 
					
					<fieldset id="space">
						<legend><b>CHAPTER 7</b></legend>
							
						<p>This is the last resort option as long as the debtor can pass the means test and does not have a previous Chapter 7 within the last eight years.</p>
							
						<p><b>Financial side:</b> After expense allowances, the debtor must show close to no disposable income for paying off debts. Also ask,”Will enough of my debts be discharged to make it worth doing?”and “Is there property I want to keep that I might have to forfeit?”</p>
							
						<p><b>Human side:</b> Consider how the client feels about bankruptcy. For some, the emotional toll may be too much.</p>
							 
						<p><b>Legal Advice:A Working Definition</b><br />It is more important than ever to define legal adviceand understand what you, as a counselor, can and cannot do. It is illegal for anyone other than an attorney to do the following:</p>
							
						<ul>
							<li>Interpret the law by explaining what a statute, regulation, or case means.</li>
							<li>Prepare legal documents or interpret any legal documents originating from a court or an attorney.</li>
							<li>Offer an opinion as to how a clients specific legal problem should be handled.</li>
						</ul>	
							
						<p>Counselors may not say, “I think you should file bankruptcy” or “If I were you, I would file a Chapter 7.”</p>
							
						<p>However, in developing a plan of action, the counselor can say, “It appears that bankruptcy may be an option”or “It looks like you may need an attorney.”</p>
											
					</fieldset>

					<fieldset id="space">
						<legend><b>PRE-DISCHARGE DEBTOR EDUCATION</b></legend>
											
						<p>Also mandated under the new law is a two-hour course in personal financial education. In order to be granted a discharge by the court, the debtor must complete the course within the 45 days following the creditors meeting. According to the U.S. Trustee’s Office, this course must include the following 4 components:</p>
							
						<ol>
							<li><b>Budget development.</b> This includes setting short and long-term financial goals and understanding the difference between fixed, variable and discretionary expenses.</li>
								
							<li><b>Money management.</b> Debtors would explore needs versus wants, the importance of savings and insurance, and effective shopping strategies.</li>
								
							<li><b>Using credit wisely.</b> This component would examine the types and availability of credit and loans, avoiding credit problems, and understanding credit reports and scores.</li>
								
							<li><b>Consumer resource information.</b> The course would include a survey of resources for the consumer such as assistance programs as well as consumer rights.</li>
						</ol>
							
						<p>As with the pre-bankruptcy counseling, the course must be administered by an approved agency.</p>
											
					</fieldset>
					
					<fieldset id="space">
						<legend><b>AFTER BANKRUPTCY</b></legend>
						
						<p>If the premise of bankruptcy is to provide a fresh start for consumers whose debt situation is truly debilitating, any counseling or education would be incomplete without providing guidance for a post-bankruptcy action plan.</p>
						
						<p>Generally speaking, clients will want to re-establish their credit as soon as possible while at the same time, avoiding the past mistakes that may have contributed to the need to file bankruptcy. The debtor who has just come out of a bankruptcy should understand that there is nothing forever about credit. Although, a bankruptcy can stay on your credit report for up to 10 years, the effect it has on your credit score can be lessened through the responsible use of credit. The best strategies are:</p>
						
						<p><b>Get your credit report and fix any errors.</b> As you know, credit reports can contain mistakes that translate into more money out of your pocket. Make sure that any debts that were filed under the bankruptcy are not still listed as open and overdue. If they are still listed as overdue, the debtor must contact the credit bureaus and insist that the accounts be reported as included in bankruptcy. Also, check to make sure the correct type of bankruptcy is listed. If you filed a Chapter 13, make sure it does not report it as a Chapter 7.</p>
						
						<p><b>Apply for a secured credit card.</b> Considering all the multiple bankruptcies filed in recent years, it is more than just possible to re-establish credit in a relatively short amount of time. You may not qualify for a regular credit card right away but you can get a secured credit card. This type of credit card draws money from an amount you deposit in the bank. By making charges on a secured card and making regular payments on time, you can get back in the credit game. Also, it is best not to charge more than 30% of your credit limit.</p>
						
						<b>Three things to keep in mind:</b>
						
						<ol>
							<li>Beware of secured cards that carry high application or annual fees.</li>
							<li>Ascertain whether or not the card issuer reports to the three credit bureaus. If not, you won’t be getting creditfor your good use of credit.</li>
							<li>After a 12 — 18 month period of sound credit use, you should be able to upgrade the secured card to a regular card.</li>
						</ol>
						
						<p>Apply for an installment loan. Establish that “healthy mix” that creditors like to see by getting an installment loan. If you have a student loan, make sure it is current and try to pay more than the minimum payment each month. Also, do not think you will not qualify for a mortgage. Typically, you could qualify for a Federal Housing Administration Loan in as little as six months after bankruptcy.</p>
						
						<p>After bankruptcy discharge, consumers are often inundated with high-cost credit offers. These offers promise to rebuild your credit but, in reality, can derail your post-bankruptcy fresh start. It is important to choose new credit very carefully. Although, many consumers coming out of bankruptcy will be anxious to obtain credit again, it is best not to succumb to creditor solicitations and, instead, pro-actively seek the best credit options.</p>
						
						<p>Whatever alternative the client chooses for dealing with debt, it is important that he or she create a spending plan that supports it. Only through a spending plan can money be allocated to resolve the debt while avoiding debt in the future.</p>
						
						<p>Above all, it is important to remember that bankruptcy is not the end of the road but a chance for a new beginning.</p>
											
					</fieldset>
					 
					<fieldset id="space">
						<legend><b>SUMMARY</b></legend>
						
						<ol>
							<li>Chapter 7 bankruptcy wipes out any debt filed under it.</li>
							<li>Chapter 13 bankruptcy reorganizes debt into a monthly payment plan and offers certain court protections from creditor actions.</li>
							<li>Considering bankruptcy involves knowing which debts are dischargeable and what property you are allowed to keep.</li>
							<li>The Bankruptcy Abuse Prevention and Consumer Protection Act is the most extensive revision of the bankruptcy code since the Bankruptcy Reform Act of 1978.</li>
							<li>A means testis applied to determine bankruptcy eligibility.</li>
							<li>New counseling and education is required for those filing bankruptcy. Consumers must receive counseling prior to filing bankruptcy and personal financial management education before receiving a discharge.</li>
						</ol>
											
					</fieldset>
					
