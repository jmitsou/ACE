						<p>Bankruptcy filings have increased steadily since the early 1970s to over 1.4 million each year today. Among the reasons for this steady increase are a volatile job market, soaring medical costs, divorce, a lack of savings and high levels of consumer debt. When financial setbacks occur, the balancing act of living paycheck to paycheck falls like a house of cards. Bankruptcy is a legal proceeding that can offer consumers who cannot conceivably pay all that they owe a fresh start from which to improve their financial situation.</p>
											
