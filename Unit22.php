						<legend><b>QUESTIONS AND ANSWERS ABOUT CHAPTER 7</b></legend>
						
						<p>There are several important points to consider before filing a Chapter 7. Below are the most important questions to ask.</p>
							
						<p><b>What debts cannot be discharged in a Chapter 7 bankruptcy case?</b><br />Nondischargeable debts fall in two categories:</p>
						
						<ol>
							<li>Debts that survive bankruptcy. These include child support obligations, tax debts and student loans.</li>
							<li>Debts the court declares non-dischargeable due to creditor objections. This could be debts incurred by fraud or malicious acts.</li>
						</ol>

						<p><b>Are there limits to the automatic stay?</b><br />Yes. In some cases, the automatic stay will not stop actions by the IRS, lawsuits to collect child support or criminal proceedings.</p>
						
						<p><b>What property is exempt in a Chapter 7 proceeding?</b>Exempt property is property that cannot be taken by the court to raise money to pay off creditors. What filers get to keep in a Chapter 7 bankruptcy varies from state to state. The following is a list of typical exemptions:</p>
						
						    <li>Motor vehicles, to certain value</li>
						    <li>A portion of the equity in a home</li>
						    <li>Necessary household furnishings and appliances</li>
						    <li>Tools necessary for employment</li>
						    <li>Pensions</li>
						    <li>Life insurance</li>
						    <li>Public benefits such as welfare, Social Security and unemployment</li>
						
						<p>Non-exempt property includes a second motor vehicle, a second home, cash, bank accounts, investments and family heirlooms.</p>
						
						<p><b>What can happen if the information filed in the petition is less than 100% accurate?</b><br />Inaccurate or incomplete information could lead to dismissal of a case. Also, if the court finds that you are deliberately hiding assets, you can be penalized severely. Remember that when you submit papers to the court, you are swearing that everything is true under penalty of perjury.</p>
						
						<p><b>How does Chapter 7 affect your credit rating?</b><br />A bankruptcy proceeding will appear as an item on a credit report for a period of 10 years. It will damage your credit rating, lower your FICO score, and make it difficult to obtain credit in the future. The worst effects of Chapter 7 can be mitigated by re-establishing credit with a secured credit card and consistently paying on time.</p>

						<p><b>What is the waiting period if I filed previously?</b><br />Under the Bankruptcy Abuse Prevention and Consumer Protection Act, a debtor can file for a Chapter 7 eight years from the date of the first filing.</p>
												
						<p><b>Can I file without an attorney?</b><br />You can but it is not advisable. Specialized knowledge is often needed concerning types of debts, exemptions, and state laws. In the case of a partnership or cooperation, an attorney is required.</p>
						
						<p><b>Can I get free legal assistance?</b><br />Legal assistance is available for some who meet income guidelines. In some cases, a petition preparer is used to help create the filing forms. However, they are prohibited from giving legal advice or representing you in court.</p>
						
						<p><b>Should I file separately or jointly with my spouse?</b><br />Whether you should file a joint petition or a separate one depends on several factors that involve the types of debt incurred, when they were incurred, property you own, and state laws. There are two types of marital property ownership — community property and common law. Currently, nine states apply the community property rules while the other states follow common law rules. What rules your state follows and how they can affect your petition should be sorted out by a qualified bankruptcy attorney prior to filing.</p>
						
						<p><b>Do I have to go through the pre-bankruptcy counseling?</b><br />Under the new law, you must complete a 90-minute counseling session within 180 days of filing. The counseling focuses on the availability of credit counseling and other alternatives to bankruptcy. A certificate of completion must be included in your petition. This requirement can be waived if the petitioner is disabled or on active duty in the military.</p>
						
						<p><b>Do I have to obtain debtor education before my case is discharged?</b><br />As with the pre-filing counseling, this is one of the new requirements in the Bankruptcy Abuse Prevention and Consumer Protection Act. It states that a certificate must be filed with the court within 45 days of the creditorsmeeting or else the court will close the case without issuing a discharge.</p>
						
						<p><b>What does the court require as far as wage statements?</b><br />Filers must include a statement of current monthly income with their petition. The court defines current monthly income to mean the average of the income you have received over the past six months. This is one of the requirements of the new bankruptcy law.</p>
						
						<p><b>Why are there so many people filing bankruptcy?</b><br />Many factors contribute to the current level of bankruptcy filings. Unforeseen events such as loss of income or a medical emergency can devastate an individual or a familys financial situation particularly when there is no money set aside to deal with these events. Other contributing factors include destructive spending habits, high levels of consumer debt and poor use of credit.</p>
						
						<p><b>How can bankruptcy be avoided?</b><br />Do not borrow more than your income can handle. Establish an emergency fund equal to 3 – 6 months of your current income. Use a spending plan to meet financial goals. Research and obtain medical insurance.</p>
											
