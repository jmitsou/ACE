						<legend><b>CHAPTER 7</b></legend>
						
						<p>Sometimes referred to as a liquidation bankruptcy, Chapter 7 bankruptcy wipes out any debt filed under it. It is a last resort option for debtors who cannot pay back their creditors. It can provide a fresh start for consumers who are ready to rebuild their personal finances while avoiding past mistakes.</p>
											
						<p>Filing bankruptcy is not free and it is not easy especially now that the new bankruptcy law has gone into effect. Bankruptcy is a process that takes about four to six months. Filing costs are now over $200. Attorney fees are much higher. Paperwork is extensive and must be accurate.</p>
							
						<p><b>Eligibility</b><br />A debtor whose income exceeds the states median income for a family of the same size must pass a means test. This test is designed to ascertain whether or not the debtor, after paying for monthly living expenses, has enough disposable incometo maintain a Chapter 13 repayment plan. If the debtor does not pass this test, a Chapter 13 may be ordered by the court.</p>	
							
						<b>Below is an outline of the process:</b>	
							
						<ol>
							<li><b>The debtor files a petition with the court.</b> A Chapter 7 proceeding begins when a consumer submits a two-page petition to the court along with a series of forms that asks for:</li>
							
							<ul>
								<li>your current income and its sources</li>
								<li>your current monthly living expenses</li>
								<li>a list of your property</li>
								<li>a list of your debts</li>
								<li>any property you are claiming as exempt by state law</li>
								<li>property you owned and money you spent during the previous two years</li>
								<li>property you sold or gave away during the previous two year</li>
								<li>a certificate stating that you received credit counseling within the 180 days prior to filing</li>
							</ul>
							
							<p>In the case of an emergency such as an impending foreclosure or eviction, the debtor can file just the two-page petition and file the rest of the paperwork within 15 days.</p>
							
							<li><b>The automatic stayis put into effect.</b> Once the petition has been filed, the automatic staystops all creditor actions to collect what is owed. It is a temporary Order of Reliefthat stops garnishments, bank seizures, utility shut-offs and seizures of property.</li>
							
							<li><b>A creditorsmeetingis scheduled.</b> A week or two after the petition has been filed, the debtor receives a notice that a creditors meeting has been scheduled. This meeting is run by a court-appointed person called a bankruptcy trusteewho swears in the debtor and asks questions concerning the case. The trustee reads over the forms and asks if the information is 100% accurate. Assets that can be sold to pay off the creditors are noted. In most cases, no assets are found. Also, it is very rare that the creditors attend this meeting which can take as little time as a minute or two.</li>
							
							<li><b>Debts filed in the petition are discharged.</b> Within 3 to 6 months after the creditorsmeeting, the debtor receives a court notice that states: All debts that qualified for discharge were discharged. The debtor no longer legally owes the creditors for any discharged debts. Also, the debtor is no longer under court supervision.</li>
						
						</ol>
												
