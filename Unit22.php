						<legend><b>THE BAPCPA</b></legend>
							
						<p>The Bankruptcy Abuse Prevention and Consumer Protection Act (BAPCPA) went into effect on October 17, 2005 after a long and contentious debate in Congress. Whereas in the past, bankruptcy filers were seen as well-intentioned victims of circumstance, they are now considered to be abusing the system unless proven otherwise.</p>
											
						<p>Its many provisions limit Chapter 7 eligibility, increase the paperwork and cost of filing, and diminish the benefits of a Chapter 13. The pendulum that swung in favor of debtors with the passage of the Bankruptcy Reform Act of 1978 has just swung in the opposite direction in favor of creditors.</p>
							
						<p>As a counselor, it is important that you assess your attitude toward bankruptcy. Do you think debtors have a right to discharge debts they cannot pay? Do you think that the creditors are to blame for soliciting consumers to take on debt they can ill afford? Are millions of bankruptcy filers abusing the system each year?</p>	
						
						<p>Studies show that the primary causes of bankruptcy are job loss, health problems, and divorce. Others, however, point to credit-card debt and frivolous spending as the real culprits. However you view the subject of bankruptcy, it is essential that you, as a counselor, remain nonjudgmental toward clients who are considering this option.</p>
						
						<b>We will now survey the most important changes ushered in by the new law.</b>
											
					</fieldset> 
					
					<fieldset id="space">
						<legend><b>CHANGES IN THE BANKRUPTCY LAW</b></legend>
						
						<ol>
							<li><b>Limits Chapter 7 Eligibility.</b> Under BAPCPA, abuse is presumed if a debtors income is above the states median income for a family of the same size. The debtor, in this case, must prove there are no funds available to pay back the creditors.</li>
							
							<p>A means test is administered to determine whether or not a debtor can afford to pay at least $100 a month to the creditors. However, the means test does not use the debtor’s actual income and expenses to make its calculations. The debtor’s income is expressed as the average income of the last six months regardless of whether or not the debtor has experienced a job loss. Also, expenses are based on IRS rules that state what reasonable expenses are.</p>
							
							<p>Critics of the new law include consumer advocates and women’s groups who
							see an essential financial safety net removed. If credit-card debt cannot be wiped out, that’s less money that can go toward essential expenses such as child support. On the other hand, proponents of the law see the means test as the primary tool for reducing the epidemic numbers of bankruptcy filings.</p>
							
							<p>Filers who do not pass the means test must file either a Chapter 13 and enter
							a five year repayment plan or slip further into debt.</p>
							
							<li><b>Limits to Chapter 13.</b> While many debtors who are ineligible for a Chapter 7 will presumably enter a Chapter 13 repayment plan, several changes in this type of bankruptcy make it less powerful:</li>
							
							<ul>
								<li>Many debts that were dischargeable in a superdischarge can no longer be discharged. These include debts resulting from theft, fraud, certain back taxes, and fines and damages.</li>
								<li>An auto lien can no longer be stripped whereby the debtor pays only the secured portion of the debt if the vehicle was purchased within 910 days of the filing.</li>
								<li>All repayment plans must last five years. In the past, some plans lasted only three years.</li>
							</ul>
							
							<li><b>Mandatory credit counseling.</b> All debtors must receive credit counseling from an approved budget and credit counseling agencyeither in an individual or group briefing within 180 days prior to filing bankruptcy.</li>
							
							<p>There are exceptions to this requirement in the case of an emergency and the debtor could not receive credit counseling within five days or if the U.S. Trustee has determined that the approved agencies are not adequate to provide the required counseling. Any debt management plan that is developed must be filed with the court.</p>
							
							<li><b>Limits on the automatic stay.</b> The automatic stay no longer stops or postpones evictions, actions against a drivers license or professional license, lawsuits to establish paternity, child custody, or child support or divorce proceedings.</li>
							
							<li><b>Increased time between filings.</b> A Chapter 7 debtor cannot receive a discharge if a prior discharge was received within eight years as opposed to six years of the new filing.</li>
							
							<li><b>Mandatory debtor education.</b> A debtor can be denied a discharge if the debtor does not complete a course in personal financial management that is approved by the U.S. Trustee.</li>
							
							<li><b>Limits to the discharge.</b> Debts owed to a single creditor totaling more the $500 in luxury goods purchased within 90 days of filing are considered nondischargeable. Also, nondischargeable are cash advances totaling $750 within 70 days.</li>
							
							<li><b>Increased paperwork.</b> A case can be dismissed if the debtor fails to submit the following documents:</li>
							
							<ul>
								<li>List of creditors</li>
								<li>Schedules of assets and liabilities, income and expenses</li>
								<li>Certificate of credit counseling</li>
								<li>Evidence of payment from employers received 60 days prior to filing</li>
								<li>Statement of monthly net income and any anticipated increase in income or expenses after filing</li>
								<li>Tax returns or transcripts for the most recent tax year</li>
								<li>Tax returns filed during the case including tax returns for prior years that had not been filed when the case began</li>
								<li>A photo ID</li>
							
							</ul>
							    
							
							<li><b>Required verification by the attorney.</b> Attorney fees are expected to rise substantially due to increased legal responsibility. Attorneys must verify and attest to the accuracy and completeness of a filers petition. Numerous other provisions in the law address homestead exemptions, domestic support obligations, taxes, and student loans. The above list in no way should be consulted as a legal resource. Many provisions are extremely detailed such as the limits imposed on the automatic stay and how the means test is administered and interpreted.</li>
							
							<p>In summary, filing bankruptcy after October 17, 2005 takes longer, costs more and offers fewer protections from creditors. New duties are imposed on debtors and their attorneys. The previous concept of the unfortunate debtor who is entitled to relief from debts has been replaced by the idea that the debtor is abusing the system unless proven otherwise.</p>
						</ol>
												
					</fieldset>
					
