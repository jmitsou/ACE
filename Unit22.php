						<p><b>Demonstrate positive regard by addressing the client by name and focusing on the client’s strengths and past successes.</b></p>
							
						<p><b>Remain non-judgmental.</b> Avoid judgmental reactions to client statements such as ”That’s terrible!” Instead, use objective responses such as “I see.” Also, do not assign blame when examining past mistakes.</p>
											
