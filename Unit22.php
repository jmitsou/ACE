						<legend><b>CLIENT EMOTIONS</b></legend>
								
						<p>Some authors writing on the subject of bankruptcy claim that the stigma of bankruptcy is a thing of the past. Others, including bankruptcy attorneys, report that creditors meetings are often very emotional occasions that can cause tears, embarrassment and shame.</p>
							
						<p>As counselors, it is important to remain sensitive to the emotional side of bankruptcy. For many, filing bankruptcy is a sign of personal failure. Some may be hesitant to give sensitive information. Others will express confusion about the entire bankruptcy process.</p>
							
						<p><b>What You Can Do?</b><br />How can counselors help quell the shame and guilt that often accompanies the decision to file bankruptcy? It is important for clients to know that many people from all walks of unexpectedly fall on hard times. A volatile job market, soaring medical costs and a culture of buy now pay later has left large numbers of Americans financially vulnerable.</p>
							
						<b>If they take nothing else away with them after a pre-bankruptcy counseling session, it should be that they are not alone. In fact, you are in good company. Consider the following list of famous people who have filed bankruptcy:</b>
							
