						<legend><b>COMPARING CHAPTER 7 AND CHAPTER 13</b></legend>

						<p>While most people who file for bankruptcy file for Chapter 7, there are reasons to file for Chapter 13. Here are the primary considerations:</p>
							
						<ol>
							<li>You filed a Chapter 7 already. If you filed for a Chapter 7 bankruptcy within the last eight years, you are still eligible for a Chapter 13 .</li>
							<li>You want to protect nonexempt property. Another reason to file for Chapter 13 is that you may have valuable nonexempt property you want to keep. Under a Chapter 7, your valuable assets are liquidated to pay the creditors a portion of what you owe. Chapter 13 offers protections from creditor actions against your property.</li>	
							<li>You have delinquent secured debt. If you are behind on your mortgage payments or car loan, a Chapter 13 allows you to keep your property while you catch up through a reorganization plan. Under a Chapter 7, you may have to give up your property.</li>	
							<li>You want to protect your cosigner. In a Chapter 7, the creditors may go after a cosigner of a loan appearing in the petition. In other words, even though the debt is discharged, a cosigner is still responsible for the debt. Under a Chapter 13, you are solely responsible for the debts through your repayment plan.</li>	
							<li>You want to do “the right thing.” Some people will not consider dissolving their debts. They feel it is their moral obligation to pay off their debts as best as they can. A Chapter 13 can make this possible.</li>
						</ol>
							
						<b>Which type of bankruptcy is right for which circumstance depends on:</b>
							
						<p><b>Eligibility.</b> Filing for Chapter 7 under the new bankruptcy code requires that you take a means test. Your income and expenses are put through a series of calculations in a determination to find funds for a monthly repayment plan. If you show consistent money left over after living expenses, you may be ordered into a Chapter 13.</p>
							
						<p>To be eligible for a Chapter 13, you must have adequate income to cover your basic living expenses and be able to maintain a court supervised payment plan lasting 5 years.</p>
							
