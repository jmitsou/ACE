						<legend><b>THE BANKRUPTCY CLIENT</b></legend>
							
						<p>If you are an approved agency to provide the required pre-filing credit counseling or predischarge education, you will be interfacing with a different type of client than one who is seeking to resolve debts through debt management or budget counseling. The bankruptcy clientwill most likely be experiencing a more severe level of financial distress than the typical client who seeks credit counseling.</p>
							
						<p>One important distinction is that the counseling is mandatory for the bankruptcy client. This client may view the required counseling as a pointless exercise and express their frustration, anxiety or even anger toward you.</p>
							
						<p>As a result, the bankruptcy client may lack motivation to consider options other than filing. By the time the client contacts you, a decision has already been made to file bankruptcy. This may have been an extremely hard decision to make after a long period of time dealing with the effects of delinquent debt. Unlike a client who pro-actively contacts an agency, the bankruptcy client is not seeking to pay down debt. Instead, the client is seeking relief from debt.</p>
							
						<p>Who is the bankruptcy client? Contrary to what you might think, the bankruptcy client is middle class, more likely to have attended college and more likely to own a home than the average American. This is not hard to believe once you consider that you need income to incur debts and that one of the reasons for filing bankruptcy is to protect assets.</p>
							
						<p>A Harvard study conducted by Elizabeth Warren found that one half of all bankruptcies are triggered by sudden uninsured medical expenses. Other leading causes of bankruptcy are job loss and divorce.</p>					
					</fieldset> 
					
