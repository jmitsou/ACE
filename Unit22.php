						<legend><b>PRE-BANKRUPTCY COUNSELING</b></legend>
						
						<p>One of the biggest changes in the bankruptcy law is the requirement for consumers who want to file for bankruptcy to complete a credit counseling briefing. It is the first time in history that credit counseling is mandatory.</p>
						
						<p><b>GUIDELINES</b><br />The U.S. Trustee of the Department of Justice, which oversees various aspects of the new law, has drafted guidelines for the required counseling:</p>
										
						<ul>
							<li>The briefing must be at least 90 minutes long.</li>
							<li>It can be completed in either an individual or group setting.</li>
							<li>It should cost about $50. (Couples, in some cases, are charged $75.)</li>
							<li>The requirement can be waived for certain filers including those with disabilities and those on active duty in the military.</li>
						</ul>	
							
						<b>The counseling must consist of:</b>	
							
						<ol>
							<li>An analysis of the current financial situation.</li>
							<li>A discussion of the factors that caused the current situation.</li>
							<li>A discussion of all the options for resolving the debt.</li>
							<li>Guidance in developing a plan of action.</li>
						</ol>	
							
