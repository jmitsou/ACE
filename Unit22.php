						<legend><b>GUIDELINES IN DETAIL</b></legend>
											
						<p>The 4 components that are required under U.S. Trustee guidelines correlate with the problem-solving process outlined in Module III. Let’s look at each one in comparison with the stages of counseling we discussed earlier.</p>
							
						<ol>
							<li><b>Analysis of the clients current financial condition.</b> Gathering all the facts while establishing and building rapport is the first step in the problem-solving process. Its finding out where the client is now. During this first stage, it is essential to view the credit report. If the client does not have a current copy, you can get a free copy by going to www.annualcreditreport.com. The report will help you identify the kinds and amounts of debts and to ascertain whether or not any court actions are listed. Also, it is important at this stage to check the report for accuracy. Any errors found can be contested under the Fair Credit Reporting Act.</li>
								
							<li><b>Discussion of the factors that caused the current condition.</b> This is the other side of gathering all the facts. While avoiding the blame game, you examine where the client is now in terms of on-going issues that may involve destructive spending habits, poor credit behavior, or conflicts over money. Other factors contributing to insolvency may include job loss, lack of medical insurance and divorce. Each factor should be discussed so that the underlying causes of debt can be addressed as well as the debt itself. Solutions are added to the action plan.</li>
								
							<li><b>Discussion of the alternatives to address the current condition.</b> This component correlates with the second step in the problem-solving process that tries to determine where the client wants to go. What is the preferred scenario? In this case, options are considered for resolving debt that may or may not necessitate filing for bankruptcy. Determining the right alternative depends on the clients level of debt and overall financial situation. The deeper a client is in debt, the fewer the options will be. Also, each option carries with it a human factor. Can this individual successfully follow through with the action plan? Are values compromised? Will the action plan cause stress in the household? Do destructive spending habits need to be addressed?</li>
								
							<li><b>Development of a plan of action.</b> The fourth step is often the trickiest. It correlates with the final step in the problem-solving process. How will the client reach the identified preferred scenario?</li>
						</ol>
							
