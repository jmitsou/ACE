						<p>A clear sense of the client’s current financial situation will set the stage for all future financial planning and goal setting. Assessing the client’s financial condition involves not only dollars and cents calculations but also the client’s attitudes and beliefs about money.</p>

						<p>Where is the client now? To find out, counselors use objective measures such as the net worth statement, debt-to-income ratio, and budget analysis. Subjective assessments are no less important in determining a client’s overall financial situation. These assessments explore cognitive and emotional issues linked to money — how we make it, how we spend it, and how we feel about it.</p>

						<p>As a counselor, you will undoubtedly encounter a variety of individuals or couples who will present a wide range of presenting concerns. These are the problems or issues that clients initially bring to the counseling process. In some cases, clients will be experiencing various levels of financial stress or dissatisfaction. In other cases, clients will be in a crisis situation stemming from excessive debt or sudden lack of income.</p>
						
						<p>Your role as counselor is to listen to their concerns and issues in a non-judgmental way. Financial setbacks affect all but the lucky few in today’s economic landscape of corporate downsizing, soaring medical costs, and easy access to credit. Finding solutions never begins by assigning blame but rather by taking a non-judgmental look at the client’s current financial scenario and helping that client move forward.</p>
						
