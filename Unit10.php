						<p>To budget or not to budget — that is the question. Budget by any other name is still a budget but you may wish to call it a spending plan. Budget is a word with a bad reputation. A derivative of the French word bouge meaning “small purse”, it conjures up images of penny-pinching deprivation. Whatever you call it –a budget or a spending plan — it is a valuable tool for reaching financial goals. It helps you to manage the cash that flows in and out of your life.</p>
							
						<p><b>Customize Your Expense Categories</b><br />Some categories like MORTGAGE, UTILITIES, and CAR PAYMENTS will be easy to fill in because you will have a paper trail. Other areas like FOOD, CLOTHING, and PERSONAL will take some thought. It can be helpful to divide these categories into smaller units. For example, FOOD can be broken down to GROCERIES and EATING OUT. PERSONAL can be divided into PERSONAL CARE and ENTERTAINMENT.</p>
											
						<p><b>Keep a Spending Record</b><br />It may be necessary to keep a daily expense record for the month so purchases can be accurately tracked. Guidelines for keeping an expense record are:</p>
						
						
						<li>Do not skip any days.</li>
						<li>Record every expense no matter how insignificant it might seem.</li>
						<li>Record the expense as soon as you make the transaction.</li>
						<li>Write down costs to the penny.</li>
											
