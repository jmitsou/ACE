						<legend><b>DEBT-TO-INCOME RATIO</b></legend>
						
						<p>Another objective measure of a clients current financial condition is the debt-to-income ratio. It will tell you how much debt the client carries in relation to his or her income. Some calculations include mortgage or rent payments and some do not. Some experts recommend including all your current debt to get a more complete picture of the current debt load. It is also recommended to double the amounts of your minimum payments on revolving accounts since paying only the minimum each month is financially unhealthy.</p>
											
						<p>To calculate debt-to-income ratio, add up all monthly payments and divide by the monthly net income. If you include mortgage or rent, a healthy debt-to-income ratio is 36% or less. If you do not include mortgage or rent, the ratio should be kept under 20%.</p>
							
						<p>Different guidelines are used for different purposes. For example, the Federal Housing Authority (FHA) calculates two ratios — a front ratio (mortgage divided by the gross monthly income) and a back ratio (total fixed payments divided by the gross monthly income).</p>
							
						<p>Debt-to-income ratio calculators are freely accessed online!</p>
												
