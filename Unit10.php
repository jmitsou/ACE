						<legend><b>MONEY TALK</b></legend>
							
						<p>Destructive financial behaviors are driven by our subconscious expectations, attitudes, and beliefs about money. People do not deliberately set out to overspend or get into debt. They are motivated by emotions and assumptions just under the surface that fuel everyday interactions and transactions.</p>
						
						<p>These feelings and ideas about money vary considerably from person to person. Let’s listen in on some of the subconscious talk that plays in peoples minds as they go about their daily activities:</p>
						
						<p><b>I Deserve It!</b> Some people have strong feelings of entitlement. They believe they deserve nothing but the best. Whether or not they can afford the best is a little detail they often overlook.</p>
						
						<p><b>I Don’t Expect Much.</b> Others are just the opposite. Feelings of worthlessness keep these individuals from expecting any more than the basic necessities of life. Small expectations often lead to low paying jobs and shabby living conditions. When these individuals get money, they spend it recklessly because they dont feel they really deserve it. ”Money in my pocket?”,they ask,”It must be a mistake!”</p>
						
						<p><b>’m Waiting For A Miracle.</b>I You might just win the lottery. Or maybe your rich uncle will die and leave you a fortune. Maybe youll make a killing in the stock market. Then, you will pay off all your debts in one mighty swoop. You just have to wait.</p>
						
						<p><b>I’m Showing Them I Really Care.</b> Some individuals see money as a way to show and express love and affection. How will they know how much they mean to me unless I put a price tag on it? But, when that special someone says, “You shouldn’t have!”, people in this category probably need to take that sentiment to heart.</p>
						
						<p><b>I’m Not Like Everybody Else.</b> These individuals rationalize their dysfunctional financial behavior by seeing themselves as a special case. Since they are so different and unique, they simply cannot be expected to follow the same rules as everyone else.</p>
						
						<p><b>It Really Doesn’t Matter.</b> When one considers the immensity of the universe, worrying about debt is pretty insignificant.</p>
						
						<p><b>I Need A Fix Now!</b> When the going gets tough, these people shop! A new wardrobe is such a wonderful mood enhancer!</p>
						
						<p><b>I’m Not Good With Money.</b> These individuals feel they are hopeless when it comes to personal finances because they don’t understand international economics or Calculus. Actually, balancing a checkbook rarely involves anything more daunting than simple addition and subtraction.</p>
						
						<p><b>I Will Impress You.</b> These individuals dress, eat out, travel, and spend, spend, spend to impress others. They are strongly motivated to display their level of success for all to see.</p>
						
						<p><b>I Believe Money Corrupts People.</b> This group of people shuns money as the root of all evil.Actually, its the love of money that is said to be the root of all evil. But it never hurts to be thorough, right?</p>
						
						<p>Stop and listen to the chatter in your mind. What money talk do you hear? What message or messages about money are continually playing in your head? You can use the list above or come up with you own.</p>
						
