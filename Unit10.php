						<legend><b>OBJECTIVE MEASURES OF THE FINANCIAL CONDITION</b></legend>
						
						<p><b>Calculating Net Worth</b><br />How much are you worth? That’s the question to be answered when figuring net worth. Net worth is the difference between the total amount of your assets and the total amount of your liabilities. It is the single best estimate of your level of wealth at a particular time. Periodic assessments of net worth are important because:</p>
											
						<p>The net worth statement provides a starting point from which to formulate financial goals. It is the YOU ARE HERE mark on the map from which to plot your financial course. Net worth is a way to measure financial progress. By calculating your net worth every six months or so, you can objectively gauge the success of your financial plan.</p>
							
						<p>Calculating net worth begins by listing all assets. Assets are things you own that have market value. They can be physical possessions such as a house, car, boat, jewelry or furniture. They can also be in the form of paper — cash, bank account balances, stocks and bonds, and investments.</p>
							
						<p>Figuring the total value of your assets requires 2 steps:</p>
							
						<ol>
							<li>Identify and list all items of value that you own.</li>
							<li>Determine the market value for each item.</li>
						</ol>
							
						<p><b>What Do You Own?</b><br />Step two is the more difficult since it is not always easy to appraise the value of an item particularly if it has sentimental value or is a family heirloom. Begin with liquid assets. A liquid asset is cash on hand, money in the bank or any other asset that can be converted into cash with a minimum amount of inconvenience and with no loss in market value.</p>
							
						<p>The market value of homes should be figured by observing the selling prices of homes that are similar in terms of size, type and location. It’s a good idea to then deduct seven percent for a realtor’s commission. Values for automobiles can be determined by referring to the blue bookof used car values. In other cases a professional appraiser may be needed to determine the worth of antique furniture or items resulting from hobbies or collections.</p>
							
						<p>It is important to take a conservative approach in assigning value to assets. Fair market values for many used items may be twenty to forty percent less than what they are listed for as new.</p>
							
							
						<p><b>What Do You Owe?</b><br />Liabilities will likely be easier to determine since lenders and billing departments make it a point to let you know exactly how much you owe. A liability is a debt obligation that must be paid some time in the future. Some balance sheets arrange liabilities in current (to be paid within one year) and non-current (debt obligations beyond one year) categories.</p>
							
						<p>Housing will represent the greatest liability for most homeowners. It is the unpaid portion of a mortgage. Automobile installment loans are also a big ticketliability that can be determined by referring to a bank’s payment schedule. Also, to be listed in the liabilities column are unpaid utility bills, credit-card balances and unsecured debts.</p>
							
						<p>Different types of documents can be used to figure net worth. A balance sheet uses the mathematical equation:</p>

						<p>Assets = Liabilities + Net Worth</p>

						<p>The balance is between assets on the one hand and the sum of liabilities and net worth on the other.</p>

						<p>Another document or financial statement in use today is the equity sheet which uses the more straightforward equation:</p>

						<p>Assets — Liabilities = Net Worth</p>
										
