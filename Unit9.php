							<li>The Problem-Management/Opportunity-Development Model compliments financial counseling specifically by focusing on the two inter-related actions of managing problems (decreasing expenses) and developing opportunities (increasing income).</li>
							<li>People are naturally motivated to resolve financial dilemmas.</li>
							<li>The problem-solving process counselors utilize consist of three stages: identifying where the client is now, determining where the client wants to go and facilitating actions to meet client-directed goals.</li>
