					<fieldset id="space">
						<legend><b>The Problem-Management/Opportunity-Development Model</b></legend>
						
						<p>The Problem-Management/Opportunity-Development Model describes the underlying process utilized by helpers to address client needs. It is based on the idea that the helping process involves the inter-related goals of managing problems more effectively and developing opportunities more fully. Financial counselors can benefit from this approach for three main reasons:</p>
											
						<ol>
							
							<li><b>It is client-centered.</b> The client is situated squarely at the center of the helping process. Clients communicate their particular financial scenarios, provide critical information, offer resources, establish objectives, and ultimately take the actions necessary to improve their financial situation. Ultimately, it is the client who pays down his/her debt and nobody else. Counselors act to facilitate and support client-directed outcomes.</li>
							
							<li><b>It is an open-systems model.</b> The Problem-Management/Opportunity-Development Model is inclusive of any approach that adds success in the helping process. Simply put, the philosophy behind this approach is one size does NOT fit all. Different approaches are necessary for different clients. Each client brings a new situation to the table  new obstacles and dilemmas as well as new strengths and resources. There is no attachment to a particular theory or model. The rules are simple: If it works, do it. If it doesn’t work, stop doing it and try something else.</li>
							
							<li><b>It specifically compliments financial problem solving.</b> People experiencing unresolved debt usually have this core problem: a negative debt-to-income ratio. Reversing this insolvency depends on two complimentary actions: (1) decreasing expenses (managing the problem) and (2) increasing income (developing opportunities). These two processes work hand in hand to pay down debt.</li>
							
							<p>Because this model views the client as the ultimate source of the solution, we will now explore how individuals naturally face and address dilemmas. We will see that people are intrinsically motivated through progressive steps toward the resolution of conflict. It is this dynamic that client-centered helpers acknowledge and work with to generate solutions.</p>
						
						</ol>				
					</fieldset>

 					<fieldset id="space">
 						<legend><b>PROBLEM SOLVING: A NATURAL PROCESS</b></legend>
 						<p>People are naturally motivated to resolve the problems in their lives. According to Aristotle, we are all hard wired to seek pleasure and avoid pain. Some more recent observers of human nature theorize that people naturally seek to resolve conflict.</p>
 						
 						<p>In The Social Brain, M.S. Gazzaniga contends that the human mind is designed specifically to seek harmony and consistency between what we believe about the world around us and what we actually experience. People naturally feel discomfort and anxiety when they have accumulated debt that they cannot seem to pay back. This situation causes tension because the individual’s belief that he or she is honest and responsible is challenged by the reality that products and services have been rendered without payment in full. This tension creates a natural motivation to set things straight.</p>
 											
 						<p>When clients seek out counseling, they are somewhere along a natural progression of steps from an awareness of the problem to finding a solution to the problem. However, the problem still exists. Somewhere in the process, the client got stuck.</p>					
 					</fieldset>

					<fieldset id="space">
						<legend><b>THE 7 STEPS TO PROBLEM SOLVING</b></legend>
						<ol>
						
							<li>Awareness of the problem. The first step involves an awareness of the problem — namely, the client is in a financially unhealthy situation.</li>
							<li>Problem creates urgency. Next, the client begins to feel a sense of urgency about the problem. For example, debt creates annoyance or interference in the client’s personal and business life.</li>
							<li>The search begins. In the third step, the client begins to search for a solution. This may be the point a which the client initiates counseling. However, at this stage, the client is merely exploring the options.</li>
							<li>Decisions, decisions. By the fourth step, the client weighs the consequences of the different options explored. For example, the client may weigh the option of bankruptcy against what that would do to his or her long term credit rating.</li>
							<li>What if I did nothing? Now the client seriously considers his/her options against the cost of simply ignoring the problem. This is often hard to do in the case of credit debt since the amount owed continues to grow.</li>
							<li>I’ve made a decision! An intellectual decision is made to follow a particular course of action. ”I’m going to sell some of my assets”,or “I’m going to get a home-equity loan.”However, a decision made only by the head is insufficient to cause action. In reality, the client does not sell his powerboat. The client does not even apply for a home-equity loan.</li>
							<li>C is for commitment. Then a decision involving the heart is made. With the client’s head and heart involved, the commitment needed to solve the problem is in place.</li>
						
						</ol>				
					</fieldset>

