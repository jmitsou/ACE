						<legend><b>PROBLEM SOLVING THROUGH COUNSELING</b></legend>
						
						<p>Effective counselors follow steps that serve to compliment, as opposed to replace, the natural process we outlined. This process assists clients in making the decisions that lead to solutions in a timely manner. Whereas the natural process of problem solving could take literally several years for an individual to work through, the process counselors utilize can be completed conceivably during the course of one interview. Counseling streamlines the problem-solving process by helping the client consider all the options. As you will see, this process involves three stages:</p>

						<ol>
							<li>Identifying the current situation – where is the client now?</li>
							<li>Setting goals – where does the client want to go?</li>
							<li>Working to get there – how will the client reach stated goals?</li>
						</ol>
						
						<p><b>Stage I: Where the client is now. </b>It is important that counselors begin by gathering facts, incomes and expenses, and current levels of debt. Remember, personal finances are just that — personal! The information that counselors seek is rarely disclosed even between friends. It is critical to establish client/counselor trust. Ask closed-ended questions to get factual information and confirmations. Use non-reflective listening techniques to encourage client communication.</p>
						
						<p><b>Stage II: Where the client wants to go. </b>Solving problems is more than just fixing something that is not working. It involves setting viable objectives and putting into place whatever is necessary to achieve those objectives. In other words, instead of being reactive — reacting to problems or sources of conflict — the client/counselor team is proactive.</p>
						
						<p><b>Stage III: Getting There.</b> Once the current scenario is understood and goals have been specified, it is time to act. Whereas Stage II identifies what the client wants, Stage III identifies how the client will get there. It consists of brainstorming for options, evaluating each option, and acting on the best alternative. It means coming up with a plan that works.</p>
						
						<p>The three stages of problem solving as utilized by counselors will provide the framework for the units that follow.</p>					
