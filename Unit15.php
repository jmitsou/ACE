						<legend><b>OBJECTIVES</b></legend>
						
						<li>To understand how to use credit advantageously.</li>
						<li>To understand the importance of monitoring billing statements and communicating with creditors.</li>
						<li>To determine how to resolve billing errors.</li>

					</fieldset>
					
					<fieldset id="space">
						<legend><b>THE REAL POWER OF CREDIT</b></legend>
						
						<p>Credit provides convenience, flexibility, and buying power to consumers who know how to use their cards advantageously.</p>

						<p>Contrary to what it may feel like when it’s sizzling in one’s hand, a credit card is not an extension of income. When clients develop a solid spending plan, they will know what they can comfortably afford and what they cannot.</p>
							
						<p>A good rule of thumb is the same that many lenders use to judge creditworthiness: Do not let monthly housing costs exceed 36% of your gross monthly income. For example, if the monthly income is $2000 and the house payment is $600, a person is already at 30%. Therefore, encourage clients not to let the power of credit allow them to make purchases they could not otherwise afford.</p>
							
						<p>Credit cards cannot take the place of savings. Whether a consumer wants a new home entertainment system or a trip to Aruba, it makes a lot more sense to set a short-term financial goal and save the necessary funds beforehand. Otherwise, that home theatre or trip will end up costing a lot more, and the consumer will end up paying for them long after the value of the product has decreased or the experience has ended.</p>
							
						<p>For many consumers, maxing outtheir credit cards on spending sprees, entertaining, and traveling is what’s fun and exciting about credit cards. Inevitably, the excitement is short-lived. Before long, the billing statements appear in the mail with balances that, at first, bewilder and then, frustrate the uninitiated.</p>
							
						<p>The real power of credit comes with the understanding that credit is an opportunity to, over time, increase the ability to receive new and better credit. Getting the best mortgage rates, car loans, and insurance rates all depends on how someone has used credit in the past. When credit is used wisely, a person is paving the road to higher levels of financial freedom while enjoying the credit now for the convenience and flexibility it affords.</p>
																
					</fieldset>
					
					<fieldset id="space">
						<legend><b>WISE CREDIT WAYS</b></legend>
						
						<p>Here are some important guidelines consumers need to follow if they are going to reap the full benefits of credit:</p>

						<p><b>Obtaining credit.</b> Shop for the best deals. There are some excellent sources online to begin searching for the best card for you. (See Appendix C.) Watch out for variable interest rates or introductory rates that can turn monstrous overnight.</p>
							
						<p><b>Making purchases.</b> It is usually not a good idea to use credit to pay for everyday expenses like groceries. Also, try not to use credit to pay for unexpected expenses such as car or home repairs. Your spending plan should provide funds for these types of emergencies.</p>
							
						<p><b>Cash Advances.</b> Most cards allow for cash advances. Don’t do it!</p>
							
						<p><b>Monitor your account.</b> Many people fail miserably at the credit game simply because they are not paying attention to what is going on with their accounts. Read over the billing statement carefully the same day it arrives in the mail. If there are unfamiliar charges or fees, call the customer service number. Watch for any unexpected changes in rates, terms, or conditions.</p>
							
						<p><b>Paying bills.</b> It is best to pay the balance in full each month to avoid any finance charges. However, if this is not realistic, try to pay more than the minimum payment. A good idea is to decide on a fixed amount to pay each month until the balance is either paid off or, at least, manageable.</p>
							
						<p><b>Check the credit report once a year.</b> Get a copy of your report from each of the three credit reporting agencies — TransUnion, Equifax, and Experian. Dispute any errors. (See Unit 16.)</p>
												
					</fieldset>
					
					<fieldset id="space">
						<legend><b>COMMUNICATING WITH CREDITORS</b></legend>
						
						<p>A cardholder’s communication with the lender primarily consists of the following:</p>	
							
						<p>The creditor sends a monthly billing statement and the cardholder pays all or a portion of the balance.</p>
							
						<p>However, there are times when further communication is necessary. For example, a person may need to call the credit-card issuer because he/she notices an error on a billing statement. A cardholder may also need to call the lender to get explanations for any changes that occur in the terms and conditions of the loan.</p>
											
						<p>Communicating with creditors is not always easy, but itis important to make the effort. If there is a problem, creditors want to be informed about it. By keeping the lines of communication open, a cardholder is seen as being actively involved with the account. If communication is avoided, lenders will think the cardholder ignores problems when they arise.</p>
											
					</fieldset>	
						
					<fieldset id="space">
						<legend><b>RESOLVING BILLING ERRORS</b></legend>
							
						<p>It is the cardholder’s responsibility to monitor any changes in the lending agreement and to check for billing errors. Although itis not always easy to communicate with lenders, disputes are usually resolved without a problem.</p>
							
						<p>Cardholders have the right to dispute billing errors under <b class="teal-text">The Fair Credit Billing Act.</b> They can then withhold payment on the specific account in question and request that the card issuer investigate and resolve the dispute.</p>
							
						<b>Common billing errors include:</b>
							
							<li>Unfamiliar charges</li>
							<li>Clerical errors</li>
							<li>Fraud</li>
							
						<p><b>Disputing by Phone</b><br />Many billing errors can be resolved by calling the customer service number on the back of your credit card. Half of all disputes are due to consumers not recognizing a charge. One reason this is so prevalent is because the merchant identifiers listed on the billing statement often use the corporate name rather than the familiar store front name.</p>
							
						<p>Many clerical errors can also be cleared up by telephone. Cases of fraud are usually resolved quickly because the card issuer wants to maintain customer confidence.</p>
							
						<p><b>Disputing by Letter</b><br />Other billing errors require a dispute letter. In this case, the credit-card issuer must receive the letter within 60 days from the date on the statement in question. Usually, there is an address for disputes listed on the monthly statement. Include copies of all documents supporting the dispute.</p>
							
						<p>The issuer then has 30 days to acknowledge receipt of the dispute letter and 90 days to resolve the matter.</p>
							
						<p>According to Consumer Reports, the number of people who dispute credit-card charges is in the millions. The cost to the creditor is at least $25 to process each dispute.</p>
							
						<b>Billing errors that may require a dispute letter are:</b>
							
						<p><b>Unauthorized charges. </b>These include automatic charges you authorized when, for example, you signed up for a cable service but have since canceled.</p>
							
						<p><b>Cramming.</b> This is when a credit-card issuer tacks on additional charges such as insurance or other services without your knowledge. Sometimes you accept the additional charges by not signing a fine-print refusal or bill stuffer.</p>
											
						<p><b>Goods not delivered. </b>Request proof that the item was delivered. This means a signature of receipt — not a computer-generated delivery confirmation.</p>
											
					</fieldset>	
						 
					<fieldset id="space">
						<legend><b>THE BILLING DISPUTE LETTER</b></legend>
						
						<p>Here is a sample letter for disputing a billing error. Use the following checklist as a guide for what to include in the letter:</p>
						
							<li>Address it to the specific department that handles billing errors.</li>
							<li>Identify the cardholder and the account number.</li>
							<li>Give the date the statement was received.</li>
							<li>Describe the problem.</li>
							<li>Request evidence of the charge.</li>    
							    
						<p>Be sure to make copies of all correspondence to creditors. When disputing an amount from a transaction, be sure to include a copy of the receipt. It is also a good idea to keep records of all letters sent with the name of the creditor, the date it was sent, and the reason for the dispute.</p>    
							    
						<p><b>Sample Letter for Disputing a Billing Error</b></p>	    
							
						<p>
							Date<br />
							Your name<br />
							Your ID number<br />
							Your address<br />
							Your telephone number<br />
							Your Social Security Number<br />
							Your date of birth
						</p>	
						<p>
							Name of creditor<br />
							Billing Inquiries<br />
							Address<br />
							City, State, Zip Code
						</p>								
							
						<p>Dear Sir or Madam:</p>	
							
						<p>I am writing to dispute a billing error in the amount of $______ on my account. The amount is inaccurate because [describe the problem]. I am requesting that the error be corrected, that any finance charge and any other charges related to the disputed amount be credited to my account and that I receive a new and accurate statement.</p>	
							
						<p>Enclosed are copies of documents [describe any enclosed information such as sales slips or payment records] supporting my position. Please investigate this matter and correct the billing error as soon as possible.</p>	
							
						<p>
							Sincerely,<br />
							Your name<br />
							Enclosures: [list what you are enclosing]
						</p>
											
					</fieldset>	
						
					<fieldset id="space">
						<legend><b>AVOID LATE PAYMENTS</b></legend>
							
											
						<p>A payment is past due even if it is one day late. Therefore, it is a good idea to make payments at least one week prior to the due date. Late payments not only incur penalty fees but they also give lenders the opportunity to increase finance charges. If that wasn’t enough, late payments lower credit scores.</p>
											
					</fieldset>	
						 
					<fieldset id="space">
						<legend><b>HOW TO CANCEL A CREDIT CARD</b></legend>
							
						<p>If you don’t want a particular credit card anymore, don’t just toss it in the trash. Here is the right way to cancel an account:</p>
						
						<ol>
							<li>Notify the card issuer by telephone.</li>
							<li>Follow up by notifying the card issuer in writing.</li>
							<li>Get a copy of your credit report and make sure that it accurately records the cancellation — that is, that the cancellation was at the cardholder’s request.</li>
							<li>Repeat steps 1 – 3 if necessary.</li>
						</ol>	

							<b>The cancellation steps in detail:</b>
							
							<p>Call the telephone number on the back of the credit card to be canceled. Confirm the status of the account and make sure the balance is 0. It is recommended that you pay off the balance first.</p>
							
							<p>Notify customer service of the desire to cancel. Hold firm! They will try to keep your business by promising lower rates or special deals. Always get the name of the customer service representative as well as the name of the person to whom you should address correspondence.</p>
							
							<p>Write a short letter to a specific person and state the following:</p>
							
							  <li>I am closing the account.</li>
							  <li>I want my credit report to show that it has been canceled by me.</li>
							
							<p>Give your name, address, and account number. Send by certified mail or return receipt requested.</p>
							
							<p>Allow 30 days for the account to be closed. Then, request a copy of your credit report from all three credit bureaus. Make sure it reads ”Closed at customer’s request.” Make sure it does not read ”Closed by creditor” as that would negatively affect the credit report.</p>
							
							<p>If the report says that the account was closed by the creditor, the cardholder must start all over again. Call customer service and explain the mistake. Send a follow-up letter with a copy of the original letter. Check the credit report again after 30 days.</p>
							
							<b>Reasons for canceling include the following:</b>
							<ol>
								<li>To lowerpotential debt. The amount of unused credit available may lower a credit score.</li>
								<li>To eliminate the temptation of plastic and easy access to funds.</li>
								<li>To close out a card that was a bad deal.</li>
							</ol>
				
					</fieldset>	
						
					<fieldset id="space">
						<legend><b>IDENTITY THEFT</b></legend>
						
						<p>The Federal Trade Commission acknowledges identity theft as a growing dilemma. Experts estimate that there are hundreds of thousands of victims each year. Every 79 seconds, a thief steals someone’s identity, opens accounts in a victim’s name and goes on a buying spree.</p>
							
						<p>Credit-card fraud accounts for fifty percent of identity theft. Victims reported that a credit card was opened in their name or that unauthorized charges were placed on their existing credit card.</p>	
							
						<p>Eighty percent of the victims do not know the perpetrators while the remaining twenty percent could identify the fraudsters.  Thirty-five percent of identity theft victims notice the theft within one month. However, some victims are unaware for up to five years! The average time it takes for victims to notice that someone has snatched their identities is 14 months. That’s plenty of time to devastate a consumer’s finances and credit rating.</p>	
							 
							
						<b>Tips for Avoiding Identity Theft</b>
							
							<li>Protect your bills and credit cards by keeping track of your mail and the physical whereabouts of all your credit cards.</li>
							<li>Frequently review credit-card and bank statements for unauthorized charges.</li>
							<li>Guard your credit-card number. Do not give your number over the phone unless it is a company you have done business with before.</li>
							<li>Do not use your credit card to make Internet purchases unless the website has a secure server.</li>
							<li>Be wary of e-mails asking for personal information to update files.</li>
				
					</fieldset>	
						 
					<fieldset id="space">
						<legend><b>SUMMARY</b></legend>
						
						<ol>
							<li>Credit cards should not be viewed as an extension of income or as a substitute for savings.</li>
							<li>An important part of using credit wisely is to read and fully understand the monthly billing statement.</li>
							<li>Communicating with creditors is essential in order to resolve billing errors and to ask about any changes that appear in the terms and conditions of the credit-card account.</li>
							<li>Purchases made with credit cards should not include everyday expenses such as groceries. Cash advances should be avoided.</li>
							<li>Responsible consumers monitor their credit report at least once every year.</li>
							<li>Consumers should avoid making only the minimum payments each month. If paying off the account in full each month is unrealistic, it is best to arrive at a fixed amount to pay each time the billing statement arrives.</li>
							<li>Consumers have the right to dispute billing errors under the Fair Credit Billing Act.</li>
							<li>There is a systematic way to cancel unwanted credit cards so that the cancellation is reported correctly by the credit bureaus.</li>
							<li>Identity theft has become increasingly problematic for consumers. Always be wary of telephone or online offers that ask for personal information.</li>
							<li>Avoiding identity theft means keeping track of you mail, your credit-card and bank statements, and the whereabouts of your credit cards at all times.</li>
						</ol>
				
