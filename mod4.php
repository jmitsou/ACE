					<p>Credit can be a good thing. The ability to receive funds, goods, and services in the present and pay for them over time presents us with vast opportunities that otherwise would be extremely difficult to realize. Buying a home, a car, paying for higher education, starting a business would be out of reach for most people if it weren’t for the availability of consumer-friendly loans and lines of credit.</p>

					<p>Credit also offers a convenient way to do business. You dont need to carry around large amounts of cash. Credit cards allow you to reserve a hotel room in a far off city, and once there, to rent a car and pay for emergency services if need be. The credit card offers a sense of security when traveling.</p>

					<p>However, credit has a dark side. For the uninitiated, the credit card can be a gateway into a debilitating debt situation of late fees and collection notices. Stress and frustration trigger family conflicts while credit card balances grow and bank accounts shrink. Current trends indicate that consumers are using a higher percentage of their disposable incomes toward credit card debt at the expense of savings. In fact, credit card debt is the fastest growing form of consumer debt.</p>	
						
