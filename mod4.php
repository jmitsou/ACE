						<legend><b>CAMBRIDGE SURVEY</b></legend>
					
					
					<p>If credit is such a powerful financial tool, then why are so many Americans experiencing it negatively? Part of the answer might be found in a survey conducted by Cambridge Credit Counseling Corporation. They asked prospective credit counseling clients:</p>

					<p>What was the main reason or recent event that made you realize you need help with your unsecured debt?</p>

					<p>Frustration with high bank rates and fees was the number one reason cited for seeking credit counseling. This reason was given by nearly 30% of respondents while lack of financial education was cited by about 10%.</p>

					<p>What does this tell us? Given the fact that people generally do not like to put all the blame for their debt squarely on their shoulders, frustration with high bank rates and fees can be seen as a way for consumers to point their fingers at lenders as, at least, part of the problem. After all, many lending agreements today are often far from consumer-friendly.</p>

					<p>However, a lot of the frustration that consumers are experiencing is a direct result of lack of financial education. If consumers knew about the credit system and how to manage their credit to their advantage, those high interest rates and fees would not be there in the first place.</p>
					
					</fieldset>
					
					<fieldset id="space">
						<legend><b>CREDIT LITERACY</b></legend>
					
						<p>Educating your clients about credit is an extremely important part of counseling the financially stressed. The goal of counseling is not to get people to stop using credit even though they may need to stop credit card use for the short term. Ultimately, the goal is to help clients get credit and use credit the smart, informed way so they can avoid excessive debt in the future.</p>

						<p>Establishing good credit, maintaining good credit habits, and knowing the best ways to resolve credit-card debt are all topics counselors and clients alike need to learn more about.</p>
						
